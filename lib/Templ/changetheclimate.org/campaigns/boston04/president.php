<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Decriminalization 
  of Marijuana</strong></font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><img src="../../images/boston04_caption.jpg" alt="This marijuana reform ad can be seen as you leave Logan Airport and in other areas around the Boston during the Democratic Party's national convention this month." width="300" height="200" hspace="3" vspace="3" align="right"> 
  <strong>GEORGE W BUSH</strong><br>
  Bush has not voiced any support for decriminalizing the possession of marijuana 
  for personal use and crime statistics from the height of his administration 
  demonstrate his lack of tolerance. According to NORML, the 2002 FBI Uniformed 
  Crimes Report shows that 697,082 people were arrested for drug violations in 
  2002, accounting for almost half of the total drug arrests in the entire nation. 
  Of those 697,082 people, 88% of them were arrested for possession-only offences 
  while the remaining arrests where charged with &#8220;sale/manufacture,&#8221; 
  while even included those who were producing it privately for medicinal uses. 
  Under Bush&#8217;s presidency, &#8220;Operations Pipe Dreams&#8221; and &#8220;Operation 
  Headhunter&#8221; have both been enacted by the Drug Enforcement Agency, spearheaded 
  by Attorney General John Ashcroft. Both target businesses that sell or distribute 
  glass pipes, or other smoking paraphernalia. In 2003 alone, 50 individuals, 
  including actor Tommy Chong, were indicted, resulting in 3 years in prison and/or 
  a $250,000 fine, depending upon the indictment. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>SENATOR KERRY</strong><br>
  Senator Kerry has voiced mild support for the decriminalization of marijuana. 
  In a November 2003 Rolling Stone article, he declares he is &#8220;not quite&#8221; 
  for the legalization of the drug, but describes his &#8220;unspoken&#8221; approach 
  to prosecuting it. &#8220;What we did in the prosecutor's office was have a 
  sort of unspoken approach to marijuana that was almost effectively decriminalization. 
  We just didn't bother with small-time use. It doesn't rise to the level of nuisance, 
  even. And what we were after was people dealing with heroin and destroying lives, 
  and people who were killing people. That's where you need to focus,&#8221; Kerry 
  told Rolling Stone At the November 4, 2003 &#8220;Rock the Vote Debate, aired 
  on CNN, Kerry did admit to using smoking marijuana. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>DAVID COBB</strong> 
  <br>
  According to his response to the National Political Awareness Test, Cobb would 
  support legalizing &#8220;small amounts of marijuana.&#8221; He is also quoted 
  in the NPAT as saying, &#8220;The &quot;war on drugs&quot; is actually a war 
  on our civil liberties. It is also a war on people . . . most of whom are poor, 
  young, and disproportionately people of color.&#8221; On his election site, 
  Cobb says he adheres to the principles and platform of the Green Party, which 
  also supports the decriminalization of small amounts of marijuana, something 
  they define as a &#8220;victimless crime.&#8221; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>MICHAEL BADNARIK</strong><br>
  According to Project Vote Smart&#8217;s National Political Awareness Test, Badnarik 
  would support decriminalizing small amounts of marijuana and eliminate federal 
  funding for the so-called &#8220;war on drugs.&#8221; NORML&#8217;s candidate 
  comparison information says that specific to marijuana, Badnarik says: &quot;Personally, 
  I have never smoked a marijuana cigarette -- a fact most people find hard to 
  believe for someone who attended college in the early 70's. Why are these people 
  in jail? ... By releasing the half million people in jail for marijuana, we 
  would have more than enough room to imprison all the violent criminals for their 
  entire sentence, rather than releasing them early because of overcrowding.&quot; 
  According to his official campaign website, &#8220;On a fundamental level, Libertarians 
  believe that it is the unalienable and constitutional right of individuals to 
  medicate themselves and choose for themselves what to put into their bodies, 
  as long as they live up to the consequences of their actions. The federal government 
  has no proper say in the matter, and state governments violate the rights of 
  the people in their own attempts to enforce morality.&#8221; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>RALPH NADER</strong><br>
  In the 2000 presidential election, Nader went on record and supported the decriminalization 
  of marijuana for personal use. On his official campaign website, Nader says 
  &quot;Law enforcement should be at the edges of drug control not at the center. 
  It is time to bring some illegal drugs within the law by regulating, taxing 
  and controlling them. Ending the drug war will dramatically reduce street crime, 
  violence and homicides related to underground drug dealing.&quot; </font></p>
<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Medical Marijuana 
  </strong></font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>GEORGE W. BUSH</strong><br>
  In an article that ran in an October 1999 edition of the Washington Post, Bush 
  says he supports allowing decisions regarding the medicinal use of marijuana 
  to be left to each state. According to Americans for Safe Access, Bush has made 
  no mention of the issues since taking office. However, under his administration, 
  raids on medicinal marijuana patients where states have legalized its use have 
  continued. In September of 2002, a federal raid was conducted by the Drug Enforcement 
  Agency against the Wo/Men&#8217;s Alliance, a medicinal marijuana collective 
  in Northern California, that destroyed over 160 plants intended for use by chronically 
  ill or debilitated patients recommended the treatment by physicians. The raid 
  was in direct violation of Proposition 215, which legalized the use of medicinal 
  marijuana in the state of California. In January of 2003, Ed Rosenthal, who 
  grew pot for medicinal purposes at the request of the city of Oakland, was tried 
  and convicted for violating federal drug laws. However, during the trial, the 
  judge permitted no mention of medicinal marijuana or Proposition 215. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>JOHN KERRY</strong><br>
  According to numerous campaign speeches, Kerry favors the legal access to marijuana 
  for seriously ill patients and a federal study to determine the appropriate 
  federal policy. Kerry co-authored (along with fellow MA senator Ed Kennedy) 
  a letter to the Drug Enforcement Agency in October of 2003, expressing his strong 
  support for the application of the University of Mass.- Amherst to become a 
  bulk manufacturer of marijuana for distribution to researchers at the Food and 
  Drug Administration and DEA-licensed laboratories. The letter also encourages 
  research into the therapeutic effects high-grade, research quality marijuana 
  will have on diseases like glaucoma and AIDS. Kerry voiced his opposition at 
  the January 2004 New Hampshire College Convention for federally prosecuting 
  medical marijuana patients who reside in states where its use is legal. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>DAVID COBB</strong><br>
  Cobb does not address the issue directly on either his website or the Project 
  Vote Smart&#8217;s National Political Awareness Test. However, the Green Party 
  platform opposes &#8220;the arrest, harassment or prosecution of anyone involved 
  in any aspect of the production, cultivation, transportation, distribution or 
  consumption of medicinal marijuana, as well as the harassment, prosecution or 
  revocation of license of any health care provider who gives a recommendation 
  or prescription for medicinal marijuana.&#8221; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>MICHAEL BADNARIK</strong><br>
  According to his campaign website, Badnarik views Bush&#8217;s refusal to permit 
  medicinal marijuana as an &#8220;outrage.&#8221; He continues, saying &#8220;The 
  decision to ingest, smoke or consume any drug should be up to the individual, 
  under the advice of his or her physician, when appropriate. Locking people up 
  for trying to relieve their pain is cruel and unusual punishment for an act 
  that hurts no one.&#8221; According to the website, Badnarik does not agree 
  with the use of the federally approved marijuana substitute Marinol, because 
  while it contains the psychoactive THC, it &#8220;lacks other cannibinoids crucial 
  to marijuana as an effective medicine.&#8221; However, on the National Political 
  Awareness Test, Badnarik did not support any issues relating to medicinal marijuana. 
  </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>RALPH NADER</strong><br>
  Nader has not made his stance on this issue public.</font></p>
<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Repealing the 
  Drug Offender Exclusionary Provision from the Federal Higher Education Act </strong></font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>GEORGE W. BUSH</strong><br>
  Under Bush, the Department of Education was instructed to enact and enforce 
  a provision in the 1998 federal Higher Education Act that bars students with 
  drug convictions from receiving aid. The provision prevents any student with 
  convictions of marijuana possession or sales from receiving federal student 
  aid for periods ranging from one year to indefinitely. According to NORML, approximately 
  50,000 citizens have been denied federal student aid annually under the Bush 
  presidency. <br>
  <br>
  <strong>JOHN KERRY</strong><br>
  Kerry told Students for Sensible Drug Policy in January he would be in favor 
  of repealing the drug use penalty currently in place for drug felons, &#8220;if 
  the offence is use [of drugs]&#8221; but not &#8220;if the offence is selling.&#8221; 
  </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><br>
  <strong>MICHAEL BADNARIK</strong><br>
  Badnarik has not taken a public position regarding the repeal of a 1998 provision 
  to the federal Higher Education Act. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>DAVID COBB</strong><br>
  Cobb, nor the Green Party, have taken a stance on the repeal of the 1998 provision 
  to the federal Higher Education Act.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>RALPH NADER</strong><br>
