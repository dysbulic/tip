<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The true cost of marijuana 
  prohibition is difficult to obtain, and since the amount of money is staggering 
  by any measure the government is not interested in having this information widely 
  available. Change the Climate has used the cost of marijuana prohibition as 
  one of our main arguments for the need to change laws. Our advertising campaigns 
  in <a href="/campaigns/">Washington DC and Nevada</a> highlight the costs associated 
  with the war on marijuana.</font> 
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> The economic downturn 
  has had a major impact on the funds available to cities and towns to pay for 
  teachers, police, fire protection services and many other essential services 
  we have come to rely on in our daily lives. In an effort to see how much money 
  could be saved and raised if marijuana was controlled and made legally available 
  in one state - Massachusetts - we commissioned <a href="/bu-study/">a study</a> 
  by Boston University Economics Professor Jeffery Miron.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Professor Miron used 
  a complicated formula to determine the costs - borne 100% by taxpayers in Massachusetts 
  - associated with enforcing marijuana laws. Professor Miron used conservative 
  estimates and still came up with $121 million in savings to taxpayers. Most 
  of the savings were the result of reduced time and expense in the criminal justice 
  arena in the Commonwealth. You can read and download Professor Miron's report 
  at <a href="/bu-study/">www.changetheclimate.org/bu-study</a>.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Even though Massachusetts, 
  like other states, would not be permitted to regulate marijuana and make it 
  available to adults through controlled and legal distribution, we asked Professor 
  Miron to consider the revenue that might be available to the state IF it was 
  allowed to develop a system of legal distribution similar to alcohol. The $17 
  million in new tax revenue that Professor Miron estimates would be available 
  is based on similar taxes generated alcohol sales. There are many unknowns but 
  the message is clear - Massachusetts' taxpayers could save many millions of 
  dollars if marijuana arrests ceased and the state decided to tax the sale of 
  what many consider to be the state's number one cash crop.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Our hope is that <a href="/bu-study/">Professor 
  Miron's report</a> and <a href="/campaigns/08_03_dc/ma.php">our related advertising 
  campaign</a> will stimulate debate among citizens and lawmakers to consider 
  alternatives to this massive waste of taxpayer dollars. We think parents want 
  marijuana less accessible to our kids and more resources (teachers, programs, 
  facilities) available to educate students. Cities and towns could hire 3,450 
  more teachers, police and firefighters based on this economic analysis (assuming 
  $40,000 salaries). If taxpayers had the opportunity to hire dozens more professionals 
  to meet the local service needs of their towns instead of arresting and prosecuting 
  hundreds of non-violent marijuana smokers, we think they would jump at the chance.</font></p>
