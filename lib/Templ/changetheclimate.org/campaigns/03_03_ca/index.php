<p><font face="Verdana, Geneva, Tahoma, Arial" size="+1"><b>About Proposition 
  215</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">On November 5, 1996 California 
  voters approved Proposition 215, which allows seriously ill patients to legally 
  use medical marijuana to help ease their pain and suffering. The law also protects 
  caregivers and doctors who are assisting patients.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Since the passage of 
  Prop. 215 the United States Justice Department and Drug Enforcement Administration 
  (DEA) have showed total disregard for this law and trampled on the rights of 
  voters in California. People with cancer, AIDS, and other serious ailments who 
  use medical marijuana to help ease their pain and suffering are being aggressively 
  pursued and prosecuted by the federal government. Federal judges overseeing 
  many of these cases are forbid people being prosecuted from making any mention 
  of Proposition 215 or medical marijuana to the jury -- effectively making them 
  look like &quot;drug kingpins&quot;.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It's time to change the 
  climate in California around medical marijuana. These seriously ill patients 
  need to concentrate on their health and not worry about being prosecuted in 
  federal court for taking a medicine recommended by their doctor. More importantly, 
  the federal government needs to show respect for the will of California voters 
  and bring an end to these senseless raids and arrests.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Finally, jurors in medical 
  marijuana cases should understand their right to vote their conscience -- a 
  right that has been part of our judicial system since the founding of the republic.</font></p>
