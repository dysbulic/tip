                                                  <li><b>Fact #2</b> Marijuana use by kids, coupled with other
                                                  drug use and behavioral problems, can be a sign that
                                                  a child needs professional attention. </li><p>

                                                <li><b>Fact #3</b> 90% of kids who try marijuana don't go on to
