<a name="5"></a>
<font face="verdana, geneva, tahoma, arial">
<b>Myth #5: Marijuana leads to harder drugs (the "gateway theory").</b></font><p>

<font face="verdana, geneva, tahoma, arial" size="-1">
<ul><li><b>
Fact #1</b> Over 70 million people have tried marijuana.
                                                  Most marijuana users never use any other illegal drug.
                                                  Indeed, for the vast majority of people, marijuana is
                                                  the last drug they try, not a "gateway" to other
                                                  drugs. If it were a gateway drug and if it were so
                                                  addictive, we would have more than 3 million heroin
                                                  and cocaine addicts in the U.S.</li> <p>

                                                  <li><b>Fact #2</b> Marijuana is the most popular illegal drug in
                                                  the United States today. Therefore, people who have
                                                  used less popular drugs such as heroin, cocaine and
                                                  LSD are likely to have also tried marijuana. </li></ul></font>

