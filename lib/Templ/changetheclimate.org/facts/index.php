<a name="1"></a>
<font face="verdana, geneva, tahoma, arial">
<b>Myth #1: Marijuana use has been scientifically proven to be really harmful.</b></font><p>

<font face="verdana, geneva, tahoma, arial" size="-1">
<ul>
<li><b>Fact #1</b> In 1972, after reviewing the scientific
                                                  evidence, the National Commission on Marijuana and
                                                  Drug Abuse concluded that while marijuana is not
                                                  entirely safe, its dangers had been grossly
                                                  overstated.</li><p>

                                                  <li><b>Fact #2</b> In 1995, based on thirty years of scientific
                                                  research, editors of the British journal Lancet (the
                                                  British equivalent of New England Journal of Medicine)
                                                  concluded that "the smoking of cannabis, even long
                                                  term, is not harmful to health." </li></ul></font>
