<font face="verdana, geneva, tahoma, arial">
<b>Myth #10: Marijuana use is a major cause of highway accidents.</b></font><p>

<font face="verdana, geneva, tahoma, arial" size="-1">
<ul><li><b>
Fact #1</b> There is no compelling evidence that
                                                  marijuana contributes substantially to traffic
                                                  accidents and fatalities. In driving studies, marijuana
                                                  produces little or no car-handling impairment -
                                                  consistently less than that produced by low to
                                                  moderate doses of alcohol and many legal
                                                  medications.</li><p> 

                                                  <li><b>Fact #2</b> People should not drive while under the
                                                  influence of marijuana. At some doses, marijuana
                                                  affects perceptions and psychomotor performance. </li></ul></font>

