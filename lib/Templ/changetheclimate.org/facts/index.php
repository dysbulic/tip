<font face="verdana, geneva, tahoma, arial">
<b>Myth #7: Marijuana causes crime. Under the influence of marijuana, people become irrational, aggressive, and violent. </b></font><p>

<font face="verdana, geneva, tahoma, arial" size="-1">
<ul><li><b>
Fact #1</b> Every serious scholar and government
                                                  commission examining the relationship between
                                                  marijuana use and crime has reached the same
                                                  conclusion: Marijuana does not cause crime. The vast
                                                  majority of marijuana users do not commit crimes.
                                                  Almost all human and animal studies show that
                                                  marijuana decreases aggression.</li></ul></font>

