<a name="8"></a>
<font face="verdana, geneva, tahoma, arial">
<b>Myth #8: Marijuana can cause infertility and retards sexual development in adolescents.</b></font><p>

<font face="verdana, geneva, tahoma, arial" size="-1">
<ul><li><b>
Fact #1</b> There is NO evidence that marijuana causes
                                                  infertility in men or women. Most studies of humans
                                                  have found that marijuana has no impact on sex
                                                  hormones. In those studies showing an impact, it is
                                                  modest, temporary, and of no apparent consequence
                                                  for reproduction.</li><p> 

                                                 <li><b>Fact #2</b> There is NO scientific evidence that
                                                  marijuana delays adolescent sexual development, has
                                                  a feminizing effect on males, or a masculinizing effect
                                                  on females. </li></ul></font>
