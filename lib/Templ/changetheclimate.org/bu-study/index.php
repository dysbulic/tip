<p><b><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> I. Introduction</font></b></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Government prohibition 
  of marijuana is the subject of ongoing debate. Advocates of this policy believe 
  prohibition reduces marijuana trafficking and use, thereby discouraging crime, 
  improving productivity and increasing health. Critics of this policy believe 
  prohibition has only modest effects on trafficking and use while contributing 
  to many problems often attributed to marijuana itself. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> One issue in this debate 
  is the effect of marijuana prohibition on government budgets. Prohibition entails 
  direct enforcement costs, and prohibition prevents taxation of marijuana production 
  and sale. If marijuana were legal, enforcement costs would be zero and governments 
  could levy taxes on the production and sale of marijuana. Thus, government expenditure 
  would decline and tax revenue would increase. A key question is the magnitude 
  of these budgetary impacts.<br>
  <br>
  This report estimates the expenditure savings and revenue gains that would result 
  from marijuana legalization in Massachusetts. The report is not an overall evaluation 
  of marijuana prohibition; the magnitude of any budgetary impacts does not by 
  itself determine the wisdom of prohibition. But the costs required to enforce 
  prohibition, and the transfers that occur because income generated in the marijuana 
  sector is not taxed, are relevant to rational discussion of this policy.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> The policy change analyzed 
  here is more substantial than marijuana decriminalization, under which governments 
  repeal criminal penalties against possession but retain them against trafficking. 
  The budgetary implications of legalization exceed those of decriminalization 
  for three reasons. First, legalization eliminates arrests for trafficking in 
  addition to eliminating arrests for possession. Second, legalization saves prosecutorial, 
  judicial, and incarceration expenses; these are minimal in the case of decriminalization. 
  Third, legalization allows taxation of the marijuana trade, assuming federal 
  policy accommodates this goal. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> This report concludes 
  that legalization of marijuana in Massachusetts would produce an annual savings 
  in state and local expenditure of about $120.6 million while generating tax 
  revenue of at least $16.9 million. These estimates necessarily rely on assumptions 
  in cases where appropriate data are not available. But in most instances these 
  assumptions bias the estimated effects downward, so the actual costs savings 
  and revenue gains are plausibly higher than suggested here.<br>
  <br>
  Although this report estimates both the cost savings and the revenue gains from 
  legalization, these components of legalization's budgetary impact need to be 
  interpreted differently. It is possible for a given state to reduce or eliminate 
  enforcement of its own marijuana laws without permission from the federal government. 
  It unlikely, however, that an individual state could tax marijuana unless the 
  federal government also legalized marijuana, since persons producing and selling 
  marijuana would be hesitant to conduct such activity above ground if marijuana 
  were still prohibited under federal law. Thus, the revenue gains are not solely 
  under control of state governments but are nevertheless interesting to consider.</font></p>
