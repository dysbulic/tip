    political advertisements on the basis of their purported guidelines. Third, 
    the statute defined its &quot;harmful to minors&quot; standard so as to exclude 
    political and other protected speech. See id. By contrast, the MBTA's purported 
    standard is nowhere defined in the guidelines. Fourth, and finally, the statute 
    at issue in Ginsberg defined a minor as a person under the age of 17. See 
    id. at 865-66. Again, the MBTA guidelines are devoid of a definitions section 
    and the defendants assert that the definition of juveniles includes riders 
    from the ages of 9 to 18, thus including college age students old enough to 
    vote. <br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It is clear from the 
    opinion that the court in Mood for a Day felt bound by no precedent at all. 
    In fact, the court was quite candid in stating that its holding was made from 
    whole cloth. See 953 F. Supp. at 1259 (&quot;[T]he court finds no direct precedent 
    from the United States Supreme Court, the Tenth Circuit Court of Appeals, 
    any other federal court of appeals or any other federal court decision.&quot;).<br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It is therefore not 
    surprising that no court in the country has cited Mood for a Day for its regressive 
    central premise - viz., that the government has the inherent right to silence 
    advocacy of unlawful conduct, no matter how remote, in any forum it creates. 
    <br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The court went on 
    to find that a fact issue remained as to whether the plaintiff's speech satisfied 
    the court's completely novel standard under which the government may ban speech 
    if it &quot;to a greater or lesser degree&quot; advocates illegal conduct. 
    See id. at 1267. <br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> It is ironic that 
    the defendants, in support of their position that the First Amendment is no 
    barrier to suppressing the political message of Change the Climate, have submitted 
    what amounts to a Brandeis brief, which eschews relevant legal authority in 
    favor of reliance on the opinions of social science and marketing experts. 
    Justice Brandeis, the Massachusetts jurist, surely would not approve of an 
    attempt to foreclose vigorous political debate in this manner. His concurring 
    opinion in Whitney v. California, 274 U.S. 357 (1927), became the template 
    for our modern Brandenburg jurisprudence. A short excerpt is illuminating:<br>
    <br>
    Those who won our independence by revolution were not cowards. They did not 
    fear political change. They did not exalt order at the cost of liberty. To 
    courageous, self reliant men, with confidence in the power of free and fearless 
    reasoning applied through the processes of popular government, no danger flowing 
    from speech can be deemed clear and present, unless the incidence of the evil 
    apprehended is so imminent that it may befall before there is opportunity 
    for full discussion. If there be time to expose through discussion the falsehood 
    and fallacies, to avert the evil by the processes of education, the remedy 
    to be applied is more speech, not enforced silence. <br>
    <br>
    Id. at 377 (Brandeis, J., concurring). <br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> In addition to demonstrating 
    a compelling interest to justify their restriction on speech, the defendants 
    must also show that their policy is narrowly tailored to advance that interest. 
    The defendants' argument concerning narrow tailoring, however, is frivolous 
    and merits only a footnote. In essence, the defendants argue that - so long 
    as the government has the option of either allowing speech or completely suppressing 
    speech - any action taken by the government is by definition narrowly tailored. 
    See Def's Memo. at 35. If this were true, there would be no narrow tailoring 
    requirement, for it would be satisfied by any outright ban on speech. Instead, 
    it is well established that narrow tailoring refers, not only to the degree 
    to which the speech is limited, but also to the process by which the government 
    separates proscribed speech from allowable speech. Where the government's 
    policy censors more speech than is necessary to serve its compelling interest, 
    the policy offends the First Amendment. See Simon &amp; Schuster, Inc. v. 
    Members of the N.Y. State Crime Victim's Bd., 502 U.S. 120-21 (1991). Similarly, 
    a policy fails the narrow tailoring inquiry if it does not reach all of the 
    speech necessary to advance the government's interest. See Florida Star v. 
    B.J.F., 491 U.S. 524, 540 (1989). Defendants make no attempt to justify their 
    vague, standardless policy, which is patently overinclusive (because it silences 
    political speech) and underinclusive (because it fails to limit demonstrably 
    harmful speech). This silence is fatal where &quot;the Government bears the 
    burden of proving the constitutionality of its actions.&quot; Playboy, 529 
    U.S. at 816.<br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> The plaintiff does 
    not dispute that the MBTA could lawfully reject an &quot;anti-&quot; or &quot;pro-&quot; 
    drug advertisement, that, for example, contains pornographic or obscene content. 
    But the distinction between the plaintiff's advertisements and those of the 
    Office of National Drug Control Policy is a difference in viewpoint alone. 
    The &quot;individual characteristics&quot; of advertisements may be considered, 
    but the viewpoints or messages expressed in those advertisements may not be 
    considered among those characteristics.<br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> The defendants' reliance 
    on AIDS Action for the notion that the mere existence of the MBTA's written 
    policy establishes the advertising space a nonpublic forum is dubious. See 
    Pl. Mem. at 11 n. 7 (discussing criticism of this very notion by other courts). 
    To reiterate here, courts have looked to the First Circuit rule that &quot;actual 
    practice speaks louder than words,&quot; Grace Bible Fellowship, Inc. v. Maine 
    Sch. Admin. Dist. No. 5, 941 F.2d 45 (1st Cir. 1991), and not a rule that 
    would render the designated public forum doctrine meaningless.<br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> The plaintiff has 
    extensively argued its contention that the MBTA has designated its advertising 
    space a public forum, and the evidence supporting that contention, in both 
    its initial memorandum and statement of facts, and relies on those discussions 
    here. See Pl. Mem. at 6-20and Pl. SOF at 6-8, 21-24. The defendants' denials 
    of that contention in their brief rest on empty assertions such as, &quot;Although 
    Change the Climate may contend that the advertising spaces in the MBTA's facilities 
    constitute a designated public forum subject to strict scrutiny, there is 
    no factual or legal support for such a contention&quot;, Def. Mem. at 29, 
    and &quot;Although the MBTA has opened designated areas of its facilities 
    for advertising, it has done so only to the extent the advertising conforms 
    to the viewpoint-neutral standards set out in the Appearance and Character 
    Guidelines,&quot; id. at 30. Even a cursory review of the record evidence 
    reveals these statements to be untrue. See Pl. SOF at 21-24; Pl. Resp. Def. 
    SOF, 14 and Ex. A. The defendants claim &quot;the undisputed facts reveal 
    that the MBTA did not intend for its advertising spaces to become a designated 
    public forum . . . . [t]hrough both its policy and practice&quot; Id. But 
    no &quot;undisputed facts&quot; are given in support. Instead, the defendants 
    admit that among the tens of thousands of advertisements submitted in the 
    last four years they have rejected &quot;at least eight,&quot; id. at 30, 
    as if to suggest that minute number evidences something other than the MBTA's 
    practice of accepting virtually all advertisements that come their way. Indeed, 
    the defendants' practice of rejecting ads is so infrequent as to make it a 
    non-practice. Cf. United Food, 163 F.3d at 353 (noting &quot;the rejection 
    of only six proposed advertisements under the Policy suggests that SORTA may 
    permit virtually unlimited access to its advertising space or grants permission 
    as a matter of course. . . &quot;). They also claim that &quot;the record 
    is devoid of evidence that any advertisements were accepted that violated 
    the Guidelines,&quot; id., which is blatantly false. See Pl. SOF. at 21-24, 
    Pl. Resp. Def. SOF, 14 and Ex. A. And they sum up their rendition of the evidence 
    by stating, &quot;In other words, the MBTA accepts only those advertisements 
    that comply with the Guidelines and rejects all advertisements that fail to 
    comply.&quot; Def. Mem. at 30. The defendants' fairy tale ending is not supported 
    by the record evidence that reveals the opposite to be true. Where the defendants 
    have rejected advertisements, they have done so on a subjective, ad hoc basis 
    which, on more than one occasion, has led to a sharp rebuke by the courts 
    of this circuit and district. See AIDS Action, 42 F.3d at 12-13; Citizens 
    to End Animal Suffering and Exploitation v. MBTA, C.A. No. 92-11118-MA (D. 
    Mass. June 5, 1992); Preterm, 1974 U.S. Dist. LEXIS 8555, at * 6. <br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> The plaintiff does 
    not quarrel with the notion that a transit authority that consistently limits 
    its advertising space to commercial messages has created a reasonable, viewpoint-neutral 
    policy that survives First Amendment scrutiny, as was the case in Lehman v. 
    City of Shaker Heights, 418 U.S. 298, 301-02 (1974) (holding no public forum 
    where authority had banned all political advertisements, subject to a consistently 
    enforced policy, for twenty-six years). Id. at 301-02. Accord Children of 
    the Rosary,154 F.3d at 978 (distinguishing designated public forum cases with 
    approval). Notably, Children of the Rosary is the only recent transit case 
    to which the defendants cite in their summary judgment memorandum. Def. Mem. 
    at 31. Their disingenuous reliance on this case and the earlier nonpublic 
    forum cases - at the exclusion of such seminal cases as Planned Parenthood, 
    and such recent cases as Christ's Bride and United Food, (see Pl. Mem. at 
    pp. 16-21) - is transparent. This Court, like the Christ's Bride court, should 
    stop the defendants in their tracks for attempting such reliance:<br>
    <br>
    In this case, however, unlike Kokinda and Lehman, the government has offered 
    no basis on which to conclude that the speech in question would interfere 
    with the accepted purposes of the advertising space. See Lee v. Intern. Soc. 
    for Krishna Consciousness, Inc., 505 U.S. 672, 691, 120 L. Ed. 2d 541, 112 
    S. Ct. 2701 (1992) (O'Connor, J. concurring) (&quot;because I cannot see how 
    peaceful pamphletting is incompatible with the multipurpose environment of 
    the Port Authority airports, I cannot accept that a total ban on that activity 
    is reasonable without an explanation as to why such a restriction 'preserves 
    the property' for the several uses to which it has been put. [citation to 
    Perry]&quot;); Airline Pilots Assoc. v. Dept. of Aviation, 45 F.3d 1144, 1161 
    (7th Cir. 1995) (Flaum, J. Concurring) (&quot;...when the government decides 
    who may speak based on substantive criteria, it acts as a censor. The government 
    should not normally take on the role of deciding who may speak on what matters, 
    regardless of what capacity in which it acts&quot;).<br>
    <br>
    148 F.3d at 256 (emphasis added). The defendants have not engaged in the lawful 
    conduct at issue in Lehman, Kokinda, and Children of the Rosary. Instead, 
