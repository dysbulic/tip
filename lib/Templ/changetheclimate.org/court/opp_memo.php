    hoc, ideologically motivated judgments to restrict speech depending upon the 
    viewpoint of the speaker. This is the cardinal sin against the First Amendment. 
    The defendants' reliance on the Lehman line of cases is unwarranted; accordingly, 
    their reasonableness analysis that relies on Lehman, and denies the primary 
    purpose of the forum, should be disregarded.</font></li>
