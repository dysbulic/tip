<center>
  <font face="Verdana, Geneva, Tahoma, Arial"><b>MEMORANDUM OF LAW IN SUPPORT 
  OF PLAINTIFF CHANGE THE CLIMATE, INC.'S OPPOSITION TO DEFENDANTS' MOTION SUMMARY 
  JUDGMENT </b></font> 
</center>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>I. INTRODUCTION.</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' linchpin 
  argument - that the plaintiff's proposed advertisements violate explicit MBTA 
  guidelines that prohibit advertisements containing &quot;criminal content&quot;- 
  rests on a on a false and misleading factual premise. The MBTA has no such written 
  policy. The actual record evidence is that the guidelines prohibit only advertisements 
  containing &quot;violent criminal content.&quot;<sup><a href="#1">1</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Once this linchpin is 
  removed, the defendants' argument unhinges. No one contends - not Lucy Shorter, 
  not Robert Prince, not the defendants' purported experts - that the advertisements 
  contain content that could be interpreted as violent and criminal. Moreover, 
  even under their own misrepresentation of the guidelines as proscribing advertising 
  containing &quot;criminal content,&quot; the defendants' argument still fails. 
  To speak about marijuana issues is, of course, not &quot;criminal.&quot; And 
  were the plaintiff's advertisements to actually advocate the breaking of existing 
  laws (a patently unreasonable interpretation of the advertisements), such advocacy 
  still could not be punished as &quot;criminal.&quot; Under longstanding Supreme 
  Court precedent, before speech can be abridged in the name of preserving law 
  and order, the government must bear the heavy burden of proving the speech at 
  issue was intentionally directed towards inciting imminent lawlessness.<sup><a href="#2">2</a></sup> 
  For the defendants to attempt to circumvent the rule of Brandenburg by way of 
  their purported guidelines and reliance on their peculiar reading of a lone 
  District Court of Utah case - without so much as even mentioning Supreme Court 
  precedent - is disingenuous to say the least. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' fall-back 
  argument, that the plaintiff's advertisements contain &quot;promotional materials&quot; 
  that are &quot;harmful to juveniles&quot; likewise fails. The advertisements 
  cannot reasonably be understood as &quot;promotional&quot; in the ordinary, 
  commercial sense of the word. Moreover, a standard that purports to prohibit 
  messages that are &quot;harmful to juveniles&quot; constitutes viewpoint discrimination 
  per se as applied to political speech, and is unconstitutionally vague and overbroad 
  on its face. This has been borne out in practice, as evidenced by the defendants' 
  rejection of the plaintiff's proposed media campaign on one hand, and their 
  unapologetic posting of advertisements for alcohol and other promotional materials 
  that are harmful to children, on the other.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The MBTA has designated 
  its advertising space a public forum in which Change the Climate, Inc., like 
  any other advertiser willing to pay the fee, has the right to have its proposed 
  advertisements processed without discrimination in order to be displayed on 
  MBTA property. By the defendants' own key admissions and the overwhelming record 
  evidence reflecting the MBTA's actual practices over the last decade to date, 
  the government's intent to create a designated public forum is undeniable, and 
  the defendants' protests to the contrary in the stance of litigation do not 
  alter that evidence. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The conceded interest 
  the government has in protecting children is not served by squelching debate 
  on marijuana issues. By denying the plaintiff its right to promote debate on 
  controversial social and political issues, the defendants have turned up their 
  noses at the tradition of deliberative democracy.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Before discussing each 
  of the defendants' legal arguments more extensively, the plaintiff addresses 
  two preliminary matters: (1) the factual red herring that the defendants did 
  not &quot;officially&quot; reject the plaintiff's proposed advertisements; and 
  (2) the defendants' purported expert testimony that should be stricken from 
  the record as inadmissible.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>II. DEFENDANTS' CLAIM 
  THAT THE PLAINTIFF'S ADVERTISEMENTS WERE NEVER &quot;OFFICIALLY&quot; REJECTED 
  IS A RED HERRING. </b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">At several points throughout 
  their brief, the defendants claim that the plaintiff never made a &quot;formal 
  advertising application&quot; and that the advertisements at issue were never 
  &quot;formally&quot; rejected. (See Memorandum in Support Defendants' Motion 
  for Summary Judgment (&quot;Def. Mem.&quot;) at 6, 8-9, and 22 n.9.) Although 
  the defendants never fully articulate what they believe to be the legal significance 
  of the absence of a &quot;formal&quot; rejection, their repeated incantation 
  of this claim compels the plaintiff to respond. If nothing else, the defendants' 
  repeated and strongly worded insistence that they would reject the advertisements 
  even if they had been submitted by way of a &quot;formal&quot; application reveals 
  their argument to be a red herring and the Court should disregard it as immaterial 
  to the analysis of the plaintiff's claims.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The evidence of the handling 
  of Change the Climate's application is largely undisputed. At the direction 
  of Elissa Albertelli of Park Transit Display, White submitted Change the Climate 
  materials in May of 1999 and again in January of 2000. Shorter acknowledges 
  she received and reviewed these materials, which included information on the 
  plaintiff organization as well as proposed advertisements. See Plaintiff's Response 
  to Defendants' Statement of Facts, (&quot;Pl. Resp. Def. SOF&quot;), 27. In 
  response, White received a fax from Albertelli which read:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Regretfully, I've 
  attached the response from the Transit Authority regarding 'Change the Climate' 
  campaign. Their reasons for not accepting this campaign are clearly stated in 
  the info. provided.&quot;</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Plaintiff's Statement 
  of Undisputed Facts (&quot;Pl. SOF&quot;), at Ex. K. Attached to the cover sheet 
  was a fax from Shorter stating: </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">See attached. The Change 
  the Climate promotes the use of marijuana is a suttle [sic] way and also is 
  really a reform marijuana [sic] in a [sic] effort to legalize? The T's appearance 
  &amp; character, Policy &amp; Drug - Alcohol policy conflict with their mission.&quot; 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Pl. SOF at Ex. L (emphasis 
  added). The defendants do not dispute White received these two documents.<sup><a href="#3">3</a></sup> 
  Following his receipt of this response, White spoke with Albertelli, who made 
  no mention of any appeal process White could pursue. See Pl. Resp. Def. SOF, 
  47. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants make much 
  out of the fact that Prince, the supposed final decision-maker, never viewed 
  the advertisements, but the sole reason for this was Shorter's failure to forward 
  them to him. White followed Albertelli's instructions to the letter and that 
  it was Shorter who short-circuited the process. The defendants also fault White 
  for failing to submit to a &quot;give-and-take process&quot; with MBTA administrators 
  after receiving the faxes from Albertelli and Shorter, in which he could have 
  changed the content of the advertisements until the MBTA was satisfied that 
  they &quot;fall within the Guidelines.&quot;<sup><a href="#4">4</a></sup> White 
  was never informed that such a process was available to him. Indeed, no formal 
  appeal process was available to anyone. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The faxes of Shorter 
  and Albertelli are plainly evidence that the MBTA rejected Change</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">the Climate's proposed 
  advertisements.<sup><a href="#5">5</a></sup> Albertelli, an agent authorized 
  to speak for the MBTA with respect to the advertising, stated unambiguously 
  that the defendants were &quot;not accepting&quot; the proposed ads. Albertelli's 
  later self-serving testimony does not alter the plain meaning of her words. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The undisputed evidence 
  is that it was the defendants' own actions - not those of Joe White - that made 
  what occurred between them a parody of the application process. White dutifully 
  followed the steps that Albertelli laid out for him. He contacted, met, followed-up 
  with, and forwarded materials to Albertelli upon her requests to do so. He called 
  her to express his frustration at Shorter's delay in responding to the materials 
  she had received about Change the Climate months prior. White cannot be responsible 
  for ensuring that Shorter delivered the proposed advertisements to Prince. Nor 
  can he be responsible for failing to discover application appeals that did not 
  exist or were never disclosed to him. The MBTA told White to go home; he cannot 
  be condemned for then failing to beat down their door.<br>
  Furthermore, even assuming that the plaintiff submitted only to the &quot;dry-run&quot; 
  application process, rather than the &quot;formal&quot; one, the defendants 
  have stated unequivocally that, because of the advertisements' content, &quot;had 
  the Ads been formally submitted without change, the MBTA would have rejected 
  them.&quot; (See Def. Mem. at 8, n.7.) There can be no clearer statement of 
  futility. See Brett v. Jefferson County, 123 F.3d 1429, 1433 n. 9 (11th Cir. 
  1997) (&quot;[T]he failure to apply is irrelevant to a First Amendment claim 
  if the application would have been futile.&quot;).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Ultimately, a footnote 
  in the defendants' brief makes clear the true purpose of the defendants' gambit. 
  The defendants state: &quot;In support of its claim of viewpoint discrimination, 
  Change the Climate may seek to rely on Lucy Shorter's fax to Albertelli. However, 
  there is no evidence that Shorter's response was a final decision on the part 
  of the MBTA.&quot; (Def. Mem. at 22, n.9.) The smoke and mirrors concerning 
  &quot;formal&quot; application processes and mere dry-runs thus amounts to an 
  elaborate effort by the defendants to distance themselves from Shorter's patently 
  viewpoint-hostile rejection of the advertisements, and from Albertelli's cover 
  letter that communicated her understanding of Shorter's fax as exactly that. 
  The reality is that Shorter rejected Change the Climate's ads, Albertelli told 
  White the ads had been rejected, and the MBTA admits that if these same advertisements 
  were resubmitted today they would be rejected again. That continuing refusal 
  to run Change the Climate's advertisements is what this case is all about. If 
  the MBTA were to now say it would run the ads, this case would screech to a 
  halt.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>III. THE DEFENDANTS' 
  EXPERT TESTIMONY IS NOT ADMISSIBLE, AND EVEN IF IT WERE, THEIR OPINIONS ARE 
  OF NO LEGAL CONSEQUENCE.</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">A. The experts' testimony 
  violates the helpfulness requirement and should be stricken in their entirety 
  as inadmissible.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As argued in Plaintiff's 
  Motion to Strike, the proposed affidavits of Drs. Goldberg and Kleber violate 
  the fundamental requirement of Fed. R. Evid. 702 that expert testimony must 
  be helpful in order to be admissible. Fed. R. Evidence 702. See Pl. Motion to 
  Strike, 5(c). First, the Court is fully competent to read simple political advertisements,<sup><a href="#6">6</a></sup> 
  and no amount of expert testimony will assist in this relatively mundane task. 
  See 4 J. Weinstein &amp; M. Berger, Weinstein's Federal Evidence &sect; 702.03[2][a], 
  at 702-33 (2nd ed. 2001) (&quot;Expert testimony is generally not permitted 
  concerning factual issues that are within the knowledge and the experience of 
  ordinary lay people . . . .&quot;). The advertisements are not a secret code, 
  and the defendants' paid cryptographers are therefore unnecessary. Second, the 
  affiants' attempts to alter the unambiguous text of the plaintiff's advertisements 
  (e.g., &quot;smoking pot is not cool&quot;) to suggest an entirely different 
  message (e.g., &quot;marijuana isn't so bad&quot;) also violate Rule 702's helpfulness 
  standard.<sup><a href="#7">7</a></sup> See United States v. Dicker, 853 F.2d 
  1103, 1109 (3rd Cir.1988) (&quot;the interpretation of clear statements is not 
  permissible, and is barred by the helpfulness requirement of both Fed. R. Evid. 
  701 and Fed. R. Evid. 702.&quot;) (emphasis in original). Just as parol evidence 
  is inadmissible to alter the plain meaning of an unambiguous contract in the 
  commercial context, so too are the defendants' attempts to adulterate the plain 
  language of the plaintiff's advertisements to suit the defendants' purposes 
  in this case.<sup><a href="#8">8</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">B. The experts' attempt 
  to impute secret meaning to the plain text of the advertisements is utterly 
  at odds with First Amendment analysis.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' experts' 
  reinterpretations of the words of the plaintiff's advertisements subverts the 
  purpose of the First Amendment. This can never be more true than when core political 
  speech is at issue. A recent Ninth Circuit decision speaks directly to this 
  point: </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">If political discourse 
  is to rally public opinion and challenge conventional thinking, it cannot be 
  subdued. Nor may we saddle political speakers with implications their words 
  do not literally convey but are later &quot;discovered&quot; by judges and juries 
  with the benefit of hindsight and by reference to facts over which the speaker 
  has no control.<br>
  <br>
  Planned Parenthood v. American Coalition of Life Activists, 244 F.3d 1007, 1019 
  (2001) (emphasis added); see also Herceg v. Hustler Magazine, 814 F.2d 1017, 
  1019-20 (5th Cir. 1987) (&quot;Under our Constitution, as the Supreme Court 
  has reminded us, there is no such thing as a false idea. However pernicious 
  an opinion may seem we depend for its correction not on the conscience of judges 
  and juries but on the competition of other ideas.&quot;) (quoting Gertz v. Robert 
  Welch, Inc., 418 U.S. 323, 339-40 (1974)). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It cannot be disputed 
  that the advertisements at issue do not literally convey a message that anyone 
  smoke marijuana. In fact, one of the advertisements at issue explicitly states 
  &quot;smoking marijuana is not cool&quot; (&quot;Teen Ad&quot;); another says 
  &quot;I don't want my kids smoking marijuana,&quot; (&quot;Mother Ad&quot;); 
  and all three of the advertisements direct the reader to the plaintiff's web 
  site, www.changetheclimate.org, which repeatedly and unambiguously conveys the 
  explicit message that &quot;marijuana is NOT for kids.&quot; (emphasis on Home 
  page, attached to Pl. SOF, White Aff. at Tab 1.)<sup><a href="#9">9</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Notwithstanding the plain 
  text of the advertisements, Dr. Goldberg, in eerily Orwellian fashion, states 
  that the plaintiff's use of such &quot;devices&quot; as colloquial language, 
  plural pronouns, capitalization, and the image of a backwards baseball cap serve 
  to transform the unambiguous text of the &quot;Teenager Ad&quot; (&quot;Smoking 
  pot is not cool, but we're not stupid, ya know. Marijuana is NOT cocaine or 
  heroin&quot;) into an entirely contrary message, namely that &quot;many other 
  juveniles are thinking about using marijuana&quot; and &quot;marijuana isn't 
  so bad.&quot; Id. at 17(a)-(f). He makes similar claims with regard to the remaining 
  CTC advertisements: the plain language of the &quot;Police Ad&quot; (&quot;Police 
  are too important &#133; too valuable &#133; too good &#133; to waste on arresting 
  people for marijuana when real criminals are on the loose&quot;) becomes &quot;marijuana 
  users &#133; will not be sought after by the police,&quot; id. at 18(a)-(b); 
  and the plain language of the &quot;Mother Ad&quot; (&quot;I've got three great 
  kids. I love them more than anything. I don't want them to smoke pot. But I 
  know jail is a lot more dangerous than smoking pot&quot;) becomes &quot;many 
  juveniles use marijuana&quot; and &quot;using marijuana is not that dangerous,&quot; 
  id. at 19(a)-(c). Dr. Goldberg surmises that the overall message of the CTC 
  advertisements is &quot;using marijuana is okay&quot; and &quot;it is not a 
  big deal&quot;. Id. at 20. Not surprisingly, Dr. Kleber reaches similar conclusions. 
  According to him, the &quot;Teenage Ad&quot; really says &quot;juveniles are 
  knowledgeable about and comfortable with marijuana use.&quot; Id. 5. The &quot;Mother 
  Ad&quot; really says &quot;many of [juveniles'] peers are using marijuana.&quot; 
  Id. 6.<sup><a href="#10">10</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The Court must recognize 
  the supposed expert opinions of Drs. Goldberg and Kleber for what they are: 
  outright attempts to ascribe to the plaintiffs' advertisements &quot;implications 
  their words do not literally convey.&quot; Planned Parenthood, 244 F.3d at 1018-19 
  (noting &quot;important theoretical objections&quot; to &quot;stretching context&quot; 
  to supply a message &quot;that language alone leaves out&quot;).<sup><a href="#11">11</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">C. Even if the experts' 
  testimony were admissible, their opinion that children might misperceive the 
  plaintiff's advertisements is of no legal consequence.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The affidavit of Dr. 
  Goldberg describes at length how those &quot;who are unlikely to be motivated 
  to pay full attention to the transit advertising&quot; Goldberg Aff., 8, will 
  misinterpret the three advertisements at issue, as not sending a &quot;clear 
  law reform message.&quot; Id. at 4. Likewise, Dr. Kleber opines that the advertisements 
  are &quot;misleading&quot; in the impressions they convey. Kleber Aff, 8.<sup><a href="#12">12</a></sup> 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Assuming arguendo the 
  admissibility of expert testimony in this case, Drs. Goldberg and Kleber's opinions 
  that children and people who do not pay attention might misperceive the text 
  of the plaintiff's advertisements - regardless of the content of the message 
  - is of no legal significance. The First Amendment does not require messages 
  to be &quot;clear&quot; or that their appeal be understood by everyone, including 
  children, the inattentive, and the misinformed. Indeed, courts recognize that 
  &quot;we must defer to the well-recognized principle that political statements 
  are inherently prone to exaggeration and hyperbole.&quot; Planned Parenthood, 
  244 F.3d at 1019 (citing Watts v. United States, 394 U.S. 705, 707 (1969) (per 
  curiam)) (&quot;The language of the political arena . . . is often . . . inexact.&quot;). 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In Lebron v. Washington 
  Metro. Transit Auth.,749 F.2d 893 (D.C. Cir. 1984), the court addressed the 
  very situation presented here: the display of provocative political advertisement 
  to the broad and diverse ridership of a metropolitan mass transit system.<sup><a href="#13">13</a></sup> 
  The panel of judges (Bork, Scalia and Starr, JJ.) took the D.C. Metro to task 
  for attempting to censor the plaintiff's political posters on the grounds that 
  they were &quot;misleading&quot;:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">That some small number 
  of careless readers might be misled by this poster changes neither our inquiry 
  nor our conclusion. Speakers are not required to indulge the lowest common denominator 
  of the populace; First Amendment protection is not limited only to messages 
  which every reader, no matter how ill-informed or inattentive, can comprehend.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Id. at 896 (emphasis 
  added) (citations omitted); see also id. at 898 (&quot;In light of the 'profound 
  national commitment to the principle that debate on public issues should be 
  uninhibited, robust, and wide-open' . . . courts ought not to restrain speech 
  where the message sought to be communicated is political and is 'sufficiently 
  ambiguous to allow a discerning viewer' (or reader) to recognize it as something 
  other than a reproduction of an actual event.&quot;) (quoting New York Times 
  v. Sullivan, 376 U.S. 254, 270 (1964)). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Just recently, in Good 
  News Club v. Milford Cent. Sch., 121 S.Ct. 2093, 2106 (2001), the Court spoke 
  directly to this issue in the context of the First Amendment's Establishment 
  Clause: </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">We cannot operate, as 
  [the defendants] would have us do, under the assumption that any risk that small 
  children would perceive endorsement should counsel in favor of excluding the 
  Club's religious activity. We decline to employ Establishment Clause jurisprudence 
  using a modified heckler's veto, in which a group's religious activity can be 
  proscribed on the basis of what the youngest members of the audience might misperceive. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Id. at 2106 (emphasis 
  added). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Lebron and Good News 
  are but two cases in the long and undisturbed line of cases that renders the 
  defendants' purported expert testimony in this case meaningless. Indeed, just 
  weeks ago, the Supreme Court reiterated, emphatically, that &quot;the governmental 
  interest in protecting children from harmful materials . . . does not justify 
  an unnecessarily broad suppression of speech addressed to adults.&quot; Lorillard 
  Tobacco Co. v. Reilly,121 S.Ct. 2404, 2426 (2001) (quoting Reno v. American 
  Civil Liberties Union, 521 U.S. 844, 875 (1997) (citations omitted)); see also 
  Bolger v. Youngs Drug Prods. Corp., 463 U.S. 60, 74-75 (&quot;The level of discourse 
  reaching a mailbox simply cannot be limited to that which would be suitable 
  for a sandbox.&quot;); Butler v. Michigan, 352 U.S. 380, 383, 77 S.Ct. 524, 
  525, 1 L.Ed.2d 412 (1957) (declaring unconstitutional a statute &quot;quarantining 
  the general reading public against books not too rugged for grown men and women 
  in order to shield juvenile innocence&quot; because its effect was &quot;to 
  reduce the adult population ... to reading only what is fit for children&quot;). 
  Accord Denver Area Ed. Telecommunications Consortium, Inc. v. F.C.C., 518 U.S. 
  (1996); Sable Communs. of Calif. v. F.C.C., 492 U.S. 115, 127 (1989).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Boiled to their essentials, 
  the defendants' experts' opinions are that, at worst, the advertisements may 
  subtly condition juveniles to view marijuana more favorably. Judge Easterbrook 
  of the Seventh Circuit disposes of the defendants' reliance upon such opinions 
  most efficiently:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">But almost all cultural 
  stimuli provoke unconscious responses. Religious ceremonies condition their 
  participants. Teachers convey messages by selecting what not to cover; the implicit 
  message about what is off limits or unthinkable may be more powerful than the 
  messages for which they present rational argument. Television scripts contain 
  unarticulated assumptions. People may be conditioned in subtle ways. If the 
  fact that speech plays a role in a process of conditioning were enough to permit 
  governmental regulation, that would be the end of freedom of speech.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">American Booksellers 
  Ass'n, Inc. v. Hudnut, 771 F.2d 323, 330 (7th Cir. 1985) (Easterbrook, J.) (emphasis 
  added.), aff'd without opinion, 475 U.S. 1001 (1986).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">IV. THE PLAINTIFF'S PROPOSED 
  ADVERTISEMENTS DO NOT VIOLATE THE MBTA'S ADVERTISING GUIDELINES.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">A. The defendants' primary 
  legal argument is based upon a false factual premise and therefore should be 
  disregarded.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' begin 
  their legal argument section as follows: </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">By promoting the illegal 
  use of marijuana in a manner harmful to juveniles, the plaintiff's three proposed 
  Advertisements violate two provisions of the MBTA's haracter and Appearance 
  Guidelines: the prohibition on 'criminal' content and the prohibition on advertisements 
  promoting material that is 'harmful to juveniles'. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Def. Mem. at 11. (emphasis 
  added.) As an initial matter, the evidence is undisputed that the guidelines 
  to which the defendants refer are bid specifications that govern the relationship 
  between the MBTA and the successor contractor to Park Transit Display as of 
  July 2000, six months after the plaintiff submitted its advertisements for approval 
  in January 2000. See Pl. Resp. Def. SOF, 15; Pl. Motion to Strike, 5(a)(2). 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">But far more importantly, 
  even assuming, for the purpose of summary judgment, that the bid specifications 
  were in effect at the time the plaintiff's advertisements were submitted, the 
  defendants' legal argument is still premised on their utterly untruthful contention 
  that the &quot;established guidelines&quot; prohibit advertisements containing 
  &quot;'violent' or 'criminal' content.&quot; It is undisputed that the actual 
  language of the policy reads as follows:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The MBTA will not accept 
  advertisements containing violent criminal content, firearms, profane content, 
  promotional materials that is [sic] harmful to juveniles, and<br>
  advertisements that denigrate groups based on gender, religion, race, ethnic 
  or political affiliation for display in and upon the Authority's transit facilities.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The plain language of 
  the policy applies to content that is both violent and criminal. The standard 
  is not disjunctive (i.e. violent or criminal), and to assert otherwise is beyond 
  reason.<sup><a href="#14">14</a></sup> </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As argued in Plaintiff's 
  Motion to Strike and discussed in Plaintiff's Response to Defendants' Statement 
  of Facts, the defendants' premise is a false and misleading one, and all references 
  to it should be stricken from the record. See Pl. Motion to Strike, 5(a)(1); 
  Pl. Resp. Def. SOF, 19. Because the defendants cannot dispute that the plaintiffs' 
  advertisements do not fall within the rubric of the actual standard, one that 
  unambiguously purports to prohibit violent and criminal content, the Court should 
  disregard the defendants' primary legal argument.<sup><a href="#15">15</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">B. Even by the defendants' 
  own misrepresentation of the MBTA guidelines, the advertisements cannot be construed 
  as &quot;containing criminal content&quot;or be punished as such. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' bold 
  assertion that because marijuana use is currently unlawful, they have no obligation 
  to post the plaintiff's political advertisements as &quot;containing criminal 
  content&quot; is untenable. This very argument was rejected by the Preterm Court 
  almost thirty years ago when the MBTA relied upon it as justification for rejecting 
  advertisements concerning abortion clinic services prior to Supreme Court's 
  landmark Roe v. Wade decision. See Preterm, Inc. v. MBTA, C.A. No. 74-159-M,1974 
  U.S. Dist. LEXIS 8555, at * 6 (D. Mass. May 13, 1974). The court recognized 
  the argument for the tautology it was: &quot;It is apparent to the court that 
  almost any 'public interest' advertisement could be perceived to disseminate 
  information about activities which might violate criminal laws.&quot; Id. Indeed, 
  by a definition that prohibits advertisements &quot;containing criminal content&quot; 
  most of the public service advertisements currently on display on MBTA buses, 
  trains, and stations can only be understood as in violation the guidelines; 
  for example, advertisements concerning alcohol products (criminal for underage 
  children), domestic violence (both violent and criminal), gay domestic violence 
  (violent and criminal<sup><a href="#16">16</a></sup>), gambling (criminal), 
  euthanasia and suicide prevention (violent and criminal<sup><a href="#17">17</a></sup>), 
  anti-drug (criminal), and anti-drug help lines for pregnant woman (criminal). 
  See Aunkst Aff., attached to Pl. SOF as Exhibit 6. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Furthermore, the defendants' 
  fear-mongering interpretation of the plaintiff's proposed advertisements is 
  an unreasonable one. Worse, it plays into the current climate of hysteria that 
  continues to silence debate and foreclose discussion of alternatives.<sup><a href="#18">18</a></sup> 
  Such tactics have been criticized by several distinguished members of the bench. 
  See Hon. James P. Gray, Why Our Drug Laws Have Failed and What We Can Do About 
  It: A Judicial Indictment of the War on Drugs, 4, et seq. (Temple University 
  Press 2001) (&quot;The only real enemy to change is the decades of rhetoric 
  that prohibit an open and honest discussion of our options and equate these 
  discussions with the condoning of drug use.&quot;); Hon. Juan R. Torruella, 
  The &quot;War On Drugs&quot;: One Judge's Attempt At A Rational Discussion, 
  14 Yale J. on Reg. 235, 267 n.2 (1997) (issuing &quot;One last caveat. Do not 
  in any way misinterpret anything that I write in this essay as an endorsement 
  of any type of drug use, legal or illegal. Nothing could be further from my 
  personal beliefs. Mine is but an attempt at rational discussion of what I understand 
  to be essentially a health and social problem, rather than a criminal one.&quot;); 
  Hon. Nancy Gertner, Is the Drug War Forever?, Remarks at Voluntary Committee 
  of Lawyers, Inc. forum, Boston (Jan. 29, 1998) (&quot;Not criminalizing drugs 
  doesn't mean we approve of it, anymore that not criminalizing tobacco means 
  that we approve of cigarette smoking or not criminalizing alcohol means we approve 
  of it. Just because something isn't criminal doesn't mean it is right. And yet 
  that's the way the debate has been cast. If you are against criminalization, 
  you are encouraging use.&quot;); Hon. J. T. Curtin,&quot;Drug Policy Alternatives 
  - A Response From The Bench,&quot; 28 Fordham Urb. L.J. 263, 272 (2000) (deriding 
  the &quot;popular tendency to equate advocacy of drug legalization or softening 
  of drug laws with advocacy of drug use&quot; as &quot;an absurd notion&quot;); 
  Hon. Martin L. Haines, Drug War: America's War of Self-Deception, N.J.L.J., 
  June 21, 1993, at n. 20 (&quot;The decades-long indoctrination of the public 
  in the need for a drug war as the only solution to the very serious problem 
  of drugs . . . . has prevented the consideration of any clearly necessary, intelligent 
  alternatives . . . . Few public officials dare to advance alternatives. Doing 
  so risks the loss of the next election, or the next appointment.&quot;). By 
  equating political speech that questions our current drug policies with advocacy 
  of drug use, the defendants feed the paranoia that demonizes the mere consideration 
  of alternatives to a &quot;war&quot; on drugs. It has no place in a court of 
  law.<sup><a href="#19">19</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Finally, as argued in 
  a later section of this brief, see pp. 21-28, infra., even if plaintiff's advertisements 
  actually advocated breaking laws against marijuana use, this too, would be protected 
  speech under Brandenburg and its progeny.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">C. The defendants' fall-back 
  legal argument fails as a matter of law. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants argue 
  that the &quot;promotional materials that is [sic] harmful to juveniles&quot; 
  standard in its guidelines also applies to prohibit the plaintiff's advertisements. 
  The argument, however, is relegated to secondary status, no doubt, because the 
  defendants recognize that the term &quot;harmful to juveniles&quot; is so amorphous 
  as to do nothing to curb the unbridled discretion of MBTA administrators in 
  accepting or rejecting advertisements.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Once again, to accept 
  the defendants' rendition of their guidelines requires a tweaking of the actual 
  language to suit their arguments. This time, they paraphrase the guideline as 
  &quot;the prohibition on advertisements promoting material that is 'harmful 
  to juveniles'&quot;, Def. Mem. at 11, when the policy actually states, &quot;The 
  MBTA will not accept advertisements containing . . . promotional materials that 
  is [sic] harmful to juveniles. To make an obvious point, the language in the 
  policy only targets &quot;promotional materials.&quot; On its face, the term 
  &quot;promotional materials&quot; refers to commercial advertisements and not 
  political advertisements such as Change the Climate's advertisements, which 
  are in no sense promotional as the term is ordinarily understood.<sup><a  href="#20">20</a></sup> 
  Nonetheless, absent any definition section to the guidelines, or any other guidance 
  whatsoever, the defendants have construed the term so broadly as to apply to 
  the plaintiff's political advertisements. Cf. Reno v. American Civil Liberties 
  Union, 521 U.S. 844, 894 (1997) (discussing importance of defining &quot;harmful 
  to minors&quot; standard narrowly so as not to restrict protected speech). <br>
  As argued in plaintiff's memorandum in support of its summary judgment motion, 
  the &quot;harmful to juveniles&quot; standard, as applied to non-commercial 
  speech such as the plaintiff's advertisements, constitutes viewpoint discrimination 
  per se. There is no denying that when left to determine which political messages 
  will be harmful to children, MBTA officials must necessarily make a viewpoint-based 
  judgment about what laws and policies benefit the well-being of children. This 
  is a purely subjective and ideologically-driven task, and its subjective nature 
  has been borne out in practice. That the &quot;harmful to juveniles&quot; standard 
  is nothing more than an appealing vehicle for viewpoint discrimination is made 
  apparent by the MBTA's undisputed failure to reject advertisements for products 
  that pose significant and demonstrable harms to juveniles.<sup><a href="#21">21</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">First, the MBTA gladly 
  runs advertisements for producers of alcoholic beverages, even though alcohol 
  is the third largest cause of preventable deaths in the United States. See Lorillard,121 
  S.Ct. at 2439. (Thomas, J., concurring). All states prohibit the sale of alcohol 
  to those under age 21, yet a great deal of alcohol advertising (such as the 
  type displayed by the MBTA) is viewed by children. Id. It should come as no 
  surprise that a considerable body of evidence shows that exposure to alcohol 
  advertising is associated with underage drinking. Id. at 2439-40. Those who 
  begin drinking early are much more likely to become dependent on alcohol and 
  suffer its consequences.<sup><a href="#22">22</a></sup> Id. at 2440. Mayor Menino 
  considered the problem of binge drinking among minors so serious in this Commonwealth 
  that he appointed a commission to initiate a media campaign to address the issue. 
  See Plaintiff's SOF, Exhibit U. Yet, mysteriously, the defendants' &quot;harmful 
  to juveniles&quot; guidelines have never been used to categorically ban advertisements 
  promoting the consumption of alcohol. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Likewise, there is no 
  evidence the defendants have ever rejected an advertisement promoting the consumption 
  of fast-food, despite the obvious harm fast-food poses to juveniles' health 
  and welfare. Obesity is second largest contributor to mortality rates in the 
  United States.<sup><a href="#23">23</a></sup> See Lorillard, 121 S. Ct. at 2439. 
  This poses a serious public health concern because childhood obesity is a major 
  health problem in its own right and, more importantly, because eating preferences 
  formed in childhood tend to persist in adulthood. Id. Yet, as with alcohol advertising, 
  the defendants have never categorically rejected fast-food advertisements under 
  the &quot;harmful to juveniles&quot; standard.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">There is &quot;no principle 
  of law or logic that would preclude the imposition of restrictions on fast food 
  and alcohol advertising&quot; under the MBTA's harmful to juveniles standard, 
  id. at 2440, yet the MBTA has never placed restrictions on such advertising. 
  The simple reason for the MBTA's inconsistent application of standards is that 
  it relies on neither law nor logic, but on the snap judgments of its policy 
  administrators. This is repugnant to the First Amendment. The undisputed evidence 
  that the MBTA accepts advertisements that have a demonstrably harmful effect 
  on juveniles yet claims it rejected the plaintiff's advertisements based on 
  a crass speculation that they might promote harmful activities gives rise to 
  a strong - and in this case unrebutted - inference of viewpoint discrimination.<sup><a href="#24">24</a></sup> 
  See AIDS Action Committee of Massachusetts v. MBTA, 42 F.3d 1, 12-13 (1st Cir. 
  1994) (holding MBTA liable for violation of First Amendment rights based on 
  unrebutted inference of viewpoint discrimination), affirming and modifying 849 
  F. Supp. 79 (D. Mass. 1993) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>V. THE MBTA HAS NO 
  COMPELLING INTEREST IN SUPPRESSING POLITICAL SPEECH ABOUT MARIJUANA.</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The government's conceded 
  interest in protecting juveniles from harm &quot;does not justify an unnecessarily 
  broad suppression of speech addressed to adults.&quot; Reno v. American Civil 
  Liberties Union, 521 U.S. 844, 875 (1997) (distinguishing Ginsberg v. New York, 
  390 U.S. 629 (1968) and FCC v. Pacifica Foundation, 438 U.S. 726 (1968)).<sup><a href="#25">25</a></sup> 
  Furthermore, juveniles have First Amendment rights and are entitled to protection. 
  See Planned Parenthood of Central Missouri v. Danforth, 428 U.S. 52, 76 (1976) 
  (&quot;Constitutional rights do not mature and come into being magically only 
  when one attains the state-defined age of majority. Minors, as well as adults, 
  are protected by the Constitution and possess constitutional rights.&quot;); 
  see also Pl. Mem. at 26-28.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Nor do states' interests 
  in protecting children or enforcing its criminal laws trump the right of individuals 
  to advocate changes to existing laws or, in most circumstances, advocate violation 
  of those laws. See Brandenburg v. Ohio, 395 U.S. 444, 448-49 (1969) (holding 
  that government may only proscribe speech that is &quot;directed to inciting 
  or producing imminent lawless action&quot;); Erznoznik v. Jacksonville, 422 
  U.S. 205, 213-14 (1975) (&quot;Speech that is neither obscene as to youths nor 
  subject to some other legitimate proscription cannot be suppressed solely to 
  protect the young from ideas or images that a legislative body thinks unsuitable 
  for them.&quot;). Were it otherwise, the government could censor any controversial 
  speech or the teaching of the moral propriety or necessity of defying the law 
  since, as a general matter, violations of the law may tend to harm children. 
  The justification for the longstanding Brandenburg rule is simple:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The constitutional protection 
  accorded to the freedom of speech . . . is not based on the naVve belief that 
  speech can do no harm but on the confidence that the benefits society reaps 
  from the free flow and exchange of ideas outweighs the costs society endures 
  by receiving reprehensible or dangerous ideas.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Herceg v. Hustler Magazine, 
  Inc., 814 F.2d 1017, 1019 (5th Cir. 1987). Brandenburg and its progeny stand 
  as a strong barrier to the government's ability to interfere with the open and 
  robust workings of the public's deliberative process. Yet the defendants turn 
  a blind eye to the rule of Brandenburg, thus setting the stage for their nearly 
  exclusive reliance on a lone district court decision, Mood for a Day, Inc. v. 
  Salt Lake County, 953 F. Supp. 1252 (D. Utah 1995).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It is not surprising 
  that &quot;research by the defendants has uncovered only one court decision&quot; 
  that supports their position that the government can restrict speech that might 
  be misinterpreted to subtly suggest the promotion of unlawful activity. Def. 
  Mem. at 16. In spite of adverse precedent from the United States Supreme Court, 
  circuit courts of appeal, and a landslide of district courts, defendants would 
  have this Court adopt their peculiar reading of this lone district court case. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The plaintiff respectfully 
  submits that, according to well-established Supreme Court authority, Mood for 
  a Day was wrongly decided. In Mood for a Day, the court reached the unprecedented 
  conclusion that the Brandenburg rule was strictly limited to speech on private 
  property, see id. at 1259,<sup><a href="#26">26</a></sup> and that the government 
  may suppress any speech advocating illegal activity - no matter how remote - 
  where the forum at issue, which was open to &quot;all comers&quot;, was &quot;an 
  area to which the public generally had access to disseminate and receive a wide 
  variety of information&quot; and for which &quot;[n]o meaningful criteria existed 
  for allowing certain types of groups and disallowing others . . . .&quot; Id. 
  at 1261. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Whatever political allure 
  such a ruling may have, this Court is constrained to follow the holdings of 
  the United States Supreme Court and the First Circuit Court of Appeals, which 
  have never so limited Brandenburg and have never so denigrated an individual's 
  right to engage in robust political discussion.<sup><a href="#27">27</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' reliance 
  on Mood for a Day is entirely misplaced, not only because the case has no controlling 
  or persuasive authority, but also because Change the Climate's advertisements 
  - even adopting the defendants' patently unreasonable interpretations of them 
  - at the very most, &quot;subtly&quot; promote the use of marijuana to those 
  who may be &quot;vulnerable&quot; or &quot;susceptible&quot; to &quot;messages 
  that convey the impression that other juveniles use marijuana.&quot; Def. Mem. 
  at 18; Pl. SOF, Exhibit D (Shorter fax). The speech at issue in Mood for a Day 
  was far more direct. For example, one bumper sticker read &quot;Thank You for 
  Pot Smoking,&quot; while another read &quot;If You Don't Smoke Pot You Have 
  Shit for Brains.&quot; Id. at 1256. Crucially, the Mood for a Day court admitted 
  that even this speech could not be proscribed under Brandenburg because it plainly 
  did not incite imminent lawless conduct.<sup><a href="#28">28</a></sup> Id. 
  at 1258. This makes even more tenuous the MBTA's far-fetched claim that it may 
  ban Change the Climate's comparatively innocuous advertisements, which cannot 
  be construed as a direct exhortation to do anything except, perhaps, log on 
  to the Internet and visit the plaintiff's web site to learn more about the organization. 
  See High Ol' Times, Inc. v. Busbee, 456 F. Supp. 1035, 1040 (N.D. Ga. 1978) 
  (&quot;Reasonable minds could not differ, the requisite seriousness of threat 
  and imminence of action are not presented in any of the sample material presented 
  entered into evidence.&quot;), aff'd without opinion, 621 F.2d 141 (5th Cir. 
  1980).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Although the defendants 
  claim that Mood for a Day is the lone case &quot;on point,&quot; the plaintiff 
  suggests that High Ol' Times is much more so. This is because High Ol' Times, 
  affirmed without opinion by the Fifth Circuit Court of Appeals, faithfully applies 
  the teachings of the Supreme Court in reaching its legal conclusion that the 
  government may not censor marijuana-related speech that does not incite imminent 
  lawlessness. In High Ol' Times, the court addressed the constitutionality of 
  the Georgia &quot;Head Shop&quot; law, which restricted the sale of &quot;drug-related 
  printed material&quot; to minors. Id. at 1037. The court noted that several 
  of the &quot;books and magazines generally exalt drugs and hawk the appurtenances 
  of drug use.&quot; Id. at 1038. Nevertheless, the court rightly held that since 
  the materials did not incite imminent lawlessness, they were entitled to full 
  First Amendment protection. See id. at 1040 (&quot;[P]rinted matter either assailing 
  the state's controlled substances laws or extolling the attractions of certain 
  unlawful substances and glorifying the drug culture may not be generally restricted.&quot;). 
  Furthermore, the court held that minors had a protected interest in accessing 
  such materials and that the Head Shop law &quot;must therefore be struck down 
  as it would be struck down if it infringed the freedom of expression of adults 
  as well.&quot; Id. at 1042.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">High Ol' Times fits comfortably 
  within a long and venerable line of cases that follow Brandenburg in holding 
  that speech can only be proscribed when the speaker intends to incite lawlessness 
  and when the speech is likely to produce imminent disorder. See Hess v. Indiana, 
  414 U.S. 105, 108-09 (1973) (&quot;Under our decisions, 'the constitutional 
  guarantees of free speech and free press do not permit a State to forbid or 
  proscribe advocacy of the use of force or of law violation except where such 
  advocacy is directed to inciting or producing imminent lawless action and is 
  likely to incite or produce such action.'&quot;) (quoting Brandenburg) (emphasis 
  in quotation); see also Herceg, 814 F.2d at 1022-23 (holding that Brandenburg 
  barred claim that magazine caused harm to reader by encouraging auto-erotic 
  asphyxiation); Zamora v. Columbia Broadcasting Sys., 480 F. Supp. 199, 206 (S.D. 
  Fla. 1979) (same with regard to claim that television desensitized convicted 
  murderer to violent behavior); Yakubowicz v. Paramount Pictures Corp., 404 Mass. 
  624, 631-32 (1989) (same with regard to claim that film The Warriors incited 
  a gang to commit murder). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">One final point from 
  the plaintiff's memorandum in support of its summary judgment motion bears revisiting 
  here. See Pl. Mem. at 27 n. 17. An important principle that emerges from cases 
  following Brandenburg is that expert testimony which merely posits a tendency 
  for certain speech to predispose individuals to unlawful behavior is, as a matter 
  of law, insufficient to establish the state's purported interest in suppressing 
  the speech. As discussed in the plaintiff's initial memorandum, id. at 27-28, 
  the Seventh Circuit, in American Amusement, addressed a defendant's reliance 
  on psychological studies purporting to show a link between violent video games 
  and violent behavior. See 244 F.3d at 578. The court rejected the speculative 
  nature of the relationship posited by the studies, stating, &quot;[t]he studies 
  do not find that video games have ever caused anyone to commit a violent act 
  . . . .&quot; Id. (emphasis added). Likewise, in Eclipse Enterprises, Inc. v. 
  Gulotta, 134 F.3d 63 (2nd Cir. 1997), the proponents of a law banning the sale 
  of trading cards that contained violent images offered expert testimony which 
  attempted to show a link between the cards and violent behavior. See id. at 
  65-66. The court, however, rejected the testimony because it could not demonstrate 
  with any degree of certainty a causal link between the trading cards and actual 
  violent behavior. See id. at 68. See also Torres v. Herber, 111 F. Supp. 2d 
  806, 820 n.5 (W.D. La. 2000) (rejecting as a matter of law expert's speculation 
  that &quot;anti-law&quot; message of rap music caused violent behavior in juveniles); 
  Davidson v. Time Warner, No. Civ. A. V-94-006, 1997 WL 405907, at *21 (S.D. 
  Tex. Mar. 31, 1997) (rejecting as a matter of law the testimony of an expert 
  who claimed that listening to rap music made an individual &quot;more likely 
  to commit violence.&quot;). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The lesson from these 
  cases, providing an evidentiary perspective on the Brandenburg rule, is that 
  the defendants gain nothing by putting forward expert testimony that posits 
  only a speculative relationship between the advertisements at issue and violation 
  of the law at some remote time in the future.<sup><a href="#29">29</a></sup> 
  Fundamentally, the defendants' cannot silence Change the Climate's protected 
  speech by reciting the state's interest in protecting children or enforcing 
  the criminal laws. See Sund v. City of Wichita Falls, 121 F. Supp. 2d 530, 552 
  (N.D. Tex. 2000) (&quot;There is simply no interest, let alone a compelling 
  one, in restricting [juveniles'] access to non-obscene, fully-protected [speech] 
  solely on the basis of the majority's disagreement with [its] perceived message.&quot;) 
  (emphasis in original). Brandenburg, which protects from government censorship 
  all political speech that does not directly incite imminent lawless action, 
  compels this Court to disregard the defendants' attempts to seek a lower standard 
  of scrutiny for the government's conduct.<sup><a href="#30">30</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>VI. THE MBTA ADMITS 
  IT ENGAGED IN VIEWPOINT DISCRIMINATION.</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As the Supreme Court 
  stated in Rosenberger v. Rectors &amp; Visitors of Univ. of Va., 515 U.S. 819 
  (1995): </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The government must abstain 
  from regulating speech when the specific motivating ideology or the opinion 
  or perspective of the speaker is the rationale for the restriction. These principles 
  provide the framework forbidding the State from exercising viewpoint discrimination, 
  even when the limited public forum is one of its own creation.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Id. at 829 (1995) (citations 
  omitted). The defendants admit - as they must - that they cannot discriminate 
  against the plaintiff on the basis of viewpoint, regardless of the type of forum 
  created. Indeed, the defendants devote a long portion of their brief to a vain 
  attempt to dispel the viewpoint-hostile implication of its decision to reject 
  the Change the Climate advertisements while continuing to run advertisements 
  for the Office of National Drug Control Policy. See Def. Mem. at 19-24. The 
  plaintiff's position on the issue of viewpoint discrimination is briefed in 
  plaintiff's memorandum in support of its summary judgment motion, see Pl. Mem.15-21, 
  30-32, and need not be reproduced here. The plaintiff can also quickly dispense 
  with the defendants' lengthy and tortured analysis because, in a clumsy attempt 
  to dodge the import of the First Circuit's holding in AIDS Action, the defendants 
  admit to the very definition of viewpoint discrimination.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants, at the 
  conclusion of their contorted rendition of the AIDS Action holding, state:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Thus, AIDS Action does 
  not hold that the government may not differentiate among different speech on 
  the same topic, such as illegal drugs. Rather, it holds that the government 
  may not discriminate among speech that is equivalent for the purposes for which 
  the government seeks to exclude the speech. Otherwise, once the MBTA accepted 
  an ad on a particular subject, it would forever be obligated to accept all other 
  ads on the same subject regardless of the individual characteristics of those 
  ads. Such a reading of AIDS Action defies common sense. The simple distinction 
  Change the Climate misses is that its Ads promote breaking the law, while those 
  of the Office of National Drug Control Policy did not.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">(Def. Mem. at 26-27.) 
  (emphasis in original.) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As the First Circuit 
  well recognizes, such picking and choosing among views expressed on the same 
  topic - a practice to which the MBTA now freely admits - is viewpoint discrimination 
  itself.<sup><a href="#31">31</a></sup> See Berner v. Delahanty, 129 F.3d 20, 
  23 (1st Cir. 1997) (&quot;The essence of viewpoint-based discrimination is the 
  state's decision to pick and choose among similarly situated speakers in order 
  to advance or suppress a particular ideology or outlook.&quot;) (citations omitted). 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' overt 
  admission of viewpoint hostility may be best illustrated by a reading of Gay 
  Lesbian Bisexual Alliance v. Pryor, 110 F.3d 1543 (11th Cir. 1997). In Pryor, 
  the Eleventh Circuit addressed a First Amendment challenge by a gay and lesbian 
  group to an Alabama Law that prohibited public universities from providing funds 
  to any organization or group that &quot;fosters or promotes a lifestyle or actions 
  prohibited by the sodomy or sexual misconduct laws.&quot; Id. at 1545. In striking 
  down the law, the court stated:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">[The university's] limited 
  public forum does not prohibit discussion of the sodomy or sexual misconduct 
  laws in general. Rather, . . . [the university] prohibited funding to GLBA based 
  on the . . . unsupported assumption that GLBA promotes a violation of the sodomy 
  and misconduct laws. The statute discriminates against one particular viewpoint 
  because state funding of groups which foster or promote compliance with the 
  sodomy or sexual misconduct laws remains permissible. This is blatant viewpoint 
  discrimination.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Pryor, 110 F.3d at 1549 
  (emphasis in original). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">A comparison between 
  the statement of the court in Pryor and the justification set forth by the defendants 
  is striking. The MBTA attempts to justify its rejection of the Change the Climate 
  advertisements by asserting that the &quot;Ads promote breaking the law, while 
  those of the Office of National Drug Control Policy did not.&quot; Def. Mem. 
  at 27. The very conduct the Pryor Court admonishes as unlawful is the government's 
  effort to silence a group it perceives to &quot;promote[] a violation of the 
  ... laws&quot; while favoring &quot;groups which foster or promote compliance 
  with the . . . laws . . . .&quot; Pryor, 110 F.3d at 1549. This Court must reach 
  the same conclusion drawn by the court in Pryor: this is blatant viewpoint discrimination. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>VII. THE MBTA'S ADVERTISING 
  SPACE IS INDISPUTABLY A DESIGNATED PUBLIC FORUM. </b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants deny that 
  the MBTA's advertising space is a designated public forum even though by their 
  own key admissions, the advertising space is, in practice, open to all comers 
  who are willing to pay the fee, and even though their written policies have 
  never proscribed, and do not now proscribe, categories of speech in any meaningful 
  way so as to limit the forum.<sup><a href="#32">32</a></sup> Moreover, whatever 
  the defendants argue, they do not dispute that the MBTA has allowed the broadest 
  range of political and public interest advertisements to be displayed.<sup><a href="#33">33</a></sup> 
  In light of all this undisputed record evidence, the Court must conclude that 
  the nature of the forum is compatible with the political expression at issue 
  in this case, and that under all three prongs of intent under Cornelius v. NAACP 
  Legal Defense &amp; Educational Fund, Inc., 473 U.S. 788 (1985), the MBTA has 
  designated its advertising space a public forum. Id. at 802-03. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendants' assert 
  that for the plaintiff to claim that the MBTA has designated a public forum 
  for its advertising space &quot;defies common sense&quot; and &quot;rests on 
  a strained interpretation of the case law&quot; on public fora doctrine. Def. 
  Mem. at 27. Yet it is the defendants who virtually ignore the entire body of 
  recent transit advertising cases in their brief, not the plaintiff. See New 
  York Magazine v. Metropolitan Transport. Auth., 136 F.3d 123, 130 (2d Cir. 1998); 
  Christ's Bride Ministries, Inc. v. Southeastern Pa. Transp. Auth., 148 F.3d 
  242, 252-55 (3d Cir. 1998), cert. denied, 525 U.S. 1068 (1999); United Food 
  &amp; Commercial Workers Union Local 1099 v. Southwest Ohio Reg'l Transit Auth., 
  163 F.3d 341, 353 (6th Cir. 1998); Children of the Rosary v. City of Phoenix, 
  154 F.3d 972, 978 (9th Cir. 1998) (citing leading cases with approval); Planned 
  Parenthood Ass'n v. Chicago Transit Auth., 767 F.2d 1225, 1232 (7th Cir. 1985); 
  Lebron v. Washington Metro. Transit Auth.,749 F.2d 893, 896 (D.C. Cir. 1984). 
  Accord Air Line Pilots Ass'n Int'l v. Dept. of Aviation, 45 F. 3d 1144, 1153 
  (7th Cir. 1995); Hawkins v. City and County of Denver, 170 F.3d 1281, 1287 (10th 
  Cir. 1999) (citing Christ's Bride as instructive);; cf. Hopper v. City of Pasco, 
  241 F.3d 1067, 1078 (9th Cir. 2001); East Timor Action Network, Inc. v. City 
  of New York, 71 F. Supp. 334, 345-46 (S.D.N.Y. 1999); National Abortion Federation 
  v. Metropolitan Atlanta Rapid Transit Auth., 112 F. Supp.2d 1320, 1327 (N.D. 
  Ga. 2000). See Pl. Mem. at pp. 8-14 (discussing both designated public forum 
  and nonpublic forum cases). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As these cases make clear, 
  the defendants' denials that the primary purpose of the MBTA's advertising space 
  has been, and continues to be, to communicate information to the public should 
  be disregarded by the Court as contrary to the overwhelming record evidence 
  in this case. See United Food, 163 F.3d at 351 (&quot;A contrary rule that focused 
  solely on whether a speaker must obtain permission to access government property 
  would allow every designated public forum to be converted into a non-public 
  forum the moment the government did what is supposed to be impermissible in 
  a designated public forum, which is to exclude speech based on content.&quot;) 
  (internal citations and quotations omitted); New York Magazine,136 F.3d at 130 
  (noting that the very standard by which the transit authority attempted to justify 
  its restriction on speech, i.e., a general interest in upholding the law, actually 
  supported the conclusion that it was operating in its capacity as a regulator 
  as opposed to proprietor, &quot;unlike the exclusions that contributed to orderly 
  internal business procedures in Perry or Cornelius.&quot;); Christ's Bride, 
  148 F.3d at 251 (holding transit advertising space was designated a public forum 
  where standards for inclusion and exclusion were promulgated &quot;without reference 
  to the purpose of the forum&quot;); Planned Parenthood, 767 F.2d at 1233 (holding 
  transit authority estopped from arguing that public-issue advertising was incompatible 
  with the primary use of the advertising space in light of demonstrated practice 
  to allow public-issue and political advertising); cf. East Timor, 71 Supp. 2d 
  at 347 (&quot;[t]he fact that the purpose for creating the forum appears to 
  have been precisely to permit expressive activities (in contrast to, for example, 
  activities which raise funds for the state, facilitate communications within 
  a work environment, help keep highways clean at limited taxpayer expense, and 
  so forth), and the relatively broad access afforded members of the public (anyone, 
  so long as the proposed sign does not fall within one of the excluded categories), 
  support a finding that the DOT's policy has created a limited public forum.&quot;); 
  National Abortion Federation, 112 F.Supp.2d at 1236) (&quot;The nature of advertising 
  space is to communicate information to the public. As can be seen by the wide 
  variety of creative advertising today, such space is very compatible with expressive 
  activity.&quot;). Because the record evidence shows that the primary purpose 
  of the MBTA's advertising space has been to communicate information to the public, 
  and that it has promulgated its policies without reference to that purpose, 
  any newly discovered reasons for their restriction of Change the Climate's access 
  to the forum should be rejected out of hand as a matter of law. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In reality, the MBTA's 
  longstanding practice of allowing political speech &quot;evidences a general 
  intent to open a space for discourse, and a deliberate acceptance of the possibility 
  of clashes of opinion and controversy that the Court in Lehman<sup><a href="#34">34</a></sup> 
  recognized as inconsistent with sound commercial practice.&quot; New York Magazine,136 
  F.3d at 130 at 130.&quot;); see also Lebron, 49 F.2d at 896 (&quot;There is 
  no . . . question that WMATA has converted its subway stations into public fora 
  by accepting . . . political advertising.&quot;).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Lastly, the defendants 
  claim their restriction on speech is also reasonable because Change the Climate 
  has access to &quot;substantial alternative channels that remain open.&quot; 
  See Def. Memo. at 33 (quoting Families Achieving Independence &amp; Respect 
  v. Neb. Dep't of Soc. Servs., 111 F.3d 1408, 1422 (8th Cir. 1997). The defendants 
  note that Change the Climate remains free to continue operating its website, 
  that it may continue to advertise in local newspapers such as the Weekly Dig, 
  and that it may advertise on television, radio, and billboards. This only demonstrates 
  that Change the Climate is entitled to judgment because, by the defendants' 
  own admission, the MBTA's restriction of Change the Climate's speech does nothing 
  to advance its purported interest in protecting children. Unless the defendants 
  can demonstrate that juveniles - the class it seeks to protect - never use the 
  Internet, never read local newspapers, never watch television, never listen 
  to the radio, and never go outside in areas where billboards are visible, then 
  it cannot show that the rejection of the advertisements will insulate juveniles 
  from their supposedly dangerous message. Deprived of any possible claim that 
  rejection of the advertisements advances a legitimate purpose, the defendants' 
  position is reduced to little more than a crass &quot;not in my back yard&quot; 
  mentality.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">More fundamentally, &quot;[t]he 
  First Amendment protects [plaintiff's] right not only to advocate its cause 
  but also to select what [it] believe[s] to be the most effective means for so 
  doing.&quot; Meyer v. Grant, 486 U.S. 414, 424 (1988). It is therefore immaterial 
  that Change the Climate has the theoretical ability to spread its political 
  message through other avenues of communication that may be more burdensome, 
  more expensive, or less effective. See id. (&quot;That [the government restriction] 
  leaves open 'more burdensome' avenues of communication, does not relieve its 
  burden on First Amendment expression.&quot;) (citing FEC v. Massachusetts Citizens 
  For Life, Inc., 479 U.S. 238 (1986)).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In the end, the defendants' 
  arguments concerning the reasonableness of their restriction are self-defeating. 
  They do nothing more than confirm the unconstitutionality of the defendants' 
  conduct. By the defendants' own admission, juveniles have ready access to the 
  plaintiff's advertisements, and the MBTA's rejection of the advertisements does 
  nothing to insulate juveniles from whatever remote, speculative harm viewing 
  them may cause.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>CONCLUSION</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">For all of the above 
  reasons, this Court should deny the defendants' motion for summary judgment 
  in its entirety. Furthermore, this Court should enter summary judgment for the 
  plaintiff and order the equitable relief requested in the amended complaint.</font></p>
