<p><font face="Verdana, Geneva, Tahoma, Arial" size="+1"><b>East Coast nonprofit 
  bankrolls Bay Area ads backing cannabis reform</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">By Josh Richman, STAFF 
  WRITER </font></p>
<font size=-1 face="verdana, geneva, tahoma, arial">
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Friday, March 07, 2003 
  - A Massachusetts-based nonprofit group is placing 150 billboards in Oakland 
  and San Francisco to urge support for convicted marijuana grower and activist 
  Ed Rosenthal. </font>
</font> 
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The billboards, of various 
  sizes, have two slogans. One is &quot;Free Ed. Free The Jury. Free America,&quot; 
  and the other is &quot;Vote Your Conscience. Free America.&quot; The signs are 
  the work of Change the Climate Inc., a Greenfield, Mass., educational nonprofit 
  group. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Change the Climate 
  is a national organization primarily of parents and business people,&quot; said 
  executive director Joseph H. White Jr. &quot;Our perspective is that existing 
  marijuana laws for the most part are really counterproductive, they're a waste 
  of tax dollars, ... and as a parent, they put my children at risk.&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The all-volunteer group 
  has run ad campaigns in Washington, D.C., and elsewhere calling attention to 
  the government's huge spending on prosecution and imprisonment of marijuana 
  offenders. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">California law allows 
  medical marijuana use, but federal law still bans all growing, possession and 
  use. Rosenthal, 58, of Oakland, was arrested in 2002 by Drug Enforcement Administration 
  agents. Jurors at his federal trial were not allowed to consider his status 
  under California's medical marijuana law, and were not told of his protection 
  by an Oakland ordinance. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;When the stormtroopers 
  of the federal government busted in while he was doing the city's good work, 
  and the judge would not allow any consideration of a law now supported by over 
  80 percent of Americans, it cried out for justice, and that's why we chose the 
  case,&quot; White said. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">White, 47, is a father 
  of three and a co-founder of Share Group Inc., a telemarketing firm that does 
  fund-raising, voter outreach, customer service and grass-roots organizing for 
  progressive nonprofits and socially responsible companies. Among Change the 
  Climate's directors is Mal Warwick, founder and chairman of a direct-mail fund-raising 
  business in Berkeley. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This is the largest billboard 
  campaign Change the Climate has mounted so far, White said, estimating its cost 
  &quot;in the tens of thousands.&quot; White said the group's funding &quot;comes 
  from business executives like Peter Lewis -- chairman and CEO of insurance giant 
  Progressive Corp. -- and others&quot; as well as parents and others who see 
  the ads. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Lewis has helped bankroll 
  drug reform efforts across the nation, including California's 1996 medical marijuana 
  initiative. Financier George Soros and University of Phoenix founder John Sperling, 
  who often pool their money with Lewis' for drug reform efforts, are not donors 
  to Change the Climate, White said. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The group's 2001 tax 
  return shows revenue of about$34,000, of which $31,000 came from just two donors 
  whose names are blanked out; White said he doesn't recall who they were. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;We don't have a 
  lot of contributors -- we have a handful of well-off individuals who are often 
  the kind of people who help fund new organizations, whether they be in marijuana 
  reform or other social issues,&quot; he said, adding that more donors have given 
  since 2001. &quot;One of our larger contributors over the last two years is 
  a well-known Bay Area high-tech entrepreneur.&quot;</font></p>
