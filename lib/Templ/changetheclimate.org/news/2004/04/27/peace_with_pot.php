<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Make peace with 
  pot</strong></font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">By ERIC SCHLOSSER</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">N.Y. Times News Service</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>A 2002 report 
  by a Canadian Senate committee found there were &#8220;clear, though not definitive&#8221; 
  benefits for using marijuana in the treatment of chronic pain, multiple sclerosis, 
  epilepsy and other ailments.</strong></font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Starting in the fall, 
  pharmacies in British Columbia will sell marijuana for medicinal purposes, without 
  a prescription, under a pilot project devised by Canada&#8217;s national health 
  service. The plan follows a 2002 report by a Canadian Senate committee that 
  found there were &#8220;clear, though not definitive&#8221; benefits for using 
  marijuana in the treatment of chronic pain, multiple sclerosis, epilepsy and 
  other ailments. Both Prime Minister Paul Martin and Stephen Harper, leader of 
  the opposition conservatives, support the decriminalization of marijuana.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Oddly, the strongest 
  criticism of the Canadian proposal has come from patients already using medical 
  marijuana who think the government, which charges about $110 an ounce, supplies 
  lousy pot. &#8220;It is of incredibly poor quality,&#8221; said one patient. 
  Another said, &#8220;It tastes like lumber.&#8221; A spokesman for Health Canada 
  promised the agency would try to offer a better grade of product.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Needless to say, this 
  is a far cry from the situation in the United States, where marijuana remains 
  a Schedule I controlled substance, a drug that the government says has a high 
  potential for abuse, no accepted medical uses and no safe level of use.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Under federal law it 
  is illegal to possess any amount of marijuana anywhere in the United States. 
  Penalties for a first marijuana offense range from probation to life without 
  parole. Although 11 states have decriminalized marijuana, most still have tough 
  laws against the drug. In Louisiana, selling one ounce can lead to a 20-year 
  prison sentence. In Washington State, supplying any amount of marijuana brings 
  a recommended prison sentence of five years.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">About 700,000 people 
  were arrested in the United States for violating marijuana laws in 2002 (the 
  most recent year for which statistics are available) -- more than were arrested 
  for heroin or cocaine. Almost 90 percent of these marijuana arrests were for 
  simple possession, a crime that in most cases is a misdemeanor. But even a misdemeanor 
  conviction can easily lead to time in jail, the suspension of a driver&#8217;s 
  license, the loss of a job. And in many states possession of an ounce is a felony. 
  Those convicted of a marijuana felony, even if they are disabled, can be prohibited 
  from receiving federal welfare payments or food stamps. Convicted murderers 
  and rapists, however, are still eligible for those benefits.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The Bush administration 
  has escalated the war on marijuana, raiding clinics that offer medical marijuana 
  and staging a nationwide roundup of manufacturers of drug paraphernalia. In 
  November 2002 the Office of National Drug Control Policy circulated an letter 
  to America&#8217;s prosecutors&#8221; spelling out the administration&#8217;s 
  views. &#8220;Marijuana is addictive,&#8221; the letter asserted. &#8220;Marijuana 
  and violence are linked . . . no drug matches the threat posed by marijuana.&#8221;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">This tough new stand 
  has generated little protest in Congress. Even though the war on marijuana was 
  begun by President Ronald Reagan in 1982, it has always received strong bipartisan 
  support. Some of the toughest drug war legislation has been backed by liberals, 
  and the number of annual marijuana arrests more than doubled during the Clinton 
  years. In fact, some of the strongest opposition to the arrest and imprisonment 
  of marijuana users has come from conservatives like William F. Buckley, the 
  economist Milton Friedman and Gary Johnson, the former Republican governor of 
  New Mexico.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">This year the White House&#8217;s 
  national antidrug media campaign will spend $170 million, working closely with 
  the nonprofit Partnership for a Drug-Free America. The idea of a &#8220;drug-free 
  America&#8221; may seem appealing. But it&#8217;s hard to believe that anyone 
  seriously hopes to achieve that goal in a nation where millions of children 
  are routinely given Ritalin, antidepressants are prescribed to cure shyness, 
  and the pharmaceutical industry aggressively promotes pills to help middle-aged 
  men have sex.</font> 
