<p> <font size="-1" face="verdana, geneva, tahoma, arial"> As you may know, marijuana 
  reform ballot initiatives won by landslide margins two weeks ago in Massachusetts. 
  What made the victories especially sweet for me is that Change the Climate conducted 
  three advertising campaigns during the summer and fall. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Five of the questions recommended 
  that state lawmakers remove the threat of arrest and imprisonment for patients 
  who use medical marijuana on the advice of their doctors. The other seven questions 
  sought to remove criminal penalties for adults who use marijuana responsibly. 
  One of those seven advocates the taxation and regulation of marijuana for responsible 
  adults aged 21 and older.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">While we did not coordinate 
  with the ballot initiatives we used messages designed to remind voters how expensive 
  it is to enforce marijuana laws. I can&#8217;t help but believe that voters 
  were influenced by these messages that were on billboards in three areas near 
  districts where the questions appeared on the ballot. You can check out the 
  campaign message at <a href="http://www.changetheclimate.org/campaigns/boston04/">www.changetheclimate.org</a>.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Overall, Change the Climate&#8217;s 
  ad campaigns generated an astonishing 17,000,000 million impressions during 
  the five month campaign period. You can review the results in more detail at 
  <a href="http://www.mpp.org/MA/" target="_blank">www.mpp.org/MA/</a>. Thanks 
  to MPP for funding 9 out of the 12 initiatives.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Local organizations, 
