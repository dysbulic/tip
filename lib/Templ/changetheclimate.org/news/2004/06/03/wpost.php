<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"> </font><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Judge 
  Voids Law Against Drug Ads On Metro</strong></font> <br>
  <font face="Verdana, Geneva, Tahoma, Arial">Pro-Marijuana Groups Hail Free Speech 
  Ruling</font> </p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">By Carol D. Leonnig<br>
  Washington Post Staff Writer</font></p>
<p> <font size="-1" face="verdana, geneva, tahoma, arial"><img src="../../../../images/bush_lawsuit_ad.jpg" width="300" height="307" hspace="3" vspace="3" align="right">A 
  federal law aimed at keeping advertisements critical of national drug policy 
  out of Metro stations and bus shelters illegally chills free speech and cannot 
  stand, a federal judge ruled yesterday. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">U.S. District Judge Paul 
  L. Friedman barred the U.S. government from enforcing a law passed by Congress 
  this year that calls for denying federal transportation money to any transit 
  system that accepts ads promoting the legalization of drugs. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Drug advocacy groups 
  that sponsored the ads applauded the decision, which came in a lawsuit they 
  filed against Congress and Metro. They predicted that Metro officials will soon 
  accept their ads in stations and shelters. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&quot;I'm just absolutely 
  delighted that free speech is alive and well in this country and that we can 
  finally have a debate about reforming marijuana laws instead of being force-fed 
  the government's one-sided views on marijuana,&quot; said Joseph White, executive 
  director of Change the Climate, one of the plaintiffs in the suit. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Metro spokesman Steven 
  Taubenkibel declined to comment, saying the decision was under review. U.S. 
  Justice Department officials said that they were analyzing the ruling and that 
  it was too early to say whether it would be appealed. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The law at the center 
  of the dispute found its origins in the ire of Rep. Ernest J. Istook Jr. (R-Okla.), 
  who said he was furious last fall at seeing marijuana legalization ads in Metro 
  shelters and stations. One ad pictured a scantily clad couple in an embrace, 
  with the caption: &quot;Enjoy better sex! Legalize and Tax Marijuana.&quot; 
  </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Istook introduced legislation 
  to stop the ads by attaching his penalty measure to the 2004 appropriations 
  bill, which President Bush signed into law in January. The measure threatens 
  53 transit authorities with the possible loss of $3.1 billion a year if they 
  accept such ads. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Change the Climate joined 
  with the Drug Policy Alliance, the Marijuana Policy Project and the American 
  Civil Liberties Union to file the lawsuit, claiming that the legislation amounted 
  to unconstitutional censorship. The case was filed after Metro rejected ads 
  in February. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">In an April hearing before 
  Friedman, the Justice Department argued that federally funded mass transit systems 
  are not required to provide a canvas for political groups to express their views. 
  Department lawyers also maintained that some of the ads encouraged illegal behavior. 
  </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">In his ruling, Friedman 
  declared that the case was not about drugs or crime, but about free speech. 
  He called the law a clear case of &quot;viewpoint discrimination&quot; and noted 
  that Metro displayed the Office of National Drug Control Policy's anti-drug 
  ads when it refused the other ads. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&quot;Congress . . . 
  cannot prohibit advertisements supporting legalization of a controlled substance 
  while permitting those that support tougher drug sentences,&quot; the judge 
  said. He called the law &quot;an unconstitutional exercise of Congress' broad 
  spending power.&quot; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Art Spitzer, the litigation 
  chief for the ACLU in Washington, said it will be difficult for Congress or 
  Metro to challenge the ruling. &quot;While reasonable people may disagree about 
  drug policy, clearly the Constitution doesn't allow Congress to try to censor 
  that debate,&quot; he said. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">But Istook indicated 
  that the case was far from over. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&quot;I'm confident that 
  ultimately the courts will agree with the long-standing principle that Congress 
  is free to decide what we will or will not fund,&quot; Istook said. &quot;We 
  provide major funding to combat drug use, and tax dollars should not be used 
  to subsidize contrary messages.&quot;</font></p>
