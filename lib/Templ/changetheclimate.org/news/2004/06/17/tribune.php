<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Censorship gets 
  derailed</strong></font> </p>
<p> <font size="-1" face="verdana, geneva, tahoma, arial">You've heard the phrase 
  &quot;drunk with power&quot;? That pretty well captures the state of mind of 
  Rep. Ernest Istook (R-Okla.) when he pushed through a measure telling the Washington 
  Metro system what kind of ads it could place in its stations, trains and buses. 
  And in a nice bit of symmetry, his mandate dealt with a different kind of intoxicant.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Istook, who chairs the 
  House subcommittee responsible for funding of mass transit, was outraged one 
  day last year when he saw ads at Metro stops with a photo of a couple embracing 
  and the message, &quot;Enjoy better sex! Legalize and tax marijuana.&quot; He 
  introduced legislation forbidding any transit system that gets federal funds 
  from allowing &quot;any activity that promotes the legalization or medical use&quot; 
  of marijuana or other illicit drugs, and it passed.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">A group called Change 
  the Climate tried to buy space from the Washington Metropolitan Area Transit 
  Authority for ads that featured the headline, &quot;Marijuana Laws Waste Billions 
  of Taxpayer Dollars to Lock Up Non-Violent Americans.&quot; Citing the new federal 
  ban, the WMATA said it was obliged to reject the ads rather than risk losing 
  the $170 million it gets each year in federal money. One point of view could 
  be expressed on the subways and buses of the nation's capital--and one only.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">But a federal district 
  court has done the inevitable, striking down the ban as an unconstitutional 
  attack on freedom of speech. The Metro system has made a practice of accepting 
  issue-oriented ads, the court said, and the government may not choose which 
  opinions shall be allowed.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&quot;While Congress 
  may be under no obligation to fund mass transit or other entities that rely 
  also on advertising revenues for their survival, once it chooses to do so, it 
  must act in a way that does not engage in viewpoint discrimination in violation 
  of the 1st Amendment,&quot; wrote Judge Paul L. Friedman. There's something 
  fundamentally wrong with a policy that lets one side spread its message while 
  forcibly silencing the other side.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">But Istook still doesn't 
  get it. After the decision came down, he said, &quot;I'm confident that ultimately 
  the courts will agree with the long-standing principle that Congress is free 
  to decide what we will or will not fund. We provide major funding to combat 
  drug use, and tax dollars should not be used to subsidize contrary messages.&quot;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">What is he talking about? 
  Running the notices would not have &quot;subsidized&quot; an unapproved message: 
  Change the Climate was trying to buy ad space, at a total price of $91,875.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Congress is free to decide 
  whether to fund mass transit or not. What it's not free to do is use its power 
  of the purse to suppress one point of view on a matter of public policy. Direct 
  government censorship is forbidden by the 1st Amendment, and so is the indirect 
  kind.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">It's understandable that 
  Istook takes issue with the policies proposed by Change the Climate and its 
  allies. But if he thinks it would be a mistake to liberalize drug laws, he should 
  use the option he's trying to deny them: Rebut their arguments.</font></p>
