<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial">Government's new tool 
  in the drug war: a muzzle</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">By Steve Chapman</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Originally published May 4, 2004</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">CHICAGO - For decades, 
  supporters of the war on drugs have been losing the debate about the policy, 
  even as they continue to lock up hordes of harmless offenders. But prohibitionists 
  have a new tactic to help them get the best of the argument: Don't let the other 
  side speak. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">One day last year, Ernest 
  Istook noticed an ad on the Washington Metro transit system with an unusual 
  message: &quot;Enjoy better sex! Legalize and tax marijuana.&quot; Most people 
  who ride the bus or the subway manage to absorb all sorts of little surprises 
  on their daily commute, but not Mr. Istook. He wrote a letter to the local transit 
  agency to say it had &quot;exercised the poorest possible judgment&quot; in 
  running the ad at &quot;a time when the nation and the Washington, D.C., area 
  in particular suffer from chronic substance abuse.&quot; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Normally a complaint like that 
  would have no effect. Mr. Istook, however, is not only a Republican member of 
  the House of Representatives from Oklahoma but also chairman of the Transportation 
  and Treasury Appropriations Subcommittee. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">He placed a provision in a funding 
  bill reducing federal funds for Metro by $92,500, as punishment for the ad, 
  and denying money to any transit system that accepts ads advocating &quot;the 
  legalization or medical use&quot; of marijuana or other illicit drugs. And it 
  passed. Transit agencies across the country now have to choose between tolerating 
  open debate and getting a total of $3.1 billion in federal funds. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">So your local bus or subway system 
  is free to run all sorts of ads and public service announcements. It is free 
  to post lurid signs warning of the evils of smoking pot or snorting cocaine. 
  But if it gets a nickel from the federal government, it may not allow any message 
  raising doubts about the wisdom of the drug war. This is the Bill O'Reilly approach 
  to policy disputes: Shut up! </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Already the policy is having an 
  effect. The group that ran the original ad, Change the Climate, recently tried 
  to buy space on Washington buses for an ad with the caption: &quot;Marijuana 
  laws waste billions of taxpayer dollars to lock up nonviolent Americans.&quot; 
  But even simple statements of fact run afoul of the censor's decree. Metro refused, 
  saying it couldn't afford to risk the loss of $170 million in federal money.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The transit system does, however, 
  display messages by the Partnership for a Drug-Free America urging parents to 
  &quot;set the rules and expect your kid to live drug-free,&quot; as well as 
  ads dealing with issues such as abortion, the Iraq war and the alleged failures 
  of the U.S. Department of Education.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Upon being rebuffed, Change the 
  Climate filed a lawsuit, supported by the American Civil Liberties Union, arguing 
  that the ban violates the First Amendment guarantee of freedom of expression. 
  On Wednesday, a federal judge in Washington hearing the case got to consider 
  a variety of preposterous rationalizations for the law.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">One is that the government is not 
  obligated to subsidize unwholesome messages. Congress, the Justice Department 
  argued, &quot;has an undeniable interest in ensuring that no federal funds are 
  used, directly or indirectly, to facilitate activity that Congress does not 
  wish to promote.&quot; But in this case, the ad would not have cost the government 
  money - Metro would have made $91,875 from renting the space.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The government lawyer also insisted 
  that Congress had good reason to ban such ads because they &quot;might encourage 
  the use of drugs, which is illegal at this time.&quot; But the ad didn't say 
  people should do something illegal. In fact, by showing a picture of people 
  behind bars, the ad might even deter violators.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The point of the ad was to change 
  the law. To Ernest Istook and John Ashcroft, though, any suggestion that a law 
  be changed amounts to incitement to violate it. In their addled version of democracy, 
  you can advocate the enactment of a ban but not its repeal.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">In the case of our drug laws, that 
  sort of rule might be prudent, because their total failure makes them vulnerable 
  to criticism. Such as the point made by Change the Climate, which says it's 
  unfair to imprison people for using a largely benign drug that one of every 
  three Americans has tried.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">To silence critics is an implicit 
  concession by the government that the drug war is impossible to defend. Alas, 
  you can't win a debate by silencing the other side, but you can lose one.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Steve Chapman is a columnist for 
  the Chicago Tribune, a Tribune Publishing newspaper. His column appears Tuesdays 
  and Fridays in The Sun. </font></p>
