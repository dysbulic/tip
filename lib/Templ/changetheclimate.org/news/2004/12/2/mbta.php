<font size="-1" face="Verdana, Geneva, Tahoma, Arial"><p>BOSTON - A Greenfield man won on his day in court when a federal appeals panel ruled
that the Massachusetts Bay Transportation Authority violated his free speech rights
by refusing to display ads from a group he founded that wants to change marijuana
laws. </p>

<p>The 1st U.S. Circuit Court of Appeals found that the authority's refusal to display
three advertisements submitted by the nonprofit group Change the Climate violated
the First Amendment because it was based on officials' disapproval of the views
expressed in the ads and not made on a content-neutral basis. </p>

<p>The ads were rejected four years ago, with the authority claiming they encouraged
children to smoke marijuana. Officials argued that the authority has the right to
protect riders from offensive messages or advertisements that &quot;promote the use of
illegal goods or services.&quot; </p>

<p>Joseph White of Greenfield, who founded Change the Climate in 1999, contends that
the ads do not promote marijuana use by children, but instead seek to stimulate
debate about current marijuana laws. </p>

<p>&quot;Teenagers have easy access to marijuana in just about every community in
Massachusetts and they have a much harder time getting alcohol and tobacco because
it's regulated by adults,&quot; White said. &quot;I think if adults are serious about keeping
marijuana away from our kids that we would regulate and control it like we do
alcohol and tobacco. </p>

<p>&quot;In particular as parents, we're concerned that we are arresting our children in
significant numbers and we're doing a lot more harm in that than probably marijuana
does to them,&quot; White said. </p>

<p>The appeals court found that the MBTA, a quasi-government agency, does not have the
right to turn down advertisements based on the personal viewpoint of its executives.</p>

<p>&quot;MBTA advertising space is literally a billboard for the expression of opinions to
citizens at large. As a government agency, they shouldn't have the right to pick and
choose what opinions they allow to be advertised,&quot; Harvey Schwartz, an attorney for
the Greenfield-based advocacy group argued.</p> 

<p>The court did find, however, that the MBTA advertising space does not constitute a
public forum, as had been claimed by Change the Climate. </p>

<p>&quot;The Supreme Court has repeatedly held that the government must have an affirmative
intent to create a public forum in order for a designated public forum to arise,&quot;
the justices wrote. </p>

<p>The advertisements proposed by Change the Climate question current marijuana laws
and penalties for people arrested on minor possession charges. </p>

<p>&quot;I've got three great kids,&quot; reads one ad. &quot;I love them more than anything. I don't want them to smoke pot. But I know jail is a lot more dangerous than smoking pot.&quot; </p>

<p>An MBTA spokesman said Tuesday that their attorneys were reviewing the court's
ruling and had no immediate comment. </p>

<p>The MBTA provides public transportation to approximately 1.2 million customers every
day on subways, trains, buses and ferries. Up to 60,000 Boston public school
students use the MBTA to get to classes. </p>

<p>As dictated by law, the MBTA uses its 40,000 advertising spaces on buses, trains,
trolley and station platforms to generate revenue beyond that raised by fares. </p>

<p>Over the years, the MBTA has received a number of complaints from its riders about
advertisements and in the five-year period before the lawsuit was filed by Change
the Climate, the MBTA rejected at least 17 advertisements barred by its ad policy,
including some depicting violence, profanity and tobacco products. They did,
however, allow advertising for alcoholic beverages and for movies with sexual
themes. </p>

<p>The 1st Circuit found that although the MBTA's advertising guidelines are &quot;viewpoint
neutral&quot; and constitutional, its rejection of the three Change the Climate ads was
unreasonable and amounted to &quot;viewpoint discrimination.&quot; </p>

<p>&quot;Inherent in the MBTA's position is its recognition that, save for the risk of
inducing juveniles to smoke marijuana, the refusal to run these advertisements for
an adult audience would be viewpoint discrimination. That conclusion is essentially
conceded in the MBTA's briefs,&quot; the justices wrote. </p>

<p>&quot;There is direct evidence through statements made by MBTA officials that the reason
for rejecting the advertisements was actually a distaste for Change the Climate's
viewpoint,&quot; they added. </p>

<p>&quot;This suspicion of viewpoint discrimination is deepened by the fact that the MBTA
has run a number of ads promoting alcohol that are clearly more appealing to
juveniles than the ads here,&quot; the court said. </p>

<p>White said he plans to begin raising money for the ads immediately. When the group
first proposed the ads four years ago, it planned to spend $50,000 on the
advertising campaign, White said. </p>

<p>In a separate matter joined with the Change the Climate case at the appellate level,
the court said the MBTA did have the right to reject ads from the Church with the
Good News because that group's ads were demeaning or disparaging toward other
religions. </p>

<p>In one case, the church's advertisements stated that &quot;Satan set up over a thousand
false religions in the world ... There is only one true religion. All the rest are
false.&quot; </p>

<p>Other advertisements by the Good News church disparaged specific religious groups by
name. </P></font>

