<p><font face="verdana, geneva, tahoma, arial"> <b>Metro's pro-pot ads get attention 
  on Hill </b></font> </p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">By Jim McElhatton<br>
  THE WASHINGTON TIMES</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Marijuana-legalization ads posted 
  recently in Metro buses and subway stations have prompted an Oklahoma congressman 
  to propose legislation making it illegal for transit agencies that accept federal 
  dollars to give advertising space to groups that advocate breaking the law. 
  </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Rep. Ernest Istook, Republican, 
  cited &quot;grave concern and displeasure&quot; at the public service announcements 
  placed on Metro buses and throughout area subway stations during October by 
  Change the Climate, a Massachusetts-based nonprofit group. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Mr. Istook took particular exception 
  to a marijuana-legalization ad showing a young couple embracing, and the caption 
  &quot;Enjoy Better Sex!&quot; <br>
  &quot;At a time when the nation and the Washington D.C. area, in particular, 
  suffer from chronic substance abuse ... I find it shocking that [the Washington 
  Metropolitan Area Transit Authority] provides this ad space, and at no cost!&quot; 
  Mr. Istook wrote in a letter to Jim Graham, chairman of the Metro Board and 
  a D.C. Council member.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The Metro ads prompted the congressman, 
  who chairs the House Transportation and Infrastructure subcommittee, to add 
  language to an appropriations bill prohibiting federally subsidized transit 
  agencies from giving ad space to organizations that tout legalizing drugs. &quot;Since 
  [Metro] has the resources to provide $46,250 in free ad space for this very 
  advertising,&quot; Mr. Istook wrote, &quot;I have to wonder why [Metro] should 
  expect to receive the $67,050,000 in federal funding.&quot; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The new language Mr. Istook inserted 
  into the transportation legislation states that federal funds will not be available 
  if a transit agency &quot;is involved directly or indirectly with any activity 
  ... that promotes the legalization or medical use&quot; of illegal drugs. Mr. 
  Istook wants to eliminate $92,500 in federal funding to Metro &quot;as a warning 
  to other transit agencies,&quot; according to the legislation. &quot;I think 
  it's a moot point,&quot; Mr. Graham said yesterday, referring to Mr. Istook's 
  amendment. &quot;As of Jan. 1, we will not be considering any more public-service 
  announcements, except as the local jurisdictions sponsor them. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&quot;In terms of losing the $92,500, 
  I think that is petty and punitive,&quot; he said. &quot;I think there would 
  have been a better appreciation of the dilemma we were in.&quot; <br>
  Metro recently reduced the space reserved for public service ads from 13 percent 
  to 5 percent, with Maryland, the District and Virginia now handling their own 
  public-service announcements. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Metro Board member Carlton Sickles, 
  who represents Montgomery County, said the transit agency would abide by the 
  new rules if federal lawmakers approve the legislation, which they will consider 
  when the House reconvenes next week. However, Mr. Sickles said the board would 
  not make any policy changes until that happens. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&quot;It seems to me that this 
  sort of legislation always gets very controversial and that it tends to get 
  stuck in committee,&quot; he said. &quot;I don't know where this is going at 
  the moment, but we'll be watching it carefully.&quot; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Metro had rejected the ad campaign 
  by Change the Climate two years ago, but reversed its position after the American 
  Civil Liberties Union interceded on the group's behalf. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Both Mr. Graham and Mr. Sickles 
  said Metro had accepted the recent ads from Change the Climate because Metro's 
  attorneys said the transit agency likely would be sued if it refused. &quot;These 
  nonprofits would have been very happy to have had a public controversy and to 
  go to court,&quot; Mr. Graham said. Mr. Sickles said, &quot;You have to take 
  what they give you. If you're going to accept the advertising, in terms of public-service 
  ads, you've got take them all.&quot; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">However, not all transit agencies 
  agree. In Boston, Change the Climate lost in court when it sued the Massachusetts 
  Bay Transportation Authority for the right to post its ads. &quot;We felt that 
  it was promoting an illegal activity,&quot; said Joseph Pesaturo, spokesman 
  for the Boston-based transit agency. &quot;Since we have a very captive audience 
  in our stations made up of a lot of young people, we weren't comfortable with 
  that. &quot;The group sued claiming the usual violations of free speech. A U.S. 
  District Court judge ruled in our favor.&quot; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Local education activists said 
  they were concerned about the pro-marijuana ads because most D.C. school children 
  use Metro to get to and from school, but they had mixed feelings about Mr. Istook's 
  bid to make Metro to stop running them. &quot;I would rather see local authorities 
  handling this issue, said Mary Levy, and analyst with Parents United, an advocacy 
  organization that works to improve conditions in D.C. schools. &quot;When you 
  try to regulate things by content, it is a very tricky business under the First 
  Amendment,&quot; Miss Levy said. &quot;At the same time, when I saw the huge 
  [pro-marijuana] ad that said 'Have Better Sex', I had to wonder what we're saying 
  to our children about drugs and about sex.&quot; &quot;Generally, I don't advocate 
  censorship,&quot; said Iris Toyer, chairwoman of Parents United. &quot;But I 
  have son in the sixth grade, and when it comes to this issue, I have to approach 
  things with my 'parent hat' on.&quot; </font></p>
