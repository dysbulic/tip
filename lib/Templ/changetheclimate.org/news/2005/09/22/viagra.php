<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Unusual Political Message: Pot Makes Better Lovers than Viagra</strong></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial"><strong>Marijuana Policy Group Launches Unusual Web-Based Donor Recruitment Campaign</strong></font></p>
<font size="-1" face="Verdana, Geneva, Tahoma, Arial"> 
<p>The words &#8220;marijuana&#8221; and &#8220;sex&#8221; come up a lot in private 
  conversations, but how often do you hear about sex and pot in public debate 
  or in political campaigns?</p>
</font>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Change the Climate, a 
  Massachusetts-based organization, today launched a light-hearted effort to spotlight 
  one of the many reasons marijuana should be legalized: For many consenting adults, 
  it&#8217;s an aphrodisiac.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">In a 90-second animated 
  cartoon titled &#8220;Sex News,&#8221; Change the Climate describes a fictitious 
  &#8220;Canadian Sex Study,&#8221; which found that &#8220;marijuana makes better 
  lovers than Viagra.&#8221; </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">In the animation, the 
  sexual effects of pot and Viagra are graphically (but tastefully) depicted. 
  View the animation by clicking on <a href="/tv/sex.html">www.changetheclimate.org</a>. 
  </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Change the Climate has 
  a serious goal behind the fun animation. The nonprofit organization is trying 
  to educate and mobilize marijuana reform supporters (marijuana smokers and non-users) 
  who are alienated by traditional politics.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&#8220;Like other advertisers, 
  we are using sex to cut through the information overload in today&#8217;s advertising 
  environment.&#8221; said Joseph H. White, Executive Director of Change the Climate. 
  &#8220;We&#8217;re following the lead of major drug companies that advertise 
  Viagra and Cialis for erectile dysfunction &#8211; so we think our message of 
  comparison will get people&#8217;s attention and hopefully foster more debate 
  about the stupidity and futility of marijuana laws. Maybe we&#8217;ll even spawn 
  interest in a real medical study!&#8221;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">This is not the first 
  time Change the Climate has directly addressed the issue of sex and marijuana.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">In 2003, Change the Climate&#8217;s 
  &#8220;Enjoy Better Sex: Legalize and Tax Marijuana&#8221; transit ads on the 
  Washington DC Metro were denounced by members of Congress and the Washington 
  Times. One Congressman &#8211; Congressman Istook from Oklahoma &#8211; was 
  so outraged he pushed a law through Congress that would have withheld billions 
  of dollars of funding from local transport agencies that allowed marijuana law 
  reform advertising on their transport systems. This law was struck down in April, 
  2004, by federal courts on First Amendment grounds. Click here for more information 
  on this campaign: <a href="/news/2004/06/02/court.php">http://www.changetheclimate.org/news/2004/06/02/court.php</a>. 
  </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Change the Climate is 
  producing a series of the provocative reform animations. Its first in the series, 
  titled <a href="/tv/overkill.html">&#8220;Operation Overkill&#8221;</a> is available 
  at <a href="/">www.changetheclimate.org</a>, where you can find more information 
  about the organization&#8217;s goals and history.</font></p>
