<font face="verdana, geneva, tahoma, arial"> Pro-Marijuana Group Given Free Ad Space on DC Buses
</b></font>
<p>

<font face="verdana, geneva, tahoma, arial" size="-1">
  By Melanie Hunter<br>
  CNSNews.com Deputy Managing Editor<br>
  November 13, 2003
<p>(CNSNews.com) - A group lobbying for the legalization of marijuana ran ads 
  touting a side benefit to the use of pot - better sex - and posted the ads on 
  buses, at bus shelters and subway stations for several weeks in Washington, 
  D.C., free of charge, courtesy of the local metro transit system. The Washington 
  Metropolitan Area Transit Authority receives federal taxpayer money to help 
  keep it operating.</p>
<p>Change the Climate's ads were featured on buses and bus shelters throughout 
  the Washington, D.C. area on Sept. 1, with additional ads that were launched 
  Oct. 1 in area subway stations. None of the ads is currently running.</p>
<p>The ad featured a man holding a woman, and underneath the picture were the 
  words &quot;Enjoy better sex! Legalize and tax marijuana.&quot;</p>
<p>U.S. Reps. Joe Pitts (R-Pa.), Henry Brown (R-S.C.), Sue Myrick (R-N.C.) who 
  chairs the House Republican Study Committee, and Mark Souder (R-Ind.), chairman 
  of the Subcommittee on Criminal Justice, Drug Policy and Human Resources, sent 
  a letter last week to Richard White, general manager and chief executive officer 
  of the Washington Metropolitan Area Transit Authority, demanding accountability.</p>
<p>&quot;As you know, in recent days there has been a public outcry regarding 
  the Washington Metropolitan Area Transit Authority (WMATA)'s decision to allot 
  free advertising space to Change the Climate, an organization that supports 
  the legalization of marijuana,&quot; the House members wrote.</p>
<p>&quot;It is our understanding that WMATA has decided to review its advertising 
  policy and to require that in the future non-profit organizations pay for the 
  use of WMATA advertising space. While this proposed policy change might address 
  some of the fiscal concerns surrounding lost advertising revenue, the larger 
  concerns regarding overall advertising standards remain unaddressed.</p>
<p>&quot;As Members of Congress, we are deeply troubled by the Change the Climate 
  advertisements. Not only do these advertisements, some of which were placed 
  near schools, promote illegal behavior. They are also sexually suggestive and 
  violate standards of common decency,&quot; the letter stated.</p>
<p>&quot;As a government entity, WMATA has a responsibility to ensure that its 
  resources are not used in an unseemly manner. In addition, because WMATA is 
  a recipient of federal taxpayer dollars, we in Congress have a responsibility 
  to ensure accountability.</p>
<p>&quot;In this regard, we would like to know what steps WMATA will be taking 
  in order to ensure that (1) advertisements do not promote illegal behavior; 
  (2) advertisements do not violate local obscenity standards; and (3) advertisements 
  in public places are appropriate for all ages,&quot; the letter concluded.</p>
<p>When contacted by CNSNews.com, Metro spokesperson Lisa Farbstein said the ads 
  were removed at the end of October, having run for four weeks. She said the 
  ads met the criteria for a public service announcement, but could not provide 
  specifics about the criteria used.</p>
<p>The transit authority's public service advertising policy has changed, Farbstein 
  said. Metro will no longer be accepting public service advertising, she said. 
  Instead, that ad space has been turned over to local jurisdictions.</p>
<p>The individual counties and cities served by Metro will determine which public 
  service ads are to be posted on buses, at bus shelters and trains in their various 
  jurisdictions, Farbstein said.</p>
<p>According to a message on Change the Climate's website, &quot;We believe that 
  sex and marijuana use exist completely in the private realm of consenting and 
  responsible adults. We realize, unfortunately, that many politicians see the 
  bedroom and other private, individual activities as important areas for government 
  legislation.</p>
<p>&quot;We want to stress that sex and marijuana are for adults - or at least 
  for individuals who have achieved a level of emotional and psychological maturity 
  that is reflected in a balanced, productive life,&quot; the message claims.</p>
<p>Change the Climate also believes a ban on marijuana use &quot;wastes taxes.&quot;</p>
<p>&quot;It is common knowledge that U.S. taxpayers pay billions of dollars each 
  year to fund the failed War on Drugs. A simple analysis of the War on Drugs 
  shows that most of the costs borne by taxpayers are for marijuana prohibition.</p>
<p>&quot;With over 700,000 annual arrests, thousands of prisoners and hundreds 
  of millions of dollars spent on anti-marijuana television ads, the War on Drugs 
  is mostly a War on Marijuana,&quot; the website claims.</p>
<p>In 2001, Metro officials rejected Change of Climate's ad campaign, but were 
  forced to reconsider after the American Civil Liberties Union protested. The 
  ACLU argued that rejecting the ads would violate the pro-marijuana group's First 
  Amendment rights.<br>
