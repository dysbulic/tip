<font face="verdana, geneva, tahoma, arial"> <b>Marijuana Ad On Metro Infuriates Lawmaker
</b></font>
<font face="verdana, geneva, tahoma, arial" size="-1">
<p>By Lyndsey Layton<br>
  Washington Post Staff Writer<br>
  Wednesday, December 3, 2003; Page B01 </p>
<p>
  An Oklahoma lawmaker is seeking to slice $92,500 from the federal government's 
  annual payment to Metro because he is angry that the transit agency accepted 
  advertising from a nonprofit group that wants to decriminalize marijuana.</p>
<p> Change the Climate Inc. has been using public service advertising space on 
  the Metro system since 2001, but it was the latest round of advertising, this 
  fall, that drew the ire of Rep. Ernest J. Istook Jr. (R-Okla.). </p>
<p>The ad showed a man carrying a tanned blonde in a short white dress, the two 
  of them set against the azure sky of some tropical retreat. Under the picture 
  appeared the declaration: &quot;Enjoy better sex! Legalize and Tax Marijuana.&quot; 
</p>
<p>In a Nov. 10 letter to Jim Graham, chairman of the Metro board, Istook called 
  the ad &quot;shocking&quot; and said the board had &quot;exercised the poorest 
  possible judgment, so I must assure that [Metro] will learn the proper lessons 
  from this experience and will only accept appropriate ads in the future.&quot; 
</p>
<p>This week, Istook inserted into a bill language that would cut Metro's funds 
  by $92,500 and prohibit any transit system that receives federal funds from 
  running advertising from a group that wants to decriminalize marijuana. The 
  money is just a fraction of the federal government's $164 million subsidy to 
  Metro for capital projects. </p>
<p>The language is part of an omnibus bill expected to come before Congress for 
  a vote in late December or January. </p>
<p>&quot;Metro is using taxpayer facilities to promote illegal activity,&quot; 
  said Micah Swafford, Istook's press secretary. She said the congressman was 
  unavailable for comment. </p>
<p>Graham sees things differently. </p>
<p>&quot;This is petty and punitive,&quot; Graham said. &quot;It's a politically 
  motivated, micromanaging bolt from the blue. I suspect it would never have risen 
  to this level if this was San Francisco or another city. Whatever they're running 
  would never have come to the attention of Congressman Istook.&quot; </p>
<p>Metro officials initially refused the ads when Change the Climate approached 
  them in 2001. </p>
<p>But when they were threatened with a First Amendment lawsuit backed by the 
  American Civil Liberties Union, officials relented. </p>
<p>&quot;The congressman would rather have us slug this out in court at great 
  expense than follow the judgment of our general counsel,&quot; Graham said. 
  &quot;The expense that's going to be involved is considerable. And this group 
  would like nothing more than to sue. It's better publicity than advertising.&quot; 
</p>
<p>Officials at the Boston subway system refused the same ad -- triggering a lawsuit 
  that has continued for three years. </p>
<p>Change the Climate officials said they were trying to attract attention to 
  certain issues. &quot;Yes, we wanted to stimulate debate, but we didn't think 
  a nutcase congressman would try to eliminate free speech,&quot; said Joseph 
  White, founder of Change the Climate. &quot;If they don't like what we're doing, 
  they ought to read the Constitution and get a life.&quot; </p>
<p>Swafford said Istook was particularly irritated because Metro gave the advertising 
  space to Change the Climate at no charge, as part of its policy of dedicating 
  a percentage of ad space to nonprofit groups for public service announcements. 
</p>
<p>Metro officials recently voted to abolish that policy, in part because it attracted 
  groups wanting to post controversial ads. </p>
<p>&quot;We're not just talking about allowing them to use the space,&quot; Swafford 
  said. &quot;They gave them free ad space. The First Amendment does not require 
  the government to give free space.&quot; </p>
<p>The space was worth $46,250. Istook's proposal would cut twice that amount 
  from Metro's budget &quot;as a warning to other transit agencies.&quot; </p>
