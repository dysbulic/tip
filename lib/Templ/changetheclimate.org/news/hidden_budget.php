<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><b>The Drug War's Hidden 
  Budget </b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Paul Armentano is a senior 
  policy analyst for the NORML Foundation in Washington, D.C. -- a not-for-profit 
  think tank which argues for the liberalization of marijuana laws. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This article first appeared 
  on the <a href="http://www.mises.org/" target="_blank">Ludwig von Mises Institute</a> 
  Web site. It is reprinted with permission. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Numbers never lie. Or 
  do they? With government, it's simply a matter of who's keeping the books. Take 
  America's so-called war on drugs, for instance. Last year, Congress earmarked 
  nearly $19 billion -- nearly twice what it spent on military operations in Afghanistan 
  -- to enforce U.S. drug laws. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This year's totals, however, 
  are remarkably different. According to the White House's 2003 &quot;National 
  Drug Control Strategy,&quot; released in February, the Bush administration will 
  now only spend some $11.2 billion fighting drugs. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">How can this be? Is some 
  part of government actually shrinking? On closer inspection, it's clear that 
  this year's supposed belt-tightening is only illusory. Thanks to new Enron-styled 
  accounting procedures initiated by the White House, America's drug war costs 
  a lot less than it used to -- at least on paper. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In a little publicized 
  announcement last year, officials from the White House Office of National Drug 
  Control Policy (ONDCP) revealed that they had developed a &quot;new methodology&quot; 
  for reporting the federal drug budget -- which had grown from less than $2 billion 
  annually in 1982 to $18.8 billion last year. Under this scheme, only funding 
  for agencies involved in so-called &quot;primary&quot; drug war activities is 
  now tabulated in the national anti-drug budget. As a result, more than two-thirds 
  of the agencies included in past years' budgets are conspicuously missing from 
  this year's financial totals. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">By far the largest and 
  most startling financial manipulations are within the Department of Justice 
  (DOJ), which reported a reduction of more than $5.5 billion dollars in drug-war 
  related expenses between 2002 and 2003. Remarkably, the majority of costs removed 
  are those associated with the incarceration and care of federal drug prisoners. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">How so? &quot;Based on 
  the criterion that they are associated with the secondary consequences of the 
  government's primary drug law enforcement and investigation activities,&quot; 
  such expenses will no longer be tabulated in the federal drug budget, the ONDCP 
  explained. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Other DOJ departments 
  and activities related to drug law enforcement, investigation and prosecution 
  are also deceptively missing from this year's tally. For example, annual funding 
  for INTERPOL, the U.S. Marshals Service, the U.S. Attorney's office, the federal 
  &quot;asset forfeiture fund&quot; and community policing are noticeably absent. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Millions of dollars in 
  annual funding for additional agencies previously tabulated in the national 
  drug war budget, such as the Department of Education, have been reduced without 
  explanation, while others -- including the Department of Transportation ($594 
  million in 2002), Department of Interior ($39 million in 2002) and the Department 
  of Agriculture ($29 million in 2002) -- have been expunged from the books all 
  together. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">To make matters even 
  more confusing, the 2003 &quot;National Drug Control Strategy&quot; makes virtually 
  no reference to the White House's new accounting procedures, and manipulates 
  past years' budgets to reflect the Feds' latest &quot;fuzzy math&quot; retroactively. 
  As a result, the White House is now claiming that America's war on drugs has 
  never cost more than $11 billion per year, even though the office itself previously 
  recorded surpassing that spending milestone in 1991. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">If you're searching for 
  the motivation behind the Drug Czar's deceptive accounting, look no further 
  than the polls. In recent years, nationwide surveys have consistently shown 
  that the majority of Americans believe the drug war's current &quot;do drugs, 
  do time&quot; approach to be ineffective, fiscally costly and doomed to fail. 
  A Pew Research Center poll conducted in 2001 found that 74 percent of the respondents 
  felt that the war on drugs could not be won. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">When given the alternative, 
  nearly seven out of 10 Americans say they support treatment for convicted drug 
  users rather than incarceration -- supported by an ABC news.com poll conducted 
  in March 2001. Nevertheless, despite the public sentiment, the percentage of 
  federal dollars dedicated to drug treatment and education programs has consistently 
  been minuscule compared to those earmarked for enforcement and interdiction. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Until now. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">At the same time the 
  White House is concealing billions in drug war related prison and interdiction 
  costs. Investigations of this year's budget by analysts from Common Sense for 
  Drug Policy and the Drug Policy Alliance reveal that the Drug Czar's office 
  is inflating their expenditures on drug treatment by including hundreds of millions 
  of dollars in alcohol treatment spending, which by law is specifically excluded 
  from the ONDCP's scope of activities. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As a result, the ONDCP 
  claims that this year's budget allots nearly equal amounts on drug treatment 
  as it does drug enforcement -- up dramatically from past years' ratios, which 
  favored enforcement nearly two to one -- despite making no substantive spending 
  changes. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Ultimately, the goal 
  of all this smoke and mirrors is to create the perception of a kinder, gentler 
  and less expensive drug war -- qualities favored by the American public but 
  seldom, if ever, delivered by federal drug policy. Of course, beneath the clouds 
  it's still business as usual. The only question is: Who's going to report the 
  Feds to the SEC?</font></p>
