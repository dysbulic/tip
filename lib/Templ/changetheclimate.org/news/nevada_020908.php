<p><font face="Verdana, Tahoma, Arial, Sans-serif"><b><font size="+1">Nevada Ponders 
  Looser Curbs on Marijuana</font></b> </font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">By Rene Sanchez<br>
  Washington Post Staff Writer<br>
  </font></p>
<p> </p>
<p></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">LAS VEGAS -- As soon 
  as he took over the nation's only campaign to make even recreational use of 
  marijuana legal, Billy Rogers laid down a few firm rules.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">No stoners hanging 
  out at the headquarters here. No pot plants either. And no straying from the 
  core message to voters: This is to free cops and courts from the burdens of 
  petty drug busts, not just to win the right to get high.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">&quot;If we were 
  a bunch of potheads, or all had tie-dyed T-shirts and long hair, we would be 
  easy targets, but we're not, and our opponents can't handle it,&quot; said Rogers, 
  a veteran political consultant from Texas. &quot;They're stunned. We're talking 
  about helping law enforcement. We're running even in the polls. Who would have 
  thunk it? We've got a real shot at winning.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Nevada, land of blackjack 
  and brothels, drive-through weddings and quickie divorces, appears tempted to 
  go to yet another live-and-let-live extreme this fall and ease its drug laws 
  in a way that few other states have even contemplated, much less put up for 
  a public vote.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">In a ballot measure 
  known as Question 9, Nevadans will decide whether to allow adults 21 and older 
  to possess and smoke as much as three ounces of marijuana, simply because they 
  feel like it, with no threat of criminal penalty. Under current state law, anyone 
  caught with that much marijuana -- which authorities say makes roughly 100 joints 
  -- could face four years in prison.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The November ballot 
  proposal forbids pot smoking in public, or while driving. It also bans marijuana 
  advertising and import of the drug. Nevada would have to grow and distribute 
  its own marijuana through state-licensed outlets, and could tax every sale. 
  Some officials say such a move could be a jackpot worth millions of dollars 
  for the state every year. To become law, voters will have to approve it twice, 
  first in November, then again in 2004.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">If the measure passes, 
  the implications could be huge here in Las Vegas, where most anything goes already. 
  Would Sin City be consumed by reefer madness and become an Amsterdam of the 
  desert, teeming with drug dealers or tourists jetting in just to take a few 
  legal tokes?</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">&quot;We don't know,&quot; 
  said Erica Brandvik, a spokeswoman for the Las Vegas Convention and Visitors 
  Authority, which has not taken a position on Question 9. &quot;But this is a 
  place where people come to do things they don't do at home. I can't say that 
  the news this might happen is jaw-dropping to people here.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Nevada's step is 
  part of a larger movement, rooted in the West but spreading nationwide, to rethink 
  a range of drug laws. The campaigns are growing despite the fierce objections 
  of federal officials, who are denouncing Question 9.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">&quot;This is a con,&quot; 
  said John Walters, director of the White House Office of National Drug Control 
  Policy. &quot;This isn't going to help law enforcement -- this is going to help 
  drug dealers. And do we really want drug tourism?&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">In the past six years, 
  voters in nine states (including Nevada) have legalized marijuana for medicinal 
  purposes. Several other states also have cut criminal punishments for simple 
  drug possession. Measures to either reduce penalties or allow medicinal use 
  will be on the ballot in Ohio, Michigan and Arizona this fall. Proponents of 
  a medical marijuana initiative in the District are pressing to get it on this 
  year's ballot.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Two years ago, California 
  voters approved Proposition 36, which required minor drug offenders to be sent 
  to treatment programs, not prison. Last year, Nevada lawmakers reduced the penalty 
  for getting caught with an ounce or less of marijuana from a felony to a misdemeanor 
  that carries no more than a $600 fine.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The latest campaign 
  here, like most others around the country on the drug, has been organized and 
  bankrolled almost entirely by the Marijuana Policy Project, a national group 
  based in Washington D.C. It spent $375,000 just to get the issue on the Nevada 
  ballot.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">More than 100,000 
  voters from around the state have signed petitions in support of Question 9. 
  And that is but one sign of the measure's apparent momentum. </font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Nevada's largest 
  newspaper, the Las Vegas Review-Journal, has expressed support for the measure, 
  saying it could end the &quot;needless harassment of individuals who peacefully 
  and privately use marijuana.&quot; No group has organized yet to oppose it. 
  Polls are deadlocked. And Nevada's governor, Republican Kenny Guinn, is vowing 
  to remain neutral on the subject this fall.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">&quot;It's a complicated 
  issue,&quot; said Greg Bortolin, his press secretary. &quot;He hasn't made up 
  his mind on it.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Bortolin noted that 
  many Nevada voters have a libertarian streak that makes them unpredictable, 
  especially on social issues. &quot;You have to throw out all the rules in Nevada 
  when it comes to politics,&quot; he said.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">That's one reason 
  the forces behind Question 9 chose Nevada as a staging ground. Another is that 
  the medical marijuana initiative here two years ago passed with more than 65 
  percent of the vote. Most of Nevada's electorate also lives in metropolitan 
  Las Vegas, making it relatively easy for a campaign to promote its message on 
  radio and television and at community meetings.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Marijuana is the 
  most widely used illicit drug in the country, federal officials say. According 
  to the 2000 National Household Survey on Drug Use, about 34 percent of Americans 
  ages 12 and older said they have tried the drug. And more than 600,000 Americans 
  were arrested for marijuana possession in 2000.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Proponents of Question 
  9 say that is exactly why it is necessary. Police are wasting too much time 
  and money busting small-time, private pot users, they argue, when they could 
  be focusing on violent crimes, even terrorism threats.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Rogers said the ballot 
  measure would bring other benefits: Cancer patients whose doctors want them 
  to use marijuana in treatment would have better access. And forcing Nevada to 
  distribute marijuana could wipe out the underground market for the drug -- because 
  the state could sell it for a lower price. Federal officials say the street 
  cost of marijuana varies from $400 to $1,000 per pound in the Southwest to $700 
  to $2,000 per pound in the Northeast.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">&quot;What we're 
  proposing is not radical -- it would still be a felony in Nevada to have four 
  ounces,&quot; said Rogers, whose office walls are lined with color-coded maps 
  of Nevada counties and polling precincts. &quot;But look, millions of Americans 
  have tried marijuana, and they didn't go crazy, and they didn't go on to harder 
  drugs. Yet we keep arresting thousands and thousands of people just for using 
  it in the privacy of their homes. It's got to stop. It's time.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Federal officials, 
  who have been cracking down on groups dispensing marijuana for medicinal purposes, 
  adamantly reject those claims. Walters said that Question 9 would set dangerous 
  policy. He predicted that many more Nevada teenagers and adolescents would fall 
  prey to marijuana addiction, and he warned baby boomers who smoked marijuana 
  in their youth, without dire consequences, to think twice about supporting the 
  measure.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">&quot;What many people 
  don't understand is that this is not your father's marijuana,&quot; Walters 
  said. &quot;What we're seeing now is much more potent.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Law enforcement officials 
  in Nevada seem divided. Some prosecutors say they are outraged. &quot;This wasn't 
  even an issue in Nevada until this outside group came here promoting it,&quot; 
  said Dick Gamick, the district attorney in Washoe County, which includes Reno. 
  &quot;It's ridiculous. We're telling our children drugs are bad, then we're 
  going to do this?&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">But last month, the 
  Nevada Conference of Police and Sheriffs, the largest law enforcement group 
  in the state, endorsed Question 9 -- briefly.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Andy Anderson, the 
  group's president, announced that its directors had decided unanimously to support 
  the measure because they believed it would allow police officers to &quot;better 
  spend our time responding to more life-threatening and serious incidents.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">A few days later, 
  the group publicly shifted its stance and said that it had not taken a formal 
  vote on the issue. Then it ousted Anderson.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The furor over Question 
  9 will intensify soon. Walters said that he plans to travel to Nevada to speak 
  against the measure. But its backers are planning television ads and say they 
  have volunteers ready to go door to door to campaign for its passage.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Tourists in Las Vegas, 
  meanwhile, are watching with amazement as the debate unfolds. &quot;I can't 
  imagine that many people would come just to smoke marijuana,&quot; said Martin 
  Mooney, a New Yorker visiting with his wife and children. &quot;But you never 
  know what to expect here. This place is wild.&quot; </font></p>
