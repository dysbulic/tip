<p><font face="Verdana, Geneva, Tahoma, Arial"><b>Current Felony Marijuana Inmates 
  in the US Tops 135,000</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><a href="http://bbsnews.net/bw2002-11-20.html" target="_blank">BBSNews</a> 
  2002-12-22 -- So often in drug policy a germ of an idea starts with one regular 
  person, and one public official or another at the other end of the spectrum 
  who could actually make positive policy changes happen. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In this case a USENET 
  poster named Dave recently wanted to know how many people are really behind 
  bars for marijuana offenses in the United States? And on the other end are public 
  officials such as DEA Administrator Asa Hutchinson who claimed on CNN's Crossfire 
  &quot;What I'm saying is that obviously it's another myth that we're locking 
  up all the [marijuana] users and they're filling up our prisons. That's not 
  the case. You still have to work pretty hard to get into prison for drug use 
  today.&quot;(aa) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This report, the first 
  of two parts, illustrates how such a claim is not very believable. If one wanted 
  to take all currently incarcerated felony marijuana offenders to a ball game 
  they could fill two large sports stadiums. For instance, the Texas Stadium which 
  houses the Dallas Cowboys and Ford Field, which hosts the Detroit Lions at 65,846 
  and 65,000 respectively.(ab) (Chart-1) To treat just the prisoners currently 
  behind bars for felony possession only to a ball game you could fill all 50,000 
  seats at Trice Field, home of the Iowa State Cyclones.(ac) </font></p>
