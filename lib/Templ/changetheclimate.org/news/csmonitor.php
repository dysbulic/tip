<P><font face="Verdana, Geneva, Tahoma, Arial"><b><font size="+1">Sheltering straphangers 
  from ... controversy?</font></b></font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>Where other transit 
  agencies around the nation back off, Boston doggedly censors subway ads - and 
  gets sued.</b></font></P>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">By Amanda Paulson | Staff 
  writer of The Christian Science Monitor </font></p>
<p></p>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><img src="http://www.csmonitor.com/2002/0220/csmimg/0220p2a.jpg" width="150" height="228" align="right" hspace="3" vspace="3">Once 
  again, the city that spawned the expression &quot;banned in Boston&quot; is 
  facing charges of government censorship.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This time, it's not controversial 
  plays or books being axed, but certain advertisements submitted to the city's 
  subways and buses - involving the relative criminality of marijuana use and 
  the &quot;Christianliness&quot; of Santa Claus.<br>
  </font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Despite repeated losses 
  over the years to advertisers suing on free-speech grounds, the Massachusetts 
  Bay Transportation Authority (MBTA) finds itself facing two fresh legal challenges 
  - both claiming that the public agency is overstepping its authority to regulate 
  ads.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Disputes over the use 
  of public advertising space - and whether free speech protections extend to 
  advertisers - are common. But the frequency with which the MBTA gets tangled 
  in these disputes - five times in the past 30 years - is unusual, especially 
  given the Massachussetts liberal reputation.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">"You read the transit 
  free-speech cases, and it's like a travel directory of the United States," says 
  Harvey Schwartz, the attorney representing both of the current plaintiffs. "But 
  you don't see any repeat cases.... Boston is stubborn."</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Indeed, many Bostonians 
  say there's a more profound cultural reality at play: A deep-seeded Puritan 
  morality and the strong political influence of the Roman Catholic Church that 
  form a conservative alterego to the region's otherwise liberal public face.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">One of the two new cases 
  is a federal lawsuit - tried earlier this month and awaiting the judge's verdict 
  - that was brought by a drug-policy reform group, Change the Climate. The Massachusetts 
  group encourages debate about the fairness of marijuana laws. The MBTA argues 
  that the group's ads tacitly encourage illegal activity.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">One of the group's three 
  ads submitted to the MBTA depicted a teenage girl, with the text: "Smoking pot 
  is not cool, but we're not stupid ya know. Marijuana is NOT cocaine or heroin. 
  Tell us the truth...."</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In rejecting the ads, 
  the MBTA says it was simply acting within its guidelines that prohibit ads that 
  contain violent criminal content, are harmful to children, or denigrate groups 
  based on gender, race, ethnicity, religion, or political affiliation.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">"We are an agency that 
  works with the public," says MBTA spokeswoman Lydia Rivera. "We owe the community 
  a transit that they shouldn't feel uncomfortable with.... Other transits might 
  agree [with us], but bend a little. And we don't. We refuse."</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Indeed, Washington, D.C., 
  for example, already ran similar ads from Change the Climate. Ever since its 
  transit agency tried unsuccessfully in 1984 to ban an ad that ridiculed Ronald 
  Reagan as president of the "jellybean republic," about the only major guideline 
  it's had is that ads can't carry inaccurate information, says Lisa Farbstein, 
  Washington Metropolitan Area Transit Authority spokeswoman.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Legally, a public agency 
  like the MBTA is allowed to create certain rules - like banning pornography 
  and profanity, for instance - so long as they are enforced equally, across political 
  views. In the case of Change the Climate, the MBTA says the ads fall into the 
  "harmful to children" category by tacitly encouraging illegal activity.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In the case of the Church 
  with the Good News - the plaintiff in the second new lawsuit - the MBTA claimed 
  the church's ad violated its prohibition against denigrating a group based on 
  religion. The church's ad promoted its website which, the MBTA says, declares 
  other religions false.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">But the court cases contend 
  that the agency guidelines are too vague. In the last similar legal battle, 
  a suit brought in 1994 by the AIDS Action Committee of Massachusetts, a federal 
  court handed down an opinion saying the MBTA used a policy so vague as to be 
  incomprehensible, says Sarah Wunsch, an attorney for the ACLU of Massachusetts.</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">"You'd think they'd learn," 
  says Ms. Wunsch, noting that in court earlier this month, the MBTA was still 
  arguing that "Boston is different: 'We have standards here.' "</font></P>
<P><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Indeed, those standards 
  are the root of a recurring trend. From its Puritan roots to the notorious Watch 
  and Ward Society in the 1920s which outlawed, among other things, Theodore Dreiser's 
  "An American Tragedy" and Walt Whitman's "Leaves of Grass," Boston has a very 
  long history of enforcing morality, says Stephen Nonack, head of reader services 
  at the Boston Athenaeum, an independent library that specializes in local history. 
  "The term 'banned in Boston' has got a familiar ring all over the country," 
  he adds.</font></P>
