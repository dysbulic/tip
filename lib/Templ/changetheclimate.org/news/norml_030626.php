<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>41 Percent Say Pot 
  Should Be Taxed, Regulated.</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">June 26, 2003</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Washington, DC: A growing 
  percentage of Americans believe the government should regulate marijuana in 
  a manner similar to alcohol, according to a national poll of 1,204 likely voters 
  by Zogby International and commissioned by the Drug Policy Alliance.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Forty-one percent of 
  respondents agree that &quot;the government should treat marijuana more or less 
  the same way it treats alcohol: it should regulate <br>
  marijuana, control it, tax it, and only make it illegal for children.&quot; 
  That figure is up significantly from the 34 percent of Americans who said they 
  supported legalizing marijuana in a 2001 USA Today/CNN/Gallup poll, and is almost 
  three times as high as the percentage who supported <br>
  legalization in 1972.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Hispanics (65 percent) 
  are most likely to agree that the government should tax and regulate marijuana. 
  Also agreeing are approximately half of Democrats, Independents, residents of 
  the East and West, Catholics, those with some college education, adults with 
  household incomes over $75,000 or more, and men.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">A separate Time Magazine/CNN 
  poll released last October found that 72 percent of Americans favored marijuana 
  decriminalization, a policy whereby marijuana offenders are fined but not jailed, 
  and 40 percent favored outright legalization. The latter figure was more than 
  double the percentage that backed marijuana legalization in 1986.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;The American public 
  are gradually coming around to the understanding that a legally regulated market 
  for marijuana, with age and quality controls, is far better than the unregulated 
  black market we have today,&quot; said NORML Executive Director Keith Stroup. 
  &quot;It's the same lesson we learned with alcohol during the 1920s. Criminal 
  prohibition is a failed public policy that does not work.&quot;</font></p>
