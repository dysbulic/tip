<p><font face="Verdana, Geneva, Tahoma, Arial" size="+1"><b> Liberals plan pot 
  law reforms</b></font><br>
  <b><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Government source says 
  bill coming in June</font></b></p>
<p><br>
  <font face="Verdana, Geneva, Tahoma, Arial" size="-1">TONDA MACCHARLES<br>
  OTTAWA BUREAU</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">OTTAWA&#151;The Chr&eacute;tien 
  government will bring in a new law in June to decriminalize simple marijuana 
  possession as part of a revamped National Drug Strategy that will include more 
  resources to combat drug trafficking, the Star has learned.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The move has met serious 
  objections around the cabinet table among senior government members who fear 
  it will further chill Canada-U.S. relations. Others worry about potential effects 
  on public health.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Still, Prime Minister 
  Jean Chr&eacute;tien backs the bill, which is expected to make possession of 
  small amounts of marijuana a mere ticketing offence that would not carry the 
  stigma of a criminal record, said a senior government source. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Chr&eacute;tien views 
  the sporadic and inconsistent enforcement of the current law against marijuana 
  possession across the country as &quot;a basic injustice&quot; which not only 
  stigmatizes some Canadians, but also breeds disrespect for the law, said the 
  insider.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Now, some young Canadians 
  convicted of pot possession, particularly in rural areas, are saddled with a 
  criminal record and even serve jail time, while others get away with only a 
  warning from police too busy to pursue minor pot offences.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The plan is to devote 
  more resources to fighting drug traffickers and give new money to police and 
  courts for that purpose.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">No decision has been 
  made on what amount of marijuana in a person's possession would be considered 
  &quot;small&quot; and for &quot;personal use&quot; under the law; it could be 
  as low as 10 grams (about 10 marijuana cigarettes) &#151; it could go as high 
  as 20 grams.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In the past, 30 grams 
  has been used as the dividing line between offences of simple possession and 
  those of drug trafficking. But the source said the government recognizes much 
  of the marijuana sold today is more potent than in the past. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The insider downplayed 
  any possibility of a border backlash from the United States, saying the bill 
  will be a made-in-Canada initiative to reflect this country's law enforcement 
  priorities and concerns.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;We're a sovereign 
  country and we make laws appropriate to Canadian circumstances,&quot; the source 
  said. &quot;And the intention of the drug strategy is to dedicate more resources 
  to deal with the problem of trafficking, which does speak to American concerns.&quot;</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The government believes 
  a strong argument can be made that a ticketing regime that imposes a penalty 
  &quot;would do more to discourage the use of marijuana than the current law.&quot;</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">John Walters, the White 
  House director of U.S. drug control policy, has repeatedly warned Canada against 
  liberalizing its drug laws, blaming much of the high-strength marijuana on U.S. 
  streets on Canadian pot growers and smugglers.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">But the Canadian government 
  has been emboldened by justice department opinion polls showing the percentage 
  of public support for decriminalization &quot;in the high 70s.&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The move is also supported 
  by the Canadian Medical Association and the Canadian Chiefs of Police, and by 
  the recommendations of two parliamentary committees last year.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">One, a special Commons 
  committee, called for the decriminalization of possession of small amounts of 
  marijuana. The other, a Senate committee, urged that possession of small amounts 
  of pot be legalized.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Chr&eacute;tien, the 
  source said, has long supported decriminalization, but in the nearly 30 years 
  since the Le Dain commission urged liberalization of marijuana laws, has watched 
  politicians avoid acting because of the &quot;political heat&quot; the issue 
  generates.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Who better to tackle 
  this issue now than this Prime Minister?&quot;</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Another government source 
  said Justice Minister Martin Cauchon has faced an uphill battle to persuade 
  fellow cabinet ministers John Manley, who is responsible for border concerns, 
  Health Minister Anne McLellan, and Solicitor-General Wayne Easter to support 
  the initiative. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Sources acknowledge there 
  is a public health perspective to the debate inside cabinet.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Marijuana is a 
  harmful substance. (There are) the same concerns one has with tobacco; the impairment 
  of driving skills &#151; that's definitely a concern,&quot; a source said. &quot;But 
  ... we're looking at taking steps that would in fact discourage the use in a 
  way where no discouragement occurs now, except in cases where a penalty that 
  is more severe than the harm is applied.&quot;</font> 
