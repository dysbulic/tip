  </font><font face="Verdana, Geneva, Tahoma, Arial"><b><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><img src="/images/behindbars.jpg" alt="Marijuana laws waste billions of taxpayer dollars to lock up non-violent americans." width="300" height="207" hspace="3" vspace="3" align="right"></font></b></font><font size="-1"> 
  WASHINGTON&#8212;The nation&#8217;s major drug policy reform groups today filed 
  a lawsuit against the U.S. government and the Washington Metropolitan Area Transit 
  Authority for censoring the speech of those critical of the government&#8217;s 
  &#8220;War on Drugs.&#8221;<br>
  <br>
  &#8220;The government does not want the public to know how badly our drug policy 
  has failed, so it is trying to silence Americans who oppose the War on Drugs,&#8221; 
  said Graham Boyd, director of the ACLU Drug Policy Litigation Project, one of 
  the groups involved in the lawsuit. &#8220;Fortunately, the First Amendment 
  clearly prohibits this kind of blatant viewpoint-based censorship.&#8221;</font></font></P>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The lawsuit responds 
  to an amendment buried in the 2004 federal spending bill that cuts off more 
  than $3 billion in federal funding from local transit authorities that accept 
  advertisements critical of current marijuana laws and other drug laws. With 
  at least $85 million at stake, the Washington Metro last week rejected an advertisement 
  submitted by a coalition of drug policy reform groups that criticizes marijuana 
  laws for wasting billions of taxpayer dollars and imprisoning non-violent offenders. 
  <br>
  <br>
  The rejected advertisement sponsored by the ACLU, Change the Climate, the Drug 
  Policy Alliance, and the Marijuana Policy Project shows a group of ordinary 
  people standing behind prison bars under the headline, &#8220;Marijuana Laws 
  Waste Billions of Taxpayer Dollars to Lock Up Non-Violent Americans.&#8221; 
  The same groups that sought to run the advertisement filed today&#8217;s lawsuit. 
  <br>
  <br>
  Representative Ernest Istook (R-OK) added the amendment to the 2004 omnibus 
  spending bill last year after voicing objections to marijuana law reform ads 
  by Change the Climate, an organization of parents and business executives that 
  educates the public on marijuana policy issues. The &#8220;Istook Amendment,&#8221; 
  as it is known, directs Congress to deny federal funds to local transit authorities 
  that display advertisements promoting &#8220;the legalization or medical use 
  of any substance listed in Schedule I &#8230; of the Controlled Substances Act.&#8221;<br>
  <br>
  &#8220;Our Constitution ensures that citizens can get both viewpoints on marijuana 
  issues,&#8221; said Joseph White, Executive Director of Change the Climate. 
  &#8220;Government ads promote expensive and harsh punishment, while our ads 
  suggest new ways of thinking that protect our children and save tax dollars.&#8221;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"> Although the only transit 
  authority named in the lawsuit is the Washington Metro, the Istook Amendment 
  applies to local transit systems across the United States &#8211; which under 
  the 2004 budget are slated to receive approximately $3.1 billion in federal 
  funding. For instance, San Francisco transit authorities have at least $100 
  million at stake, Denver has at least $80 million and New York at least $75 
  million, while Illinois could forfeit nearly $150,000,000 designated for three 
  major projects if transit authorities in the state were to accept a pro-drug 
  policy reform advertisement.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&#8220;Congress keeps 
  forgetting that there is no &#8216;drug exception&#8217; to the Constitution,&#8221; 
  said Ethan Nadelmann, Executive Director of the Drug Policy Alliance. &#8220;But 
  there&#8217;s a silver lining to the Istook Amendment in the opportunity it 
  presents for us to show just how foolish and extreme the drug war&#8217;s proponents 
  have become.&#8221;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"> The groups noted that 
  the same budget that includes the ban on advertising in support of marijuana 
  law reform also includes $145 million in taxpayer money for pro-War on Drugs 
  advertising that focuses primarily on an anti-marijuana campaign. Advertisements 
  appearing in public buses, trains, and subway systems have become a key strategy 
  for the White House&#8217;s billion-dollar media campaign, which delivers its 
  message via more than 1,300 media outlets nationwide. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&#8220;Ads promoting 
  the government&#8217;s position on marijuana are all over the Washington Metropolitan 
  Area Transit Authority and other public transit systems around the country,&#8221; 
  said Steve Fox, Director of Government Relations for the Marijuana Policy Project. 
  &#8220;For the government to advertise one view while banning ads expressing 
  differing opinions in the same forum is viewpoint discrimination and a clear 
  violation of the First Amendment.&#8221;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"> The lawsuit, ACLU v. 
  Mineta, asks the court to declare the Istook Amendment unconstitutional, to 
  order the Washington Metro System to accept the groups&#8217; paid advertisement, 
  and to prohibit the federal government from cutting off any funds to the Washington 
  Metro or any other transit authority that permits the display of advertisements 
  &#8220;promoting the legalization or medical use&quot; of marijuana or other 
  Schedule I drugs. </font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">&#8220;Congress is trying 
  to block needed political change by censoring speech that gives the public the 
  facts about drugs and drug laws,&#8221; said Arthur B. Spitzer, Legal Director 
  of the ACLU of the National Capital Area. &#8220;All messages should be allowed 
  to compete in the marketplace of ideas. That&#8217;s what America is about.&#8221;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial"> The lawsuit names Norman 
  Y. Mineta, U.S. Secretary of Transportation and the Washington Metropolitan 
  Area Transit Authority as defendants. The Washington D.C. law firm Arnold &amp; 
  Porter LLP will serve as lead counsel. </font></p>
