  &#8211; At 9:30 a.m. next Wednesday, April 28 the nation's major drug policy 
  reform groups will present oral arguments before Judge Paul L. Friedman in two 
  consolidated lawsuits filed against the United States government and the Washington 
  Metropolitan Area Transit Authority (WMATA) for censoring the speech of those 
  critical of the government's &quot;War on Drugs.&quot;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">With more than $85 million 
  at stake, WMATA rejected in February an advertisement submitted by the ACLU, 
  Change the Climate, the Drug Policy Alliance, and the Marijuana Policy Project 
  that promotes the reform of our nation&#8217;s marijuana laws. The groups are 
  challenging Section 177 of the 2004 federal spending bill, which directs Congress 
  to deny federal funds to local transit authorities that display advertisements 
  promoting &quot;the legalization or medical use of any substance listed in Schedule 
  I&#8230;of the Controlled Substances Act.&quot;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The lawsuits, filed on February 
  18, ask the court to declare the law unconstitutional because it coerces local 
  transit authorities into censoring pure political speech protected by the First 
  Amendment. The lawsuits name Norman Y. Mineta, U.S. Secretary of Transportation, 
  and Richard White, the CEO of WMATA, respectively, as defendants. </font></p>
