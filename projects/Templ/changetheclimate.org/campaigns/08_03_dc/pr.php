<p><font face="Verdana, Geneva, Tahoma, Arial"><b>Boston University Study Shows 
  Marijuana Legalization Saves Massachusetts Taxpayers $138 Million</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As Massachusetts cities 
  and towns struggle to keep teachers in public schools and police officers on 
  the streets, a Boston University study shows $121 million could be made available 
  to local communities if the state stopped arresting and prosecuting people for 
  marijuana trafficking and use. The study, commissioned by Change the Climate, 
  a non-profit organization that uses outdoor advertising to educate people about 
  marijuana issues, also shows that an additional $17 million could be raised 
  if marijuana sales were regulated and taxed. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The study was conducted 
  by Boston University Economics Professor Jeffery Miron. &quot;My study looks 
  at real expenses associated with marijuana prohibition,&quot; says Professor 
  Miron.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Change the Climate commissioned 
  the study to see how much money taxpayers could save if the Commonwealth legalized 
  marijuana. Joseph White, Founder and Executive Director of Change the Climate 
  explains, &quot;So many talented teachers are being laid off while politicians 
  in Boston raise their salaries and cut funds for local services. This study 
  shows there are new monies available. All we need is for politicians to show 
  some creativity and have some guts.&quot;</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;We want Massachusetts 
  voters to know that they have choices that affect the quality of life in their 
  communities, and one of those choices is whether taxpayers pay for public services 
  like police and fire protection or for arresting and prosecuting people for 
  marijuana violations,&quot; continues White, a father of three who lives in 
  Greenfield. </font></p>
<p></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">White explains, &quot;The 
  economic downturn in Massachusetts has taken a huge toll in local communities. 
  This study provides an answer to how the state can provide some financial relief 
  to cities and towns.&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In October 2002, Professor 
  Miron conducted a study that showed $24 million in tax savings if marijuana 
  was decriminalized. After his report was completed, 39 Massachusetts state legislative 
  districts voted in favor of non-binding resolutions to decriminalize marijuana. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Change the Climate is 
  a national organization of parents and business people who believe that restrictive 
  and counter-productive marijuana laws endanger our children and squander our 
  tax dollars. Change the Climate has conducted advertising campaigns in Washington 
  DC, California and Nevada. It has also sued the Massachusetts Bay Transit Authority 
  (MBTA) for denying advertising space to the organization in a much-watched First 
  Amendment case. </font></p>
