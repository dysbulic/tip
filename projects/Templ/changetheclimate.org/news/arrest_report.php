  seems low given that in 1998 about one quarter of the 4,000,000 adults under 
  supervision had a drug law violation and marijuana is the most commonly used 
  currently illicit drug in the United States. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The average cost of community 
