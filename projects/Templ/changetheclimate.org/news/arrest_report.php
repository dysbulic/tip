<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The huge human toll and 
  financial cost of marijuana prohibition in the United States needs to be weighed 
  against a legal and well regulated market. A good &quot;litmus test&quot; for 
  politicians considering drug policy should be to ask themselves two questions: 
  &quot;Can a twelve year old anywhere in the US belly up to the bar and be served 
  a shot of whiskey?&quot; And &quot;What policy best helps prevent minors from 
  access to alcohol: Prohibition or well crafted regulation?&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">States are facing huge 
  budget deficits from the huge drain prison spending has become. As shown in 
  this BBSNews report, Punitive marijuana prohibition has resulted in hundreds 
  of thousands of arrests each year, half of all drug arrests. And states are 
  feeling the pressure of warehousing non-violent offendors serving mandatory 
  minimum sentences. What is especially troubling is the fact that arsonists and 
  other criminals that may really pose a threat to their communities are being 
  released as well as non-violent drug prisoners. The New York Times reported 
  on December 18th, 2002: &quot;Montana, Arkansas and Texas, along with Kentucky, 
  have discovered loopholes that allow them to release convicted felons early, 
  getting around the strict truth-in-sentencing laws and no parole policies passed 
  in the 1990's that were supposed to prevent such releases.(8g)&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">We should not need &quot;loopholes.&quot; 
  Regulating the trade in marijuana would pay for itself in savings to cash strapped 
  States budgets. And there would be tax revenue. Then the arsonists, robbers 
  and those that perpetrate domestic violence would not have to be released early 
  into society. </font></p>
