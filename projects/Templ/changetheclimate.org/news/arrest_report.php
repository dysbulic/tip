<p><b><font face="Verdana, Geneva, Tahoma, Arial">Pre Sentence Incarceration in 
  Marijuana cases </font></b></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>Costs About $83 Million 
  Per Year</b> </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It's likely that a number 
  of defendants in marijuana cases are currently in jail awaiting trial or sentencing. 
  Not all get out on bail or a personal recognizance bond. From a BJS report using 
  data from 40 of the 75 most populous counties: </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;From 1992 to 1998, 
  the proportion of drug defendants increased from 30% to 37%. During this period, 
  the percentage of defendants charged with a property crime decreased from 35% 
  to 29%. The proportion of defendants charged with a public-order offense has 
  remained at just under 1 in 10.(4a)&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Which suggests that law 
  enforcement resources may have shifted away from property crime more towards 
  drug crime. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;For about 3 in 
  8 defendants, the most serious arrest charge was a drug offense (37.1%). Nearly 
  half (48%) of drug defendants were charged with drug trafficking. Overall, defendants 
  were more likely to be charged with drug trafficking (17.7%) or other drug offenses 
  (19.4%) than any other type of offense.&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Using BJS statistics 
  above which show 56,589 defendants in 1998 were convicted of marijuana offenses; 
  and relying on the pre trial report referenced below: </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Of the defendants 
  who had State felony charges filed against them in the Nation's 75 most populous 
  counties during May 1998 - Thirty-six percent were detained until case disposition, 
  including 7% who were denied bail.&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Suggesting about 12,022 
  state marijuana possession defendants and 8,349 marijuana trafficking defendants 
  for a total of 20,371 are spending time behind bars prior to and regardless 
  of case disposition in 1998. The stay for those released prior to trial and 
  or sentencing is one day or less for about half of those released, within one 
  week for 8 out of ten and the remaining 20% will be released within the month. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The report also states: 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;For 54% of felony 
  defendants in the 75 largest counties, adjudication of their case occurred within 
  3 months of arrest, and 75% of cases were adjudicated within 6 months of arrest. 
  By the end of the 1-year study period, 90% of all cases had been adjudicated.&quot; 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This finding indicates 
  that thousands of marijuana possession defendants each year go through the arrest 
  and period of incarceration awaiting release if one can make bail and then they 
  may wait as long as 6 months or even a year to learn their final sentencing 
  outcome. Regardless if final sentencing resulted in probation, a substantial 
  number of marijuana defendants, thousands of them, get a de facto jail sentence. 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">There were probably 20,000 
  persons were under correctional supervision awaiting trial for felony marijuana 
  offenses in 1998. Considering that about half will have their case disposed 
  of within 90 days, we could conservatively divide the annual cost of housing 
  an inmate by four and get a low approximation of the cost of housing marijuana 
  defendants awaiting final disposition of their case. The total cost amounting 
  to about $83 million for 1998. </font></p>
