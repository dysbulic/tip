<p><font face="Verdana, Geneva, Tahoma, Arial"><b>Conclusions</b></font><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">We bear some three quarters 
  of a million arrests for marijuana each year in the United States and about 
  135,000 persons are currently in prison after being convicted of either a state 
  felony or federal marijuana offense. This is a costly exercise at more than 
  $7 billion per year. At the time of this writing, US states are reporting a 
  combined $40 billion shortfall: &quot;Saying states face the most dire fiscal 
  situation since World War II, the National Governors Association(8a) and the 
  National Association of State Budget Officers released a report(8b) today that 
  concludes many states have exhausted budget cuts and drawing down rainy-day 
  funds and that the most difficult decisions still lay ahead.&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">And in light of information 
  from the Rand Corporation a week later, which shows beyond reasonable doubt 
  that the already discredited &quot;gateway theory&quot; of marijuana use leading 
  to heroin or cocaine use is simply not likely. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Marijuana use among 
  adolescents does not appear to act as a &quot;gateway&quot; leading to the use 
  of harder drugs such as cocaine and heroin.(8c)&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It's clear that at such 
  an enormous cost, there is some value to be gained by considering the wisdom 
  of jailing tens of thousands of Americans for no other reason than possession 
  of a plant that is currently considered politically incorrect. Just as this 
  report was going into public press another report has come out again decimating 
  the &quot;gateway theory.&quot; This one from Britain's Home Office said: &quot;The 
  analysis, based on recent survey data on nearly 4,000 children and young adults, 
  finds:No significant impact of soft drug use on the risk of later involvement 
  with crack and heroin. Very little impact of soft drug use on the risk of later 
  involvement in crime.(8d)&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">(8a) http://www.nga.org/nga/newsRoom/1,1169,C_PRESS_RELEASE^D_4693,00.html 
