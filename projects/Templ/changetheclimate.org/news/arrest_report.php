  probation for marijuana trafficking. The average length of probation is 3 years 
  4 months so it's possible as many as 54,000 adults were under probational supervision 
  mid-year 2001 for marijuana convictions starting from 1998. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">As many as 32,000 adults 
