<p><b><font face="Verdana, Geneva, Tahoma, Arial">Marijuana Offenders Lost Wages 
  Due to Incarceration </font></b></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>$4.2 Billion Lost 
  For 2002 </b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Contributed by Brian 
  Bennett </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">For many years the U.S. 
  Office of National Drug Control Policy has justified waging drugwar based in 
  part on the societal &quot;costs&quot; of illegal drug use. The greatest of 
  these costs are attributed to &quot;lost productivity&quot; which is said to 
  account for $110.5 billion (or 69 percent) of the $160.66 billion 2000 estimated 
  total costs. This cost categorization is comprised of (among other things), 
  premature death ($18 billion) and the cost of hospitalization and instutionalization 
  of drug users ($1.9 billion). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The largest chunk of 
  these lost productivity costs however, is that attributed to being incarcerated 
  which accounts for 32 percent of the total costs attributed to &quot;lost productivity.&quot; 
  People who are incarcerated are no longer able to earn a living and the monies 
  thus lost to society in this manner are said by the drug czar's office to total 
  an estimated $35.6 billion for the year 2000 alone. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">When you consider that 
  the drug czar's office has claimed that 77 percent of illegal drug users are 
  employed(6a), one has to wonder what is accomplished by the double cost to society 
  incurred by jailing them. Not only is it costing money to keep them warehoused, 
  but society is also losing the monies they otherwise would earn and contribute 
  to society. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The ONDCP estimated the 
  total using the Bureau of Labor Statistics Hourly Compensation Index. With some 
  648,544 prisoners incarcerated for &quot;drug-related&quot; crime, that works 
  out to $54,893 per person incarcerated. Of the total prisoner population thus 
  represented, 70.9 percent are serving sentences not for stealing money or hurting 
  people, but for violating drug laws. Assuming unemployment runs at 23% for marijuana 
  users per ONDCP claim, the 99,488 people in prison for marijuana charges are 
  thus &quot;costing&quot; society some $4.2 billion per year in lost wages and 
  other compensations.(6b) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">(6a) - http://usinfo.state.gov/regional/ar/colombia/strategy04.htm 
  <br>
