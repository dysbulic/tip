  drug arrest cost. Averaging the above four examples including lower cost, &quot;typical 
  incident&quot; costs, it seems reasonable conclude that an average marijuana 
  arrest conservatively costs $1076.79.(*) Total cost of marijuana arrests each 
  year are probably topping three quarters of a billion dollars per year at $779,198,667 
