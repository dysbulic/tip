  come from somewhere. As noted above almost a quarter of a million arrests are 
  made each year for marijuana offenses with about nine out of ten of those for 
  possession alone. Police resources are not cheap, the average police officer 
  hourly pay nationally works out to about $23.(7a) And then of course equipment, 
  vehicles and other overhead drives these costs up further. Especially it seems 
  in drug arrests. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Pennsylvania State Police 
