<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>$500 Million Per Year 
  for Incarceration Costs</b> </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Between 1984 and 
  1999, the number of defendants charged with a drug offense in the Federal courts 
  increased from 11,854 to 29,306. Of the 38,288 suspects referred to U.S. attorneys 
  during 1999, 31% were involved with marijuana; 28%, cocaine powder; 15%, crack 
  cocaine; 15%, methamphetamine; 7%, opiates; and 3%, other drugs.&quot; (<a href="http://bbsnews.net/bbsnphotos/faqlet-charts/fed-referred99.jpg" target="_blank">Chart-4</a>) 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;During 1999 criminal 
  cases involving 28,432 defendants charged with a drug offense were concluded 
  in U.S. district courts; 89% of defendants charged were convicted. ...About 
  60% of drug defendants adjudicated were detained between arrest and adjudication.&quot; 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Which would yield 25,304 
  total drug convictions. Federal convictions for marijuana offenses therefore 
  resulted in about 8,162 marijuana cases or 31% of the total. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Nearly 9 in 10 
  of those convicted of a drug offense received a sentence that included imprisonment, 
  8% were sentenced to a term of probation, and 3% received another type of sentence 
  such as a fine. The average prison term imposed during 1999 was 74 months.&quot;(1a) 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The report quoted above 
  also makes note that federal prisoners are required to serve 87% of their sentence. 
  That would work out to about 64 months. But the report also states elsewhere 
  that &quot;By contrast, those involved with marijuana received the shortest 
  sentences, on average -- 34 months.&quot; Using the lower figure and given that 
  these offenders will likely have to serve about 87% of their sentence, they 
  will probably serve about two and a half years. Meaning that even as 8,162 people 
  went to prison for felony marijuana charges in 1999 they were still there when 
  the year 2000 crop came in and so on. It is fair to conclude then that there 
  are at least as many as 24,488 in federal prison currently for marijuana offenses 
  from based upon what was occuring during the years 1999-2001. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Federal inmates also 
  cost more to house, $22,174(1b) each, as compared to state/local populations. 
  This federal detention of marijuana offenders over 1999-2001 cost over a half 
  a billion dollars per year or $542,996,912. It cost more than $1.5 billion dollars 
  to pay for housing more than 8000 inmates per year over the cumulative cycle 
  of their incarceration. </font></p>
