  4,000 in local jails. Nearly 50,000 people are likely currently incarcerated 
  for marijuana possession alone in state prisons and local jails. The average 
  cost of housing such an inmate is $16,600(2b) each. Meaning as much as $1.36 
  billion dollars is spent each year to pay for marijuana offenders in local and 
  state facilities. </font></p>
