<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">If we use the conviction 
  rate for marijuana possession and trafficking as indicated above for a baseline 
  we can arrive at a plausible approximation of how many people are serving probation 
  for marijuana offenses. 24% of adults on probation comes to 989,418. Taking 
  2.5% and 3.6% of those on probation for marijuana trafficking and possession 
  respectively, we find 35,619 for possession and 24,735 for trafficking for a 
  total of 60,354 people under community supervision. At an annual cost per marijuana 
  offender of $1066(*) each, those on probation in 1998 cost $64,337,364 to supervise. 
  Spanning the years from 1998-2001, probation for marijuana offenses added an 
  additional cost of $257 million. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Or, looked at in another 
