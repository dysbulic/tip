<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Showing that when 1999's 
  intake of convicted marijuana offenders were sentenced, the prior years intake 
  were already incarcerated and serving out their time, which was likely to be 
  about two years or less than half, or 47% of the average sentence being served. 
  When 2000's intake arrived those convicted in 1998 were still finishing up their 
  time meaning as many as 75,000 felony marijuana inmates could conceivably be 
  incarcerated currently in state prisons alone. With drug prosecutions on the 
  rise, it's not likely this number has decreased. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Another 13,581 marijuana 
  offenders are sentenced to local jail for stays of a year or less. The average 
