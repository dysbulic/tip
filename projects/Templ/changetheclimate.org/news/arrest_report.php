  in state courts in 1998. 44% of drug offenders are sentenced to state prison 
  meaning about 24,899 total marijuana offenders likely go to prison each year. 
  The average drug sentence to prison was almost five years in 1998 yet according 
  to the same report: </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;Felons convicted 
