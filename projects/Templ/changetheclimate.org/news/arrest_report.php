<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Without the so-called 
  &quot;gateway theory&quot; the underpinnings of all these arrests become suspect. 
  It is time to revisit some basic assumptions about where resources are actually 
  being spent that could be better brought to bear on hard drug use, such as tobacco, 
  heroin and cocaine as well as alcohol. It's time to admit that current legal 
  distinctions between drugs in the US do not equal a given drugs pharmacology 
  or possible risk from harm. New strategies must be considered. New to us anyway. 
  Dutch &quot;coffee shops,&quot; quite successfully and happily, just passed 
  their thirtieth anniversary. The Ottawa Citizen in a story on December 19th, 
  reported &quot;Quebec court Judge Gilles Cadieux, who had postponed making a 
  decision on the case about 10 times, said the absence of a legal source of marijuana 
  takes away the right to life and liberty for those who need it.(8e)&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In this closely watched 
  medical marijuana case in Canada, &quot;Marc-Boris Saint-Maurice, director of 
  the Compassion Club, and Alexandre Neron, who worked at the club, were accused 
  of possession of marijuana and trafficking the drug.&quot; Saint-Maurice also 
  leader of the Marijuana Party in Canada promptly opened a Website offering medical 
  marijuana for sale, $30 for two grams and $120 for 10 grams of marijuana said 
  to be at least 8% THC, the most demonized active ingredient in marijuana. (Which 
  is also quite legal in the United States as Marinol.) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This kind of pressure 
  will surely be acutely felt in the US. Indeed the issue has been brought to 
  a boil in Santa Cruz, California where federal authorities reportedly handcuffed 
  a paraplegic to a bed in a medical marijuana case that sparked nationwide outrage. 
  From the WAMM Website: &quot;A hearing has been set for the Motion to Return 
  Property filed shortly after the DEA raid on Wo/Men&#146;s Alliance for Marijuana 
  (WAMM), a Santa Cruz based medical marijuana cooperative. The motion requests 
  the return of all personal property including 137 medical marijuana plants taken 
  in a DEA raid in early September from the 238 patient collective garden. The 
  raid touched off protests at federal buildings around the San Francisco Bay 
  area. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Attorney Ben Rice emphasized 
  this will be the first case heard after a 9th Circuit Court of Appeals unanimous 
  decision earlier this week in the Conant v. Walters case in which Valerie Correl, 
  Director of WAMM, is a plaintiff. The decision emphasized the government cannot 
  revoke the licenses of California doctors who recommend medical marijuana to 
  their patients. It also states &quot;Medical marijuana, when grown locally for 
  personal consumption, does not have any direct or obvious effect on interstate 
  commerce.(8f)&quot;&quot; </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">(8e) http://www.canada.com/ottawa/story.asp?id=3421C064-0605-454B-9E9A-0917A29251F1 
