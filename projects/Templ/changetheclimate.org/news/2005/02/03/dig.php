<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>These Laws are 
  So Fucking Stupid</strong></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial"><strong>Fresh off a Masterfully 
  Played Rout of the T, Change the Climate's Joe White is Continuing to Change 
  the Way Joe Sixpack Thinks About Weed Reform in America</strong></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size=-1><strong>By:</strong> Paul 
  McMorrow, Weekly Dig Staff Writer</font></p>
<font size="-1" face="Verdana, Geneva, Tahoma, Arial"> 
<p>Though it took Joe White four years and two rounds of litigation to force a 
  stubborn MBTA into posting his advertisements advocating marijuana reform, White, 
  a Greenfield, MA, resident who heads the marijuana advocacy group Change the 
  Climate, knew the region's transportation agency was fighting a losing battle 
  as soon as its lawyers deposed him.</p>
<p>"They knew this was a clear-cut free speech case, and they knew they were going 
  to lose," White boasts. "They were just milking the T for money-I mean, their 
  lawyers were pot smokers!"</p>
<p>Last month, the US Court of Appeals for the First Circuit confirmed what White 
  knew all along, ruling that the T had violated White's First Amendment rights 
  when it refused to run three Change the Climate ads in 2000. The appellate court 
  soundly rejected the T's argument that the ads encouraged minors to smoke marijuana, 
  instead finding that the T had unconstitutionally silenced Change the Climate 
  for criticizing government drug policy. Soon, White promises, Change the Climate 
  will parlay its win against the T into a provocative, high-profile ad campaign.</p>
<p>The victory over the T is just the latest in a series for White, who runs Change 
  the Climate almost single-handedly (and on top of a full-time job, to boot.) 
  Over the last four years, Change the Climate has defied the Bush administration's 
  offensive against marijuana, recording a string of otherwise rare victories 
  for drug reform, while drawing the ire of state and federal authorities. And 
  in the process, he has redefined how pot activists do business.</p>
<p>At first blush, Greenfield would seem an ideal place to launch a marijuana 
  advocacy group&#8212;that is, if Greenfield did indeed live up to its name&#8217;s 
  narcotic connotation, with rolling fields of marijuana swaying in the breezes 
  of Western Mass. As it is, though, Greenfield is remarkable only in its unremarkable 
  nature. It&#8217;s a wholesome-looking old mill town in the middle of nowhere, 
  boasting a main drag&#8212;Main Street, if you believe it&#8212;that&#8217;s 
  lined with banks, bakeries, war memorials and small-time department stores. 
  From Boston, it&#8217;s reached by running a gauntlet of auto parts stores and 
  farmers&#8217; co-ops. When I stumbled into the local Dunkin&#8217; Donuts, 
  I was greeted by a group of local youths, cutting school to sing Christmas carols, 
  of all things. </p>
</font> 
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">But in that same 
  Dunkin&#8217; Donuts parking lot, I also spied the first MoveOn.org and Human 
  Rights Campaign stickers I&#8217;d seen in an hour. Despite the aw-shucks veneer, 
  Greenfield is represented&#8212;in the State House and in Congress&#8212;by 
  strident liberals. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">The town&#8217;s 
  paradoxes shine in Joe White. White doesn&#8217;t cavort around town in a three-piece 
  hemp suit and a marijuana leaf crown, nor did he found Change the Climate as 
  a way to promote the Ideology of Freedom, man. But, as the MBTA and the Bush 
  administration have discovered, he is shrewd, sophisticated and relentlessly 
  dedicated to his cause. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;It&#8217;s 
  amazing how large this issue has become in my personal and professional life,&#8221; 
  White explained from his home office recently. &#8220;And it&#8217;s amazing 
  how unashamed I am of that fact.&#8221; </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">White is also 
  unashamed of how markedly he stands out among marijuana activists. He readily 
  confesses to projecting &#8220;a certain conservative, preppy look,&#8221; to 
  not listening to the Grateful Dead &#8220;as much as I should&#8221; and to 
  staying far away from High Times &#8211; &#8220;I&#8217;m not in that part of 
  the consumer movement,&#8221; he explains. And, although both he and his 16-year-old 
  son attended this year&#8217;s rain-soaked Freedom Rally, neither smoked. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;I&#8217;ve 
  been more of a non-activist over the years,&#8221; White said. &#8220;I got 
  involved with this issue partially as a business endeavor, but really in response 
  to my teenager, who&#8217;s now 23, asking me why adults lie to kids about marijuana. 
  He&#8217;s part of the DARE generation, the generation that&#8217;s been bombarded 
  by ads that make marijuana out to be the most dangerous substance on the planet. 
  He told me that he didn&#8217;t smoke, but his friends did, and if we weren&#8217;t 
  telling them the truth about marijuana, maybe we weren&#8217;t telling them 
  the truth about other drugs, more dangerous drugs. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;And once 
  I started learning how much money is spent on arresting and prosecuting people 
  for marijuana&#8212;how much money is spent disseminating inaccurate information 
  about marijuana&#8212;the more obsessed I became about telling people the truth 
  and educating the public about what a huge fiasco this is. It costs us billions 
  of dollars a year. Plus, it&#8217;s easy for me &#8211; I smoke pot. I&#8217;m 
  fighting against stupid laws that go against my self-interest. Nobody ever died 
  from smoking marijuana &#8211; that&#8217;s why these laws are so fucking stupid.&#8221;</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">Whatever steep 
  odds marijuana reform faced when White founded Change the Climate in 2000 have 
  swelled exponentially under George W. Bush, who has made what White calls the 
  &#8220;demonization&#8221; of marijuana the centerpiece of his administration&#8217;s 
  drug policy. White House Drug Czar John Walters has called marijuana the most 
  dangerous drug in America; Bush&#8217;s anti-drug ad campaign focuses almost 
  exclusively on marijuana, rather than OxyContin or heroin; and John Ashcroft&#8217;s 
  Justice Department has sunk considerable resources into very publicly persecuting 
  the cultivators and users of medicinal marijuana. Most recently, the Drug Enforcement 
  Administration forcefully blocked a UMass-Amherst professor&#8217;s bid to study 
  the medicinal effects of marijuana, essentially stymieing any FDA approval of 
  medical marijuana while the Justice Department extinguishes medical users and 
  suppliers at the state level. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">In the face of 
  all this, Joe White has enjoyed a series of high profile victories against the 
  marijuana clampdown. White has not done this by orchestrating public showings 
  of mass dissatisfaction, nor by pounding his head against the Capitol&#8217;s 
  walls; instead, Change the Climate fights for change by funding visible advertisements 
  that weigh the costs of marijuana laws and then baiting government officials 
  into highly public confrontations that expose the flaccid rationale behind those 
  laws. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;We are 
  flexible and entrepreneurial enough to pick and choose our battles,&#8221; White 
  explains.</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">In the first of 
  those chosen battles, Change the Climate&#8212;with the ACLU&#8217;s legal support&#8212;celebrated 
  George W. Bush&#8217;s inauguration by placing 560 pro-marijuana reform ads 
  on Washington Metro buses and subway stations. Initially, the Metro rejected 
  Change the Climate&#8217;s submission, &#8220;but they backed down when we threatened 
  to sue, and they realized they&#8217;d lose in court.&#8221; All of which was 
  according to plan, White says. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;At first, 
  we approached billboard companies, and they politely declined. But transportation 
  systems are publicly funded, so they&#8217;re subject to the free speech provisions 
  of the Constitution.&#8221; White not only won a forum for criticizing government 
  drug policy, but he also garnered national attention for sticking his thumb 
  in Bush&#8217;s eye as soon as the boy president arrived in Washington. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">Several rounds 
  of advertisements touting drug policy reform followed Change the Climate&#8217;s 
  initial Metro salvo. One featured a business executive rhetorically asking if 
  it&#8217;s his choice to smoke pot after his chemotherapy and then being told, 
  &#8220;Wrong.&#8221; Another argued that &quot;Police are too important ... 
  too valuable ... too good ... to waste on arresting people for marijuana when 
  real criminals are on the loose&quot;; and a third asked, &quot;Why do kids 
  go to jail for doing what politicians did when they were young? Tell us the 
  truth.&quot; A fourth tried to hit Georgetown yuppies in their wallets: &#8220;5.2 
  million marijuana arrests since 1992. It cost you $4.5 billion. Is it really 
  worth it?&#8221;</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">Then, in December 
  2003, congressmen who had been chafing at Change the Climate&#8217;s DC-area 
  advertisements suffered a full-blown conniption when they were greeted with 
  Metro ads saying, &#8220;Enjoy better sex! Legalize and tax marijuana.&#8221; 
  </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;That ad 
  actually came from a conversation I had with one of the executives who fund 
  our campaigns,&#8221; White recounts. &#8220;He told me marijuana works much 
  better than Viagra, that people should really try it.&#8221;</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">Ernest Istook, 
  a Republican congressman from Oklahoma, declined this suggestion, instead labeling 
  the ad &#8220;shocking&#8221; and pushing legislation that stripped $92,500 
  in federal appropriations from the Metro &quot;as a warning to other transit 
  agencies.&quot; </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;I must 
  assure [sic] that [the Metro] will learn the proper lessons from this experience 
  and will only accept appropriate ads in the future,&quot; Istook cautioned. 
  </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">The congressman 
  subsequently authored legislation that barred any federally subsidized transit 
  authority from becoming &#8220;involved directly or indirectly in any activity 
  that promotes the legalization or medical use of any substance listed in schedule 
  I of section 202 of the Controlled Substances Act,&#8221; meaning any transit 
  authority that ran ads from Change the Climate would lose its federal funding. 
  </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;This is 
  a right-wing nut in Congress who probably wants unmarried women to wear chastity 
  belts,&#8221; White says of Istook. &#8220;He was foaming at the mouth&#8212;sex 
  and marijuana! That was one high point in Change the Climate&#8217;s efforts&#8212;to 
  successfully bait a member of Congress, who had the audacity to ban all ads 
  from transit agencies around the country that expressed a different viewpoint 
  than the government&#8217;s. It was quite a surprise, and a great opportunity 
  for marijuana reform. I did not imagine that I&#8217;d ever be personally targeted 
  by the Congress, that I&#8217;d sue Bush over free speech. I don&#8217;t think 
  our messages are too radical.&#8221; </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">Radical or not, 
  Istook&#8217;s attack on Change the Climate garnered national attention, allowing 
  White to ridicule Istook in The Washington Post while pushing his case for marijuana 
  reform&#8212;for free. Change the Climate then teamed with the Marijuana Policy 
  Project, the Drug Policy Alliance and the ACLU to challenge the constitutionality 
  of Istook&#8217;s anti-drug stance, suing the Bush administration and its transportation 
  secretary, Norman Mineta. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">Last June, a federal 
  judge overturned the Istook amendment in a strongly worded opinion, finding 
  that the legislation did &#8220;not express Congress&#8217; desire to shield 
  the nation&#8217;s youth from all conversation regarding controlled substances 
  but rather eliminates only dialogue regarding legislative reform of the narcotics 
  laws, and the prohibition applies to minors and adults alike &#8230; The government 
  has articulated no legitimate state interest in the suppression of this particular 
  speech other than the fact that it disapproves of the message, an illegitimate 
  and constitutionally impermissible reason.&#8221;</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">It was a remarkable 
  victory, even more so considering it took place in the midst of a four-year 
  period that could safely be called a rout of the marijuana reform movement. 
  </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">After initially 
  rejecting three Change the Climate ads in 2000, the MBTA assumed the rising 
  national anti-marijuana wave would pound White into oblivion. And it nearly 
  did.</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">In August 2003, 
  a federal judge ruled that the MBTA had not violated Change the Climate&#8217;s 
  constitutional rights when it rejected three pro-marijuana ads; instead, the 
  judge argued that Change the Climate&#8217;s ads misled the public, encouraged 
  illegality and targeted children. But Change the Climate, again with the help 
  of the ACLU (&#8220;Probably the most important organization in America,&#8221; 
  White argues), appealed, and in late November, a three-judge panel ruled in 
  Change the Climate&#8217;s favor, striking down the T&#8217;s stance as unconstitutional. 
  </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">During that appeal, 
  T officials argued that Change the Climate&#8217;s ads encouraged teens to use 
  marijuana&#8212;arguments similar to the ones employed by the Transportation 
  Department during the Istook trial. In one ad, a teenage girl with a baseball 
  cap on backwards declares, &quot;Smoking pot is not cool, but we're not stupid, 
  ya know. Marijuana is NOT cocaine or heroin. Tell us the truth &#8230;.&quot; 
  In another, a woman says, &quot;I've got three great kids. I love them more 
  than anything. I don't want them to smoke pot. But I know jail is a lot more 
  dangerous than smoking pot.&quot; </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">In the December 
  24, 2003 issue of the Dig, MBTA spokesman Joe Pesaturo wrote a letter justifying 
  the T&#8217;s years-long battle with Change the Climate by citing both concern 
  for our children and the not-yet-unconstitutional Istook amendment. &#8220;In 
  defending its decision to reject ads from Change the Climate, the T&#8217;s 
  legal fees have totaled less than 1 percent of the amount Viacom pays the T 
  for exclusive rights to the system&#8217;s advertising space,&#8221; Pesaturo 
  argued. &#8220;It&#8217;s a very small price to pay to protect children and 
  others from harmful and irresponsible messages that encourage, promote and glamorize 
  illegal drug activity. Two judges, including a federal one, have sided with 
  the T. Now, with Congress approving a measure that penalizes transit agencies 
  that accept pro-drug ads, the T doesn&#8217;t have to worry about losing millions 
  in federal dollars.&#8221; </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">Five months after 
  Pesaturo&#8217;s letter, the Istook amendment was declared unconstitutional; 
  six months after that, Pesaturo&#8217;s other plank, defense of our children, 
  was similarly dismissed by a federal appellate panel that ruled, &#8220;The 
  ads do not advocate illegal drug use. Rather, these two ads make a sophisticated 
  argument that the criminalization of marijuana imposes worse consequences on 
  society than would alternatives.&#8221; </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;I&#8217;m 
  glad the judges have good taste,&#8220; White says, &#8220;but I&#8217;m not 
  happy that this suit enabled the Commonwealth to squander $1 million, money 
  that could be better spent on the average T rider. That&#8217;s a shitload of 
  money, especially in a losing cause. If the T were smart, they would&#8217;ve 
  just put the ads up. We would&#8217;ve gotten one day of press, and then in 
  30 days, the ad would be gone. But they gave us the opportunity to talk about 
  the case for marijuana reform for four years&#8212;and we&#8217;re not done 
  yet.&#8221;</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">While White is 
  careful to credit his peers for the work they do, pointing to collaborations 
  with the Marijuana Policy Project, the Drug Policy Alliance and MASS CANN, and 
  saying, &#8220;This issue won&#8217;t be won by a single organization or message,&#8221; 
  it&#8217;s clear that Change the Climate has established its own hyper-successful 
  niche in the marijuana reform movement. That&#8217;s why it&#8217;s no accident 
  that, in spite of vehement federal opposition, Change the Climate has been able 
  to advance its reformist cause against the Washington Metro, the MBTA and the 
  Bush administration. Joe White&#8217;s success stems from taking marijuana reform 
  to a place it hasn&#8217;t been in a while&#8212;the average American.</font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;I can&#8217;t 
  think of many organizations that are talking directly to citizens about marijuana 
  issues, so we decided to be that voice,&#8221; White explains. &#8220;We focus 
  more on popular culture, more on the way that people get their information&#8212;through 
  advertising. We don&#8217;t have lobbyists; we don&#8217;t do publications. 
  One business executive told me, &#8216;I love Change the Climate, because you&#8217;re 
  on the streets, talking to the people.&#8217; We don&#8217;t hide our ads in 
  glossy magazines for policy wonks; our ads are in front of average citizens 
  who know that we&#8217;re speaking the truth. We want to communicate information 
  and get people thinking. And that hopefully motivates people to talk to their 
  political representatives. </font></p>
<p><font size="-1" face="Verdana, Arial, Helvetica, sans-serif">&#8220;That&#8217;s 
  the whole notion behind Change the Climate&#8212;in order for there to be legislative 
  change, politicians need to feel comfortable voting for change, instead of being 
  chickenshit. If our advertising gets people talking more, politicians will feel 
  more comfortable voting, or even advocating, for change. The climate around 
  marijuana needs to change. We need to de-demonize it. We&#8217;re looking at 
  the preconditions for change, not at the change itself, because we need a broader 
  discussion before there can be any legislative change. Asking for money to knock 
  on more politicians&#8217; doors isn&#8217;t going to initiate change.&quot; 
  </font> 
