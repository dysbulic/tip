<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>Long-Term Use Of Marijuana 
  Has Minimal Impact On Cognition, Study Says Results Reveal No &quot;Substantial, 
  Systematic Effect&quot; Of Chronic Pot Smoking On Neurocognitive Performance</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">San Diego, CA: Smoking 
  marijuana long-term has little-to-no impact on neurocognitive performance in 
  adults, according to findings published last week in the Journal of the International 
  Neuropsychological Society. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Researchers at the University 
  of California, San Diego (UCSD) School of Medicine analyzed data from 15 previously 
  published, controlled studies regarding the impact of long-term, recreational 
  marijuana use on neurocognitive performance - including simple reaction time, 
  verbal/language skills, executive function, motor skills, earning, and recognition, 
  among other measurements. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;The results of 
  our meta-analytic study failed to reveal a substantial, systematic effect of 
  long-term, regular cannabis consumption on the neurocognitive functioning of 
  users who were not acutely intoxicated,&quot; authors determined.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Marijuana smokers were 
  indistinguishable from non-users in six of the eight neurocognitive ability 
  areas surveyed, the study found. The two exceptions were in the domains of &quot;learning&quot; 
  and &quot;forgetting&quot; (failure to recall or recognize), though authors 
  said that marijuana's apparent negative impact on these skills was so slight 
  that &quot;the 'real life' impact of such a small and selective effect is questionable.&quot;</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Because the studies did 
  not measure volunteers' neurocognitive abilities prior to their marijuana use, 
  nor distinguish between marijuana-only smokers and polydrug users, authors said 
  it was impossible to verify whether the small but measurable decrements to learning 
  and forgetting were due to marijuana or other factors. Authors did note, however, 
  that by failing to control for such factors, they &quot;actually increased the 
  likelihood of finding a [negative] cannabis effect.&quot; As a result, they 
  said it was &quot;surprising to find [that marijuana has] such few and small 
  effects&quot; on neurocognitive abilities. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">&quot;The small magnitude 
  of the effect[s] ... suggests that cannabis compounds ... should have a good 
  margin of safety from a neurocognitive standpoint,&quot; especially in controlled 
  settings, authors concluded.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Allen St. Pierre, Executive 
  Director of The NORML Foundation, said that the results reinforce NORML's core 
  position that marijuana prohibition causes far greater harm than the responsible 
  use of marijuana itself. &quot;The only significant long-term impact marijuana 
  has upon cognitive function is upon those who continue to irrationally demonize 
  and criminalize this plant,&quot; he said.</font></p>
