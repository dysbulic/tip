<p><b><font face="Verdana, Tahoma, Arial, Sans-serif" size="+1">British Parliament 
  Committee Urges: END MARIJUANA ARRESTS</font></b></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">CONTACT: Bruce Mirken, 
  MPP director of communications 202-462-5747 x113</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">WASHINGTON, D.C. 
  -- Britain moved a step closer to making marijuana possession a non-arrestable 
  offense today as the Home Affairs Select Committee of Parliament endorsed a 
  proposal to sharply reduce marijuana penalties. The idea was put forth last 
  fall by U.K. Home Secretary David Blunkett and was endorsed in March by the 
  Advisory Council on the Misuse of Drugs, the government's scientific body on 
  drug policy.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Today's committee 
  report, the result of a 10-month-long investigation, marks a key step in the 
  government's consideration of this major policy change.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Blunkett has proposed 
  that marijuana be &quot;downgraded&quot; from Class B to Class C, the &quot;least 
  harmful&quot; category of illegal drugs under British law. Class B includes 
  drugs of &quot;intermediate&quot; danger, including barbiturates and amphetamines. 
  Class C drugs include Valium and anabolic steroids. Such drugs remain illegal, 
  but possession generally brings a warning or fine rather than arrest and jail.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">In the United States, 
  marijuana remains in Schedule I, the category reserved for substances such as 
  heroin and LSD, which are deemed the most dangerous.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The report, available 
  at <a href="http://www.publications.parliament.uk/pa/cm200102/cmselect/cmhaff/318/31802.htm" target="_blank">www.publications.parliament.uk/pa/cm200102/cmselect/cmhaff/318/31802.htm</a>, 
  urges a shift in anti-drug policy to focus on abuse and misuse rather than on 
  occasional users who do little harm to themselves or society. Regarding marijuana, 
  the document points out the damage done by excessively strict laws, arguing 
  that &quot;we do not believe there is anything to be gained by exaggerating 
  its harmfulness. On the contrary, exaggeration undermines the credibility of 
  messages that we wish to send regarding more harmful drugs.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The parliamentary 
  group's conclusions echo those of the Advisory Council, which stated, &quot;The 
  high use of cannabis is not associated with major health problems for the individual 
  or society.&quot; The council also noted that the addiction potential of marijuana 
  is &quot;well below nicotine and alcohol.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">&quot;The British 
  government is taking a thoughtful, science-based approach to reconsidering its 
  drug laws,&quot; commented Bruce Mirken, director of communications for the 
  Washington, D.C.-based Marijuana Policy Project. &quot;In this country, policymakers 
  regularly commission expert reports and then ignore them when they don't like 
  the findings. From the National Commission on Marihuana and Drug Abuse in 1972 
  to the 1999 Institute of Medicine review of the data on medical marijuana, experts 
  have repeatedly challenged the assumptions underlying marijuana prohibition. 
  The British are listening to their experts. Maybe someday the U.S. will do the 
  same.&quot;</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The Marijuana Policy 
  Project works to minimize the harm associated with marijuana -- both the consumption 
  of marijuana and the laws that are intended to prohibit such use. MPP believes 
  that the greatest harm associated with marijuana is imprisonment. To this end, 
  MPP focuses on removing criminal penalties for marijuana use, with a particular 
  emphasis on making marijuana medically available to seriously ill people who 
  have the approval of their doctors. For more information, please visit <a href="http://www.mpp.org" target="_blank">www.mpp.org</a>.</font><br>
