<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>Marijuana Arrests For Year 2003 Hit Record High, FBI Report Reveals Pot Smokers Arrested In America At A Rate Of One Every 42 Seconds</strong></font></p>

<p> <font size="-1" face="verdana, geneva, tahoma, arial"> Washington, DC: Police 
  arrested an estimated 755,187 persons for marijuana violations in 2003, according 
  to the Federal Bureau of Investigation's annual Uniform Crime Report, released 
  today. The total is the highest ever recorded by the FBI, and comprised 45 percent 
  of all drug arrests in the United States. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">&quot;These numbers belie 
  the myth that police do not target and arrest minor marijuana offenders,&quot; 
  said Keith Stroup, Executive Director of the National Organization for the Reform 
  of Marijuana Laws (NORML), who noted that at current rates, a marijuana smoker 
  is arrested every 42 seconds in America. &quot;This effort is a tremendous waste 
  of criminal justice resources, costing American taxpayers approximately $7.6 
  billion dollars annually. These dollars would be better served combating serious 
  and violent crime, including the war on terrorism.&quot; </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">Of those charged with 
  marijuana violations, 88 percent - some 662,886 Americans - were charged with 
  possession only. The remaining 92,301 individuals were charged with &quot;sale/manufacture,&quot; 
  a category that includes all cultivation offenses - even those where the marijuana 
  was being grown for personal or medical use. In past years, approximately 30 
  percent of those arrested were age 19 or younger. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">&quot;Present policies 
  have done little if anything to decrease marijuana's availability or dissuade 
  youth from trying it,&quot; Stroup said, noting that a majority of young people 
  now report that they have easier access to pot than alcohol or tobacco. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">The total number of marijuana 
  arrests for 2003 far exceeded the total number of arrests for all violent crimes 
  combined, including murder, manslaughter, forcible rape, robbery and aggravated 
  assault. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">Marijuana arrests for 
  2003 increased 8 percent from the previous year, and have nearly doubled since 
  1993. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">&quot;Arresting adults 
  who smoke marijuana responsibly needlessly destroys the lives of tens of thousands 
  of otherwise law abiding citizens each year,&quot; Stroup said. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">In the past decade, more 
  than 6.5 million Americans have been arrested on marijuana charges, more than 
  the entire populations of Alaska, Delaware, the District of Columbia, Montana, 
  North Dakota, South Dakota, Vermont, and Wyoming combined. Nearly 90 percent 
  of these total arrests were for simple possession, not cultivation or sale. 
  During much of this time, arrests for cocaine and heroin have declined sharply, 
  indicating that increased enforcement of marijuana laws is being achieved at 
  the expense of enforcing laws against the possession and trafficking of more 
  dangerous drugs. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">&quot;Marijuana legalization 
  would remove this behemoth financial burden from the criminal justice system, 
  freeing up criminal justice resources to target other more serious crimes, and 
  allowing law enforcement to focus on the highest echelons of hard-drug trafficking 
  enterprises rather than on minor marijuana offenders who present no threat to 
  public safety,&quot; Stroup said. </font></p>
<p><font size="-1" face="verdana, geneva, tahoma, arial">Later this fall, the 
  NORML Foundation will be releasing a comprehensive report examining the nature, 
  extent and costs of marijuana arrests in the United States. The report will 
  feature state-by-state analysis of marijuana arrests by race, as well as an 
  economic and geographic analysis of US marijuana arrests. Further information 
  on NORML's forthcoming report is available by contacting the NORML Foundation 
  at: <a href="mailto:media@norml.org">media@norml.org</a>. </font></p>
