<p><font size="+1" face="Verdana, Geneva, Tahoma, Arial"><strong>WAR ON DRUGS</strong></font><br>
  <font size="-1" face="Verdana, Geneva, Tahoma, Arial"><strong>Pot Shot</strong></font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">BY KRISTEN LOMBARDI</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The anti-pot crusaders 
  are at it again. This time, they&#8217;re not taking aim at petty pot smokers 
  or medical-marijuana patients. Rather, they&#8217;ve set their sights on those 
  who simply criticize the status quo: drug-law reformers.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">A little-known provision 
  in a pending omnibus federal-spending bill would ban private advertising on 
  buses, subways, and trains that calls for the reform of our country&#8217;s 
  marijuana laws. The language would bar local and state transportation authorities 
  that receive federal grants from allowing private advertising &quot;that promotes 
  the legalization or medical use of any substance listed in schedule 1 ... of 
  the Controlled Substances Act&quot; &#8212; in other words, marijuana. If enacted, 
  it would effectively silence drug-law-reform groups, many of which have turned 
  to billboard and poster ads to push for medical marijuana and similar initiatives.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The provision materialized 
  last week after a Republican lawmaker from Oklahoma spotted an ad funded by 
  Change the Climate, a drug-law-reform group based in Greenfield, Massachusetts. 
  The ad depicted a sunny-blond, tanned man romancing a sunny-blond, tanned woman 
  at a tropical resort. Under the picture appeared the words, enjoy better sex! 
  legalize and tax marijuana.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The ad so offended Representative 
  Ernest Istook, who noticed it while riding the subway in Washington, DC, that 
  he subsequently inserted the anti-marijuana-ad provision into the omnibus spending 
  bill. Apparently, says Joe White, who heads Change the Climate, the ad &quot;sent 
  the right wing crazy off the deep end.&quot; Conservatives, he says, &quot;are 
  so interested in sex that it wasn&#8217;t surprising they jumped all over our 
  ad. But we&#8217;re wondering why they&#8217;re so afraid to have a debate about 
  marijuana laws in this country?&quot;</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">This, of course, is why 
  the ads exist in the first place. To date, Change the Climate has funded seven 
  ad campaigns on the DC Metro, which offers a reduced rate for nonprofit organizations. 
  It&#8217;s also bought billboards in Nevada and California. Currently, you can 
  see the group&#8217;s billboards dotting Bay State highways and touting the 
  &quot;$138 million to save local services&quot; that could be spared and funneled 
  toward social services if marijuana were legalized.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">The anti-marijuana-ad 
  provision raises obvious First Amendment concerns. Three years ago, the Massachusetts 
  Bay Transit Authority refused to run the same ad Istook found so offensive, 
  triggering a protracted lawsuit. Last August, Change the Climate lost; it&#8217;s 
  now working on an appeal. Despite the ruling, White believes the provision &quot;is 
  completely unconstitutional,&quot; since courts have generally ruled that public-transportation 
  authorities cannot discriminate against any political viewpoint.</font></p>
<p><font size="-1" face="Verdana, Geneva, Tahoma, Arial">Federal-spending bills 
  are notoriously difficult to amend; indeed, the only way to stop the provision 
  from becoming law is for Congress to vote against the entire omnibus bill &#8212; 
  a &quot;super large&quot; piece of legislation funding dozens of federal agencies. 
  But drug-law-reform advocates aren&#8217;t giving up yet. Though the US House 
  of Representatives passed the bill on December 8, 176 members voted against 
  it. And momentum against it is mounting in the Senate, which will vote on the 
  bill in January. So, drug-law-reform advocates see a slim opportunity to shoot 
  down the anti-marijuana-ad provision. &quot;People calling legislators always 
  makes an impression,&quot; says Fatema Gunja, of the Drug Policy Forum of Massachusetts.<br>
  </font></p>
