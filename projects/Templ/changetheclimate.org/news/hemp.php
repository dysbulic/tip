The Marijuana Policy Project has traditionally not involved itself in hemp-related 
issues, primarily because people were not being arrested and imprisoned for hemp 
law violations. Unfortunately, the DEA's new hemp regulations could change this.<br>
<br>
In sum, hemp products that can introduce trace amounts of THC -- even just a few 
molecules -- into the body are now illegal, even though these hemp products cannot 
produce a psychoactive effect of any kind. Only hemp products that aren't consumed 
and introduce literally zero molecules of THC into the body remain legal.<br>
<br>
The motivation behind the federal government's war on hemp food is similar to 
the federal government's war on marijuana-using patients: The federal government 
opposes any kind of evidence -- including clinical research -- that shows marijuana 
does not destroy lives and ruin society. Marijuana-using patients are evidence 
that marijuana can actually help save lives, so the government puts them in prison. 
Hemp food products show that a part of the marijuana plant can be nutritious without 
any negative side effects, so the government is now waging its war on this new 
front.<br>
<br>
