<p><font face="Verdana, Tahoma, Arial, Sans-serif"><b>Medical Marijuana in Court</b></font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">May 25, 2002</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Whether marijuana 
  can be used for medicinal purposes in California and several other Western states 
  is about to be decided by a federal appeals court in San Francisco. The court 
  is to rule on a challenge by doctors to a federal policy prohibiting them from 
  recommending medical marijuana. The federal campaign, which is designed to block 
  a California state referendum on the issue, is mean-spirited and unconstitutional. 
  The appeals court should not delay in calling an end to it.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">In 1996 California's 
  voters adopted Proposition 215, also known as the Medical Marijuana Initiative, 
  which holds that the state's criminal laws against marijuana do not apply to 
  seriously ill patients who use the drug on the advice of their physicians. Shortly 
  after it passed, the federal government announced that, as part of the war on 
  drugs, it would use its authority under the Controlled Substances Act to revoke 
  a doctor's license to prescribe drugs if he or she recommended marijuana to 
  a patient.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">A group of doctors 
  and patients sued, arguing that the federal policy intrudes on the doctor-patient 
  relationship and prevents them from honestly rendering and receiving medical 
  advice. The lower court that heard the case took a dim view of the federal government's 
  policy, and enjoined it from using its rules to take away the licensing power 
  of doctors who recommend medical marijuana to their patients.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">Medical marijuana 
  can be a legitimate treatment for cancer patients who are nauseated by chemotherapy, 
  AIDS patients who lose their appetites and other seriously ill people. In cases 
  where a patient is considering stopping treatment because of the agony, or cannot 
  keep food down, medical marijuana can be life-saving. The federal government's 
  attempt to block its use flies in the face of mainstream medical opinion. One 
  Harvard study of 2,000 oncologists found that 44 percent had recommended marijuana 
  to patients undergoing chemotherapy.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The federal policy 
  also clashes with the free-speech protections of the First Amendment. The federal 
  government wants to punish physicians merely for advising a patient about the 
  benefits of marijuana. That restriction infringes directly on the First Amendment 
  right of doctors to speak freely and, no less important, the right of patients 
  to receive the best possible medical information.</font></p>
<p><font face="Verdana, Tahoma, Arial, Sans-serif" size="-1">The court's ruling 
  will affect not only California but six other Western states in the same judicial 
  circuit that have medical marijuana laws. It is being closely watched by doctors 
  and patients in those states, and nationwide, since this ruling is likely to 
  be influential on other courts that take up the question. The California appeals 
  court judges have an opportunity to strike an important blow for free speech 
  and honesty in medicine by striking down the medical marijuana gag rule.</font><br>
</p>
