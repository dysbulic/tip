<p><b><font face="Verdana, Tahoma, Arial, Sans-serif" size="+1">The Federal Government's Attack on Prop. 215 and Medical Marijuana in California </font></b></p>
<p align="center"><img src="../images/cancer.jpg" width="468" height="60" border="1"></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">On November 5, 1996 the 
  citizens of California approved Proposition 215, which gives protection to seriously 
  ill patients and their doctors and allows them to legally use medical marijuana 
  to help ease their pain and suffering.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Since the passage of 
  Prop. 215 the United States Justice Department and Drug Enforcement Administration 
  (DEA) have showed total disregard for this law and trampled on the rights of 
  voters in California. People with cancer, AIDS, and other serious ailments who 
  use medical marijuana to help ease their pain and suffering are being aggressively 
  pursued and prosecuted by the federal government. Federal judges overseeing 
  many of these cases are forbidding these seriously ill patients from making 
  any mention of Proposition 215 or medical marijuana to the jury -- effectively 
  making them look like &quot;kingpins&quot;.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">One half of the federal 
  marijuana cases in San Francisco now involve medical marijuana, even though 
  patients covered under Prop. 215 account for just 1% of the state's marijuana 
  users.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It's time to change the 
  climate in California around medical marijuana. These seriously ill patients 
  need to concentrate on their health and not worry about being prosecuted in 
  federal court for taking a medicine recommended by their doctor. More importantly, 
  the federal government needs to show respect for the will of California voters 
  and bring an end to these senseless raids and arrests.</font></p>
