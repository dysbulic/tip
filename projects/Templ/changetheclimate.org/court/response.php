<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">A. Statement of legal 
  and factual elements of plaintiff's claims.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The plaintiff has three 
  legal claims: 1) The defendants, acting under color of state law, violated plaintiff's 
  rights under the First and 14th Amendments and 42 U.S.C. section 1983 by refusing 
  to sell advertising space to Change the Climate for the display of three advertisements 
  because of the content and viewpoints expressed in the ads and/or because of 
  the goals and mission of Change the Climate. The advertising space is a public 
  forum for expression in which the defendants impermissibly rejected the ads 
  based on their content. Even if the space is not a public forum, the defendants 
  impermissibly rejected the ads based on their viewpoint or based on the mission 
  of Change the Climate. 2) The defendants, acting under color of state law, violated 
  plaintiff's rights under the First and Fourteenth Amendments and 42 U.S.C. &sect;1983 
  by rejecting plaintiff's ads by using a policy (policies) on the use of MBTA 
  advertising space which is (are) vague, subjective, and lacking in narrowly 
  drawn and specific standards. 3) The defendants, acting under color of state 
  law, violated plaintiff's rights to equal protection of law by refusing to display 
  plaintiff's ads relating to drug policy and the use of illegal drugs while displaying 
  ads from other organizations relating to these subjects.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The factual elements 
  of these claims are as follows:</font></p>
<ol>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Claim of Content 
    and Viewpoint Discrimination in Violation of the First Amendment and 42 USC 
    &sect; 1983<br>
    <br>
    Defendant MBTA is a political subdivision of the Commonwealth of Massachusetts. 
    At all times relevant to this case, Robert Prince was the general manager 
    of the MBTA and Lucy Shorter was the director of marketing communications 
    of the MBTA. The MBTA sells advertising space for the display of ads on MBTA 
    facilities and vehicles. Park Transit Displays (PTD) was the agent for the 
    MBTA for the purpose of selling such advertising space. Elissa Albertelli 
    was an employee of PTD and her duties involved processing requests by those 
    wishing to display their ads on MBTA advertising space. The MBTA historically 
    and continuing to the present has sold its advertising space for the display 
    of ads on a wide variety of subjects, including the use of illegal drugs by 
    minors.<br>
    In the Spring of 1999, plaintiff Change the Climate (by Joe White, its executive 
    director) submitted three ads to Elissa Albertelli of PTD for display on MBTA 
    advertising space. The text of these ads is set forth in the Complaint and 
    is incorporated herein. The ads concern public policy on marijuana. Shorter 
    received copies of these ads from either Albertelli or White. On January 24, 
    2000, Albertelli informed White as follows: &quot;Regretfully, I've attached 
    the response from the Transit Authority regarding &quot;Change the Climate&quot; 
    campaign. Their reasons for not accepting this campaign are clearly stated 
    in the info. provided.&quot; The information provided included a written statement 
    from Lucy Shorter which stated: &quot;See attached. The Change the Climate 
    promotes the use of marijuana in a suttle [sic] way and also is really a reform 
    marijuana [sic] in a effort to legalize? The T's appearance &amp; character, 
    Policy &amp; Drug - Alcohol policy is in conflict with their mission.&quot; 
    Shorter attached the MBTA policies referenced in the memo. These statements 
    constituted a rejection of plaintiff's ads based on their viewpoint and based 
    on the perception by the MBTA of Change the Climate's goals. <br>
    <br>
    The MBTA has accepted ads for display on its subway cars for many years on 
    a wide variety of topics, including issues of ongoing public debate and disagreement. 
    These include ads about AIDS, abortion, gay rights, animal rights, use of 
    illegal drugs by minors, and alcohol. As a result, the advertising space created 
    by the MBTA is a public forum for expression. The MBTA rejected plaintiff's 
    ads because of their content and refused to display them in this public forum.<br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Facts relating to 
    claim of violation of First Amendment by defendants' use of a vague, standardless 
    policy or policies for display of ads in its advertising space.<br>
    <br>
    The defendants themselves do not agree on or know which policies were applicable 
    to the decision to refuse to allow plaintiff to purchase advertising space 
    to display the ads it submitted. They refer variously to the MBTA's 1992 bid 
    specifications, 1993 Commercial and Public Service Advertising Policy, the 
    1995 Mabardy letter, and the 1999 bid specifications. All of these purported 
    policies are vague and lack clear standards to guide decisionmakers about 
    what ads are permissible on MBTA advertising space. The MBTA currently lacks 
    a clear policy with content-neutral standards to guide decisionmakers about 
    what advertising is permitted on MBTA space.<br>
    <br>
    </font></li>
  <li><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> Facts relating to 
    claim of denial of right to equal protection.<br>
    <br>
    The defendants have permitted the display of ads on its advertising space 
    which concern the use of illegal drugs (including marijuana) by minors. These 
    ads stress the dangers of the use of drugs by children and encourage parents 
    to talk to their children about drugs. The defendants accepted these ads because 
    they view the message of these ads as acceptable. The defendants rejected 
    plaintiff's ads which relate to the use of drugs by minors because they viewed 
    the message of Change the Climate's ads and goals and mission of the organization 
    as unacceptable.</font></li>
</ol>
