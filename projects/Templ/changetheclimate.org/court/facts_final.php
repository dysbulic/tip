<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>B. The Promotion of 
  Discussion and Debate Our Nation's Failed Drug Policies Is An Important Matter 
  of Public Concern. </b><br>
  <br>
  To survey what has been written and spoken on the vast subject of our nation's 
  war on drugs over the years is a task well beyond the scope of this case. What 
  is material is that increasingly, respected public figures are urgently calling 
  for citizens to do exactly what Change the Climate, through its media campaign, 
  is trying to do: get people to think about and debate rational alternatives 
  to our failed war on drugs.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">An excerpt from Chief 
  Judge Torruella's thoughtful article, The"War on Drugs": One Judge's 
  Attempt At A Rational Discussion, bears on this point directly:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Drug enforcement policy 
  involves the consideration of issues as diverse as international and national 
  politics, law enforcement, sociology, economics, organizational dynamics, and 
  penology, to mention only the most salient. In addition, there are complex scientific, 
  pharmacological and health questions, and, of course, moral quandaries, raised 
  by the various policy choices and alternatives. Nevertheless, I do not think 
  that the complexity of the subject should deter us from exchanging views, for 
  we must start at some point, and now is as good a time as any. In fact, I believe 
  that more vigorous and frank public discussion of these issues is what is presently 
  needed.<sup><a href="#2">2</a></sup> </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Veteran Judge, former 
  federal prosecutor and Navy JAG criminal defense attorney, James P. Gray, recently 
  authored the new book, Why Our Drug Laws Have Failed And What We Can Do About 
  It, in which he echoes Judge Torruella's sentiment:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The real problem in this 
  area actually is not the drugs themselves. The real problem is that our citizens 
  and our leaders simply will not look at the evidence, even though it is all 
  around us. Our present policy is exacerbating the problems and will not stand 
  up to scrutiny. What we really need to do is to open the subject to rigorous 
  public debate. This is our best and perhaps our only hope for moving forward 
  to a better strategy, and to adopting programs that will actually work. The 
  advocates of the status quo stand firmly against any full or open discussion 
  of federal drug policy, but what we really need to do is explore our options 
  realistically, and tell each other the truth.<sup><a href="#3">3</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Numerous other lawyers 
  and judges have expressed those same concerns in "An Open Letter from Judges 
  and Attorneys," sponsored by the Voluntary Committee of Lawyers, Inc.:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">. . . . [W]e join with 
  our colleagues in calling upon our profession, elected officials, the media 
  and the public to initiate a truly open and honest evaluation of the efficacy 
  and consequences of our drug control laws. Only a public debate guided by mutual 
  respect can yield better drug laws in which fear, prejudice and punitive prohibitions 
  yield to common sense, science, public health and human rights. As America we 
  must never "surrender" to drugs, neither must she surrender to inertia 
  or fear that shuts off debate, suppresses critical analysis, dismisses alternatives 
  to current policies, and vilifies those who express dissenting views.<sup><a href="#4">4</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The citations could go 
  on and on and on, from speakers in the legal community to speakers in the scientific 
  and medical community throughout society. The salient point, for the purposes 
  of this litigation, is that the promotion of public discussion and debate on 
  the subject of our nation's drug polices, and the efficacy of those policies, 
  is a matter of important public concern unto itself.<sup><a href="#5">5</a></sup></font></p>
