<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>J. Change The Climate's 
  Other Advertising Campaigns.</b><br>
  Despite the serious setback Change the Climate experienced in launching its 
  first advertising campaign in Boston that prompted the instant law suit, CTC 
  initiated a second campaign in Washington, D.C. Initially the Washington Metro 
  Transit Authority ("the Metro") rejected Change the Climate's proposed 
  advertisements claiming the ads violated a Metro policy that purported to restrict 
  public advertisements to those "not designed to influence legislation or 
  public opinion on a controversial subject." After the American Civil Liberties 
  Union threatened suit on behalf of CTC, however, the Metro relented, and agreed 
  to post 560 ads on marijuana issues. CTC ultimately paid a discounted advertising 
  rate to the Metro in exchange for illuminated signs on 10 subway platforms, 
  large exterior signs for 50 buses and smaller signs for the interiors of an 
  additional 500 buses. The month-long campaign consisted of three ads<sup><a href="#29">29</a></sup> 
  - one of which was the very same ad that is the subject of this lawsuit - that 
  were ultimately displayed by the Metro.<sup><a href="#30">30</a></sup> (White Dep., p. 49; White Aff., 14.) 
  </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In light of the positive 
  responses CTC received on the D.C. campaign, it launched a second ad campaign 
  that ran on 36 Metro bus shelters throughout the month of May 2001.<sup><a href="#31">31</a></sup> 
  (White Aff., 14) After two successful campaigns in the nation's capital, CTC 
  is currently planning transit advertising campaigns in New York City, Chicago, 
  and San Francisco. CTC has also received requests for assistance with advertising 
  campaigns in Indianapolis, Portland, Oregon, and other locations. With each 
  successful campaign, CTC hopes to gain the momentum necessary to accomplish 
  its goal to generate millions of advertising impressions through advertising 
  in as many cities throughout the nation as possible. (White Aff., 15.)</font></p>
