  on the facilities of the authority." Mass.Gen.L. ch. 161A, §§ 
  2 and 3 (1993). During all times relevant to this action, the MBTA regularly 
  authorized its exclusive advertising agent, Park Transit Display, Inc. ("PTD"), 
