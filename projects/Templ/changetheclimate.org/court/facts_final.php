  on MBTA buses and trains (Deposition of Lucy V. Shorter ("Shorter Dep."), 
  pp. 20, 80, attached as Exhibit 3; White Dep., p. 86.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The defendant Robert 
