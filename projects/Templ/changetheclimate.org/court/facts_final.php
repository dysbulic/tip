<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">It was not until after 
  Change the Climate filed suit in May 2000 that the MBTA indicated that policies 
  other than the contract specifications that Shorter attached to the January 
  2000 fax may have applied to Shorter's decision to reject Change the Climate's 
  proposed advertisements. These indications, whether by MBTA counsel in answers 
  to interrogatories or by the individual witnesses in deposition, have been tentative 
