  p. 17.) At his deposition, however, Prince testified that he could not recall 
  whether or not he applied this document to any of his decisions to reject or 
  accept proposed advertisements one way or the other during the 1999 -2000 time 
  period. (Prince Dep., pp. 7-9.) Indeed, he testified that he could not recall 
  whether he had ever even seen the document prior to the date of his deposition 
