  S.)<sup><a href="#20">20</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">For the first time, in 
  their answers to interrogatories and deposition testimony, the defendants claim 
  that a letter dated April 16, 1995, from Robert Mabardy, then Interim General 
  Manger of the MBTA to Michael Geden, then Vice President/General Manager, set 
  up new guidelines regarding advertising covered by the contract between the 
  MBTA and PTD in effect at the time CTC's ads were rejected. (Defs. Ans. to Ints. 
  No. 9, at Ex. S; Prince Dep., pp. 5-6; Shorter Dep., p. 19.) A copy of the April 
  16, 1995 "Mabardy letter" is attached as Exhibit W. These "MBTA 
  Advertising Guidelines" in that letter provide:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The MBTA will refuse 
  any advertisement that is indecent as to child viewers, or is of a nature to 
  frighten children, either emotionally or physically. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The following terms, 
  as used in these guidelines shall have the following meanings:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The terms "child" 
  or "children" shall mean any child under the age of seventeen years.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The terms "indecent" 
  shall mean the exposure of children to language or pictures that describe or 
  depict sexual or excretory activities or organs in a patently offensive manner 
  as measured by contemporary community standards for the subway advertising medium.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The terms "frightening" 
  or "of a nature to frighten children, either emotionally or physically" 
  shall mean language or pictures that describe or depict violent or brutal activities, 
  or the end result of violent or brutal activities, whether such violence or 
  brutality was intended or not, in a manner which causes a child physical or 
  emotional distress or causes a child to fear for his personal safety or for 
  the safety or other persons or animals. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">At the end of the day, 
  this is the only document that Prince could be certain was in effect at the 
  time Change the Climate's proposed ads were submitted. (Prince Dep., pp. 6, 
  9-12.) It is undisputed that the plaintiff's proposed advertisements at issue 
  in this case are neither "indecent as to child viewers," nor "of 
  a nature to frighten children, either emotionally or physically" according 
  to the very language of the policy.<sup><a href="#21">21</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Thus, even if it were 
  in effect at the time, the Mabardy letter has no application to CTC's proposed 
  advertisements.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Also in answers to interrogatories, 
  the defendants suggest for the first time that the "Commercial and Public 
  Service Advertising Policy, adopted in or about February 1993," might have 
