<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>D. MBTA's Public Record 
  of Unconstitutional Viewpoint Discrimination.</b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In three reported cases 
  brought in this district concerning its advertising practices, the MBTA was 
  found to have impermissibly discriminated against non-profit advertisers on 
  the basis of the viewpoint of the message in the advertisements. See AIDS Action 
  Committee of Massachusetts v. MBTA, 42 F.3d 1 (1st Cir. 1994), affirming and 
  modifying 849 F. Supp. 79 (D.Mass. 1993) (attached to Plaintiff's Memorandum 
  of Law as Exhibit 1a); Citizens to End Animal Suffering and Exploitation v. 
  MBTA, C.A. No. 92-11118-MA (D.Mass. June 5, 1992) (attached as Exhibit 1b); 
  Preterm, Inc. v. MBTA, C.A. No. 74-159-MA,1974 U.S. Dist. LEXIS 8555 (D.Mass. 
  May 13, 1974) (attached as Exhibit 1c). In each of these cases, the MBTA was 
  ordered to display the messages and refrain from so discriminating in the future. 
  See AIDS Action, 42 F.3d at 38-40 (enjoining viewpoint discrimination and ordering 
  MBTA to post safe sex practices advertisements); Citizens to End Animal Suffering, 
  C.A. No. 92-11118-MA (ordering MBTA to post animal rights advertising placard); 
  Preterm, C.A. No. 74-159-M,1974 U.S. Dist. LEXIS 8555, at * 6 (granting preliminary 
  injunction and ordering display of ads concerning abortion services available 
  at clinic). </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Shorter was the Director 
  of Marketing and Communications of the MBTA in 1994, when the First Circuit 
  affirmed the district court injunction in AIDS Action. Despite the outcome in 
  that case, Shorter testified that she never received any training whatsoever 
  to prevent future violations of advertisers' First Amendment rights by MBTA 
  officials. (Shorter Dep., pp. 16-17.) Despite this record of unconstitutional 
  conduct, the MBTA did not distribute a single memorandum to educate employees 
  in the aftermath of any of these decisions.<sup><a href="#7">7</a></sup> Despite this record of unconstitutional 
  conduct, Defendant Prince, the General Manager and CEO of the MBTA, testified 
  that if, in his judgment, a political opinion is "harmful to juveniles," 
  he could decline to run an advertisement on that basis. (Prince Dep., p. 41.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>E. MBTA's Advertising 
  Approval Process. </b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">At all times relevant 
  to this action, the Marketing Department was the exclusive department that dealt 
  with proposed advertisements for display on MBTA facilities and vehicles. (Shorter 
  Dep., p. 5) Defendant Shorter was the point person from the Department with 
  whom PTD, the MBTA's exclusive advertising agent, dealt in the advertising process. 
  (Albertelli Dep., p. 7.) When a business wanted to buy advertising space on 
  the MBTA, it did not have the option of dealing with the MBTA directly. It had 
  to go through PTD. (Albertelli Dep., p. 8.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In her capacity as Director 
  of Marketing Communications, Shorter reviewed ads that came in from PTD and 
  forwarded ads for final approval by the General Manager, who was at all times 
  relevant, defendant Robert Prince. (Shorter Dep., p. 6, 13; Prince Dep., p. 
  5; Albertelli Dep., p. 25.) Shorter created a sign-off sheet for advertisements 
  that she would review, to be sent for approval by Prince. (Shorter Dep., pp. 
  9-11.) Albertelli was never privy to this process or sheet. (Albertelli Dep., 
  p. 24.) The procedure would be used when PTD was unsure whether a submitted 
  ad fell within the MBTA guidelines. (Id. at 13-14.) Once she submitted an ad 
  to Shorter, Albertelli would await an answer. (Albertelli Dep., p. 28.) Shorter 
  testified that ninety-nine percent of the time, however, PTD would post the 
  ad without seeking approval. (Shorter Dep., at 14.) Albertelli agreed. It was 
  left to PTD's discretion whether the Marketing Department would need to see 
  the ads in the first instance. (Albertelli Dep., pp. 12-14.) Albertelli's guiding 
  criterion in determining whether to bring an ad to the MBTA's attention was 
  whether the ad was "a little bit provocative." (Id. at p. 15.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Albertelli also testified 
  that when there was an ad copy in question, she and Shorter would discuss it, 
  bring in the client at some point, and talk to him or her about the campaign 
  and any objections raised by Shorter. (Albertelli Dep., p. 48.) Neither Albertelli 
  nor Shorter ever invited White in to discuss objections to the plaintiff's ads. 
  (Albertelli Dep., p. 33.)<sup><a href="#8">8</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>F. Change The Climate's 
  Submission of Ads for Approval to the MBTA. </b></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In September 1998, White 
  contacted Albertelli at PTD to request information about the process for placement 
  of advertisements on the transit authority's public advertising space. (White 
  Dep., pp. 65, 108) Subsequently, Albertelli mailed a standard media kit to White, 
  explaining advertising rates, demographic information, and population statistics 
  on advertising coverage. (Albertelli Dep., p. 16; White Dep., p. 65) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">In the spring of 1999, 
  through a series of phone calls, White and Albertelli arranged a meeting in 
  Cambridge to discuss a paid public service campaign for CTC. (Albertelli Dep., 
  p. 17; White Dep., p. 68.) In telephone conversations and a meeting that took 
  place at the Charles River Hotel on or about May 10, 1999, White explained to 
  Albertelli that CTC wanted to get a message out in general around the subject 
  of marijuana. (Albertelli Dep., p. 20; White Dep., pp., 85-86.) Albertelli recalls 
  that White told her that CTC was looking to educate the public and raise public 
  awareness on various issues surrounding marijuana and that she received for 
  her review CTC materials, including some draft ads, early on in her dealings 
  with White (Albertelli Dep., pp. 17, 40; White Dep., p. 85).<sup><a href="#9">9</a></sup></font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> During the course of 
  their conversations, Albertelli explained to White that the process between 
  PTD and the MBTA for getting advertisements approved was that samples of the 
  "creative" (i.e. proposed copy of the ads) would first be sent to 
  Shorter, and upon receiving a response from Shorter, a formal application would 
  be made on his behalf. (White Dep., pp. 85, p. 112-13) White recorded the substance 
  of one of these conversations with Albertelli in his day planner. One entry 
  reads "she needs to run creative by MBTA." Copies of White's day planner 
  entries of May 1999 are attached as Exhibit G. (See also White. Dep., pp. 112-13) 
  Also during the course of their conversations, White recalls Albertelli telling 
  him words to the effect that she didn't think that Shorter would have a problem 
  with the mild content of the ads that CTC was proposing to display.<sup><a href="#10">10</a></sup> (White 
  Dep., p. 86.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">On May 11, 1999, Albertelli 
  sent White proposals for media buys of varying monetary amounts from PTD, as 
  well as additional information on advertising demographics and bus geographic 
  coverage. (Albertelli Dep., p. 17.) Upon receiving the proposals, White sent 
  Albertelli several proposed ads that he wished the MBTA to consider. A copy 
  of a handwritten undated note to Albertelli from White, attaching several draft 
  ads is attached as Exhibit H. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">On May 21, 1999, at Albertelli's 
  direction, White sent Shorter additional information about Change the Climate, 
  so that she could learn more about the organization. Among other things, the 
  information describes CTC's media campaign and the emphasis CTC places on its 
  position that marijuana is not for kids. A copy of the May 21, 1999 letter to 
  Shorter from White is attached as Exhibit I. Shorter received the packet and 
  reviewed that information, as well as the proposed ads that White had sent to 
  Albertelli. (Shorter Dep., p. 54.) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">From May through December 
  1999, White called Albertelli on several occasions, inquiring about the MBTA's 
  response to the advertising copy and other materials that he had mailed to both 
  Albertelli and the MBTA, and how to complete the process and get the ads up 
  and running. (White Dep., pp. 88, 90-91, 120-121.) </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">After more weeks of waiting 
  and hearing nothing back, on January 3, 2000, White contacted Albertelli again 
  and expressed his frustration with the MBTA's delay in responding to the creative 
  sent months prior. (White Dep., p. 122.) Albertelli told White at that point 
  that she would submit a formal application to get a response. (White Dep., p. 
  123.) The next day, White followed up that phone call with a letter to Albertelli, 
