<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>A. The Parties.</b><br>
  Change the Climate ("CTC") is a not for profit organization which, 
  quite literally, seeks to "change the climate" around the discussion 
  of marijuana issues in the United States. Joe White, Executive Director of CTC, 
  founded the organization in 1999, as a concerned parent and citizen, for the 
  purpose of raising public awareness about marijuana issues, including the inordinate 
  amount of tax dollars spent on marijuana law enforcement and the potential harm 
  to our children resulting from arrest and incarceration. (Affidavit of Joseph 
  White ("White Aff."), 2, attached as Exhibit 1; Deposition of Joe 
  White ("White Dep."), pp. 26-27, 148-149, attached as Exhibit 2.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">CTC's mission is to stimulate 
  debate about marijuana issues. Among its many concerns, CTC seeks to promote 
  discussion about the negative consequences of the marijuana laws on our children, 
  the extraordinary resources our government has devoted to marijuana law enforcement, 
  the differences between marijuana and other drugs such as heroin and cocaine, 
  and the proven beneficial medical and industrial uses of marijuana.<sup><a href="#1">1</a></sup> (White 
  Aff., 3; White Dep., pp. 37-39, 148-49, 157-58, 168-70.) By engaging the public 
  in discussion on marijuana issues, and attempting to combat misinformation with 
  accurate information, CTC hopes to help reduce the level of hysteria that has 
  so long dominated the public domain about marijuana. Toward that end, as Executive 
  Director, White created and operates an interactive web site, www.changetheclimate.org. 
  (White Aff., 3-4.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">One way CTC seeks to 
  raise public awareness is through its media campaign. CTC embarked on a national 
  advertising campaign in an effort to stimulate public debate through provocative 
  advertisements on public transit systems throughout the nation's cities. (White 
  Dep., pp. 23-26; White Aff. 5.) The media campaign intends to raise questions 
  in people's minds about the broad issues of marijuana policy. (White Dep., pp. 
  28-30, 37-39, 67.) CTC chose the medium of public transit systems because it 
  reaches the most number of people and is a proven vehicle to attract attention, 
  to stimulate debate, and to raise issues in the public domain. (White Dep., 
  pp. 23, 41.) Three advertisements used in CTC's campaign - one of which has 
  already been successfully displayed on transit buses in our nation's capital 
  - are the subject of this lawsuit. Copies of these proposed advertisements are 
  attached as Exhibit B. (See also White Aff., 12.)</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">While CTC hopes to raise 
  ideas and questions in peoples' minds about the efficacy of our current marijuana 
  laws and policies through its provocative advertisements, in no way does the 
  organization advocate the use of marijuana or any violation of existing laws. 
  (White Aff., 5.) Woven throughout CTC's promotional materials, the interactive 
  web site, and its media campaign are explicit messages that the truth need be 
  spoken about marijuana and that marijuana is not for kids. (White Aff., 6; White 
  Dep., pp. 40, 151-156, 161, 168.)</font></p>
