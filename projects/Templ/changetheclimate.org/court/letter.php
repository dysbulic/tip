<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>April 29, 2002</b><br>
  READER'S VIEW: Opposed to marijuana ads on the MBTA <br>
  By LEA PALLERIA COX, Hanover </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">On April 18 Steven Epstein 
  writes in support of marijuana ads on the MBTA. This reflects his long-standing 
  marijuana activism as co-founder of the Massachusetts Cannabis Reform Coalition, 
  editor of &#145;&#145;Mass Grass,&#146;&#146; and organizer of the annual Boston 
  Common &#145;&#145;pot rally.&#146;&#146;</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Given Epstein&#146;s 
  background, his support of the MBTA marijuana ads - which downplay the dangers 
  of marijuana - should surprise no one.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">One of the proposed ads, 
  stating that police are &#145;&#145;too good to waste on arresting people for 
  marijuana when real criminals are on the loose&#146;&#146; implies that users 
  go to jail - what a joke that is! When there are convictions, the charges are 
  usually for drug dealing, but through plea-bargaining, the lesser charge of 
  possession is recorded.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Another proposed ad reads 
  in part: &#145;&#145;jail being more dangerous than pot,&#146;&#146; overlooks 
  the evidence that marijuana is a health hazard and changes brain chemistry, 
  etc. - are these effects inconsequential?</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The third ad shows a 
  picture of a teenager and the text is: &#145;&#145;Smoking pot is not cool, 
  but we&#146;re not stupid, ya know, marijuana is not cocaine or heroin.&#146;&#146; 
  Again, this downplays the harms and is truly directed toward the younger generation.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The court should uphold 
  the ban and deny the use of the MBTA vehicles to advertise the harmful, addictive, 
  carcinogenic, illegal drug - marijuana.</font></p>
<p>&nbsp;</p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>April 18, 2002</b><br>
  READER'S VIEW: Response to Lapey&#146;s letter on marijuana ads<br>
  By STEVEN S. EPSTEIN, Georgetown</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Janet Lapey&#146;s letter 
  (&#145;&#145;Supports MBTA ban on pro-marijuana ads,&#146;&#146; March 25) makes 
  one thing perfectly clear, she supports the right of free speech only if she 
  approves of the contents of the communication. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">I for one do not approve 
  of the content of her letter, which misrepresents both the scientific evidence 
  of marijuana&#146;s dangers and the message of the three advertisements at issue, 
  but would not tell you not to print it. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The ads the MBTA does 
  not want on its system pose three points for consideration: a photograph depicting 
  two police officers with the text: &#145;&#145;Police are too important... too 
  valuable... too good... to waste on arresting people for marijuana when real 
  criminals are on the loose.&#146;&#146; A picture of a woman at work with the 
  text: &#145;&#145;I&#146;ve got three great kids. I love them more than anything. 
  I don&#146;t want them to smoke pot. But I know jail is a lot more dangerous 
  than smoking pot&#146;&#146;; and, a picture of a teenager with the text, &#145;&#145;Smoking 
  pot is not cool, but we&#146;re not stupid, ya know. Marijuana is not cocaine 
  or heroin.&#146;&#146; I leave it to your readers to decide if such statements 
  promote marijuana or promote rational discussion about a public policy that 
  many, perhaps a majority, believe unwise.</font></p>
<p>&nbsp;</p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"><b>March 25, 2002</b><br>
  READER'S VIEW: Supports MBTA ban on pro-marijuana ads<br>
  By JANET D. LAPEY, M.D., Quincy</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The courts should support 
  the MBTA&#146;s policy banning pro-marijuana advertisements. The marijuana legalization 
  lobby, like the tobacco lobby, targets our youth; just witness their annual 
  free rock concert on Boston common. Public transportation, supported by our 
  taxes, should not be used to promote a harmful illegal substance which leads 
  to costly societal damage.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Each year more and more 
  scientific studies demonstrate marijuana&#146;s dangers. The drug can be addictive, 
  and withdrawal symptoms are well-documented. Like tobacco smoke, marijuana smoke 
  causes cancer, lung disease and heart attacks. Although advertised as an &#145;&#145;asthma 
  medicine,&#146;&#146; it actually causes asthma. Even passive smoking is harmful.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Furthermore, marijuana 
  undermines sound thinking and judgment, adversely affecting school performance 
  and life decisions. It is a major cause of motor vehicle crashes. Since tolerance 
  develops to marijuana&#146;s effects, users often &#145;&#145;graduate&#146;&#146; 
  to drugs such as cocaine and heroin. If parents can keep their children away 
  from marijuana, it is highly unlikely that they will turn into the heroin addicts, 
  which are the bane of society.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">You can bet that the 
  marijuana legalization advertisements would not tell the truth about these harmful 
  effects. Their purpose is to convince the public that marijuana is &#145;&#145;no 
  big deal.&#146;&#146;</font></p>
