<center>
  <font face="Verdana, Geneva, Tahoma, Arial"><b>PLAINTIFF CHANGE THE CLIMATE, 
  INC.'S RESPONSES TO THE QUESTIONS POSED BY THE COURT AT SUMMARY JUDGMENT HEARING 
  </b></font> 
</center>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1"> The Court posed three 
  questions to the parties at the July 25, 2001 hearing. The plaintiff believes 
  these questions1 reflect a misunderstanding of the factual record and the legal 
  issues in this case, and respectfully responds to the questions as follows:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">2(a)(1) Has plaintiff 
  at any time submitted to MBTA or to the court a proposal for the content of 
  a message to be displayed in the space reserved for advertising on MBTA vehicles 
  used for public transportation that is specific enough to enable the court (or 
  a jury if the court determines that jury trial is appropriate) to decide the 
  subquestions identified in some or all of the subparts to this question?</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">YES. The Court has the 
  three proposed advertisements at issue in this case. They have been submitted 
  and resubmitted to the Court by both parties throughout this litigation: (1) 
  attached as exhibit A to the plaintiff's complaint; (2) attached as exhibit 
  B to the plaintiff's statement of uncontested facts; and (3) attached as exhibits 
  B, C, and D to the witness affidavit of Marvin E. Goldberg, Ph.D., filed in 
  support of the defendants' motion for summary judgment. The content of each 
  of these advertisements speaks for itself and is specific enough to enable the 
  Court to decide the subquestions identified in the subparts to this question. 
  In addition, there is no genuine dispute as to the fact that these advertisements 
  were submitted to MBTA official Lucy Shorter who disapproved of their content.<br>
  2(a)(2) Is the proposed content such that it could not be reasonably interpreted 
  by one or more identifiable groups of readers using an MBTA vehicle for public 
  transportation as a statement of position on political issues not only by plaintiff, 
  Change the Climate, Inc., but also as a statement by MBTA of its position regarding 
  the extent in fact of use of marijuana by children and acquiescence in, or support 
  for, allowing that use to continue without enforcement by public officials of 
  laws prohibiting that use?</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">YES. The proposed content 
  could not be reasonably interpreted as a statement by the MBTA of its position 
  regarding the use of marijuana by children and acquiescence or support for allowing 
  that use to continue without enforcement by public officials of the laws prohibiting 
  that use. Such an interpretation would only follow from the unreasonable assumption 
  that the MBTA espouses the views set forth in all of the advertisements it posts 
  for display. The MBTA's advertising space is open to advertisements on a broad 
  array of topics, both commercial and noncommercial, including advertisements 
  for political and public service messages, as well as religious messages. The 
  MBTA accepts advertising from parties with opposing views on some issues. If 
  acceptance of the advertisements on the MBTA could reasonably be construed as 
  endorsements, that would mean that the MBTA endorses alcohol, condoms, MTV, 
  R-rated movies, and Christianity. The MBTA has never taken the position that 
  it has to agree with the message of an advertisement in order to post it for 
  display. Were it to take such a position, it would plainly violate the First 
  Amendment's Establishment Clause, and the ban on viewpoint discrimination at 
  issue in this case - a legal principle this Court ignored entirely at hearing. 
  The First Amendment strictly forbids the MBTA from rejecting an advertisement 
  because it disagrees with the viewpoint expressed in the advertisement. Nothing 
  is more basic to First Amendment jurisprudence than that principle. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The plaintiff concedes, 
  however, that some portion of the MBTA's ridership, ignorant of the First Amendment, 
  will unreasonably interpret the advertisements to mean that the MBTA condones 
  or espouses the views expressed by the advertisements.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">2(a)(3) Is it reasonable 
  for the court to decline to grant plaintiff a remedy that in its nature is a 
  mandatory injunction to the MBTA to publish a message that it considers to be 
  a misrepresentation of its position on political issues of public concern?</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">NO. It is not reasonable 
  for the court to decline to grant a remedy which orders the MBTA to publish 
  a message that the MBTA considers to be a misrepresentation of its position 
  on a political issue of public concern. The plaintiff's advertisements, as with 
  all other advertising on the MBTA, espouses the advertiser's position, not that 
  of the MBTA. The remedy proposed by the plaintiff for viewpoint discrimination 
  - mandatory placement of the advertisements - is the only appropriate remedy. 
  This has been recognized by numerous federal courts, including the First Circuit 
  and district courts of Massachusetts. See AIDS Action Comm. v. MBTA, 42 F.3d 
  1, 3 (1st Cir. 1994) (affirmed injunction requiring transit authority to post 
  plaintiff's controversial advertising); New York Magazine v. Metropolitan Transp. 
  Auth., 136 F.3d 123, 125 (2d Cir.1998) (same); United Food &amp; Commer. Workers 
  Union, Local 1099 v. Southwest Ohio Reg. Trans. Auth., 163 F.3d 341, 346 (6th 
  Cir. 1998) (same). See also Citizens to End Animal Suffering and Exploitation 
  v. MBTA, C.A. No. 92-11118-MA (D. Mass. June 5, 1992) (granting preliminary 
  and permanent injunction, ordering MBTA to post animal rights advertising placard); 
  Preterm, Inc. v. MBTA, No. 74-159-M, 1974 U.S. Dist. LEXIS 8555, at *7 (D. Mass. 
  May 13, 1974) (granting plaintiff preliminary injunction requiring transit authority 
  to post advertisements); National Abortion Fed'n v. Metropolitan Atlanta Rapid 
  Trans. Auth., 112 F. Supp. 2d 1320, 1329 (N.D. Ga. 2000) (granting plaintiff 
  permanent injunction requiring transit authority to post advertisements). No 
  court has ruled to the contrary.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">This Court suggested 
  that its remedial powers have suddenly and dramatically been limited by the 
  Supreme Court's recent decisions in Lorillard Tobacco Co. v. Reilly, 121 S. 
  Ct. 2404 (2001) and Good News Club v. Milford Cent. Sch., 121 S. Ct. 2093 (2001). 
  After carefully reviewing those opinions, the plaintiff respectfully submits 
  that, although these two decisions bear on First Amendment issues in this case, 
  they support the plaintiff's First Amendment claims and do not suggest any impropriety 
  of affirmative equitable relief at all. There is simply no discussion in any 
  of the majority, concurring, or dissenting opinions that purports to limit a 
  federal court's ability to order appropriate relief for a violation of the First 
  Amendment. In Lorillard, the Court did not so much as pause to analyze the propriety 
  of the remedy it ordered, which was to enjoin Massachusetts from enforcing its 
  tobacco advertising ban. In Good News Club, the Court held that a school violated 
  students' First Amendment rights by excluding a religious group from meeting 
  space available to other groups. Notably, the Court did not state that the school 
  could not be ordered to allow the group access to the meeting space because 
  it might be perceived as endorsing the objectives of the group. Instead, the 
  Court explicitly held that allowing the group to use space would not create 
  an impression of endorsement that would offend the Establishment Clause:</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Milford's implication 
  that granting access to the Club would do damage to the neutrality principle 
  defies logic. For the &quot;guarantee of neutrality is respected, not offended, 
  when the government, following neutral criteria and evenhanded policies, extends 
  benefits to recipients whose ideologies and viewpoints, including religious 
  ones, are broad and diverse.&quot; Rosenberger, supra, at 839. The Good News 
  Club seeks nothing more than to be treated neutrally and given access to speak 
  about the same topics as are other groups. Because allowing the Club to speak 
  on school grounds would ensure neutrality, not threaten it, Milford faces an 
  uphill battle in arguing that the Establishment Clause compels it to exclude 
  the Good News Club.</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Good News Club, 121 S. 
  Ct. at 2104. Thus, to the extent that Good News Club has any relevance to the 
  issue of a remedy, it would suggest that this Court is actually compelled to 
  provide the remedy sought by the plaintiffs because it is unreasonable to infer 
  endorsement of a particular advertisement when the space is open to a broad 
  spectrum of commercial and noncommercial views. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">Finally, the plaintiff 
  asserts that questions 2(a)(2) and 2(a)(3) indicate the court's fundamental 
  misunderstanding of the law concerning expression by private parties in designated 
  public fora - in this case, advertising space that the MBTA has indisputably 
  opened up to the public for political expression of views held by the public. 
  See Grace Bible Fellowship, Inc. v. Maine Sch. Admin. Dist. No. 5, 941 F.2d 
  45, 47 (1st Cir. 1991); Lebron v. Washington Metro. Transit Auth.,749 F.2d 893, 
  896 (D.C. Cir. 1984); New York Magazine v. Metropolitan Transport. Auth., 136 
  F.3d 123, 130 (2d Cir. 1998); Christ's Bride Ministries, Inc. v. Southeastern 
  Pa. Transp. Auth., 148 F.3d 242, 252-55 (3d Cir. 1998), cert. denied, 525 U.S. 
  1068 (1999); United Food &amp; Commercial Workers Union Local 1099 v. Southwest 
  Ohio Reg'l Transit Auth., 163 F.3d 341, 353 (6th Cir. 1998); Planned Parenthood 
  Ass'n v. Chicago Transit Auth., 767 F.2d 1225, 1232 (7th Cir. 1985). Accord 
  Air Line Pilots Ass'n Int'l v. Dept. of Aviation, 45 F. 3d 1144, 1153 (7th Cir. 
  1995);Children of the Rosary v. City of Phoenix, 154 F.3d 972, 978 (9th Cir. 
  1998) (citing leading cases with approval) Hawkins v. City and County of Denver, 
  170 F.3d 1281, 1287 (10th Cir. 1999) (citing Christ's Bride as instructive). 
  Cf. Hopper v. City of Pasco, 241 F.3d 1067, 1078 (9th Cir. 2001); East Timor 
  Action Network, Inc. v. City of New York, 71 F. Supp. 334, 345-46 (S.D.N.Y. 
  1999); National Abortion Federation v. Metropolitan Atlanta Rapid Transit Auth., 
  112 F. Supp.2d 1320, 1327 (N.D. Ga. 2000).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">There are simply no cases 
  involving political speech in a public forum in which a court refused to order 
  the display of private speech because the message was one with which the government 
  did not agree. Indeed, the very point of the &quot;viewpoint discrimination&quot; 
  cases is that the government may not prohibit speech in any forum, whether a 
  traditional, designated or nonpublic forum, because it disagrees with the message 
  expressed. </font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The only cases in which 
  any consideration is given to whether private speech in a public forum may be 
  misperceived as the government's endorsement of the message are cases involving 
  religious speech, where the government has a compelling interest in preventing 
  the appearance that it endorses religion, prohibited by the Establishment Clause. 
  See, e.g., Capitol Square Review and Advisory Board v. Pinette, 515 U.S. 753 
  (1995) (affirming order that Klan be allowed to erect cross where other unattended 
  private displays have been allowed, rejecting government's claim that public 
  would misperceive religious display as having government's approval). This case 
  obviously does not involve the Establishment Clause and there is no basis for 
  the Court's questions 2(a)(2) or 2(a)(3).</font></p>
<p><font face="Verdana, Geneva, Tahoma, Arial" size="-1">The plaintiff urges the 
  Court to reconsider the analysis suggested by the proposed questions. </font></p>
